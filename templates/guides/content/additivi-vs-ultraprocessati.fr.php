    <section id="come-riconoscerli" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">5</div>
        <h2 class="guide-section-title">Comment les reconnaître en pratique : la double lecture</h2>
      </div>

      <p>
        Pour une évaluation complète, appliquez les deux critères chaque fois que vous lisez une étiquette.
      </p>

      <ol class="guide-step-list">
        <li>
          <span class="guide-step-num">1</span>
          <div class="guide-step-body">
            <strong>Repérez les ingrédients « de laboratoire »</strong>
            <p>Amidons modifiés, protéines isolées, hydrolysats, sirops de glucose-fructose, arômes « nature-identiques », huiles partiellement hydrogénées : ce sont les principaux signaux d'ultra-transformation, même sans beaucoup de codes E.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">2</span>
          <div class="guide-step-body">
            <strong>Comptez les codes E et identifiez les catégories critiques</strong>
            <p>Un E d'antioxydant naturel (E306) est très différent d'un colorant azoïque (E102) ou d'un édulcorant intense (E951). Utilisez l'application pour connaître le niveau de risque de chaque additif.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">3</span>
          <div class="guide-step-body">
            <strong>Tenez compte du contexte du repas</strong>
            <p>Un repas équilibré avec légumes, légumineuses et céréales complètes « absorbe » mieux la présence de quelques produits plus transformés. Le problème survient quand les ultra-transformés deviennent la base de l'alimentation quotidienne.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">4</span>
          <div class="guide-step-body">
            <strong>Utilisez l'application comme aide, pas comme juge absolu</strong>
            <p>E-Codes Reader vous montre la classification NOVA et les détails sur les additifs. Servez-vous-en pour mieux comprendre les produits, pas pour créer de l'anxiété. L'objectif est de réduire les ultra-transformés dans le temps, pas de tous les éliminer en un jour.</p>
          </div>
        </li>
      </ol>

      <div class="guide-insight">
        <strong>Résumé en une phrase :</strong> un produit avec un E naturel n'est pas un problème. Un produit avec 15 ingrédients industriels sans aucun E est quand même un ultra-transformé. Regardez les deux aspects.
      </div>
    </section>

