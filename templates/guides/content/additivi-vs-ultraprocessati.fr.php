    <!-- SECTION 1 -->
    <section id="cosa-sono-additivi" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">1</div>
        <h2 class="guide-section-title">Qu'est-ce que les additifs alimentaires</h2>
      </div>

      <p>
        Les additifs alimentaires sont des substances intentionnellement ajoutées aux aliments pour remplir une fonction technologique : conserver, colorer, épaissir, émulsifier, stabiliser ou améliorer le goût. En Europe, chaque additif approuvé reçoit un code <strong>E</strong> (E100, E200, E300...) et doit passer une évaluation de sécurité de l'EFSA.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🔵</span>
          <div class="guide-checklist__body">
            <strong>Tous les additifs ne sont pas synthétiques</strong>
            <p>L'acide citrique (E330), la curcumine (E100), la lécithine de soja (E322) et l'acide ascorbique (E300, vitamine C) sont des additifs d'origine naturelle, présents également dans de très nombreux aliments non transformés.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🔵</span>
          <div class="guide-checklist__body">
            <strong>Tous les additifs ne sont pas dangereux</strong>
            <p>La grande majorité des E approuvés est considérée comme sûre aux doses d'utilisation autorisées. Le problème n'est pas la présence d'un E, mais la <em>quantité</em> et la <em>combinaison</em> de nombreux additifs différents dans le même produit.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">⚠️</span>
          <div class="guide-checklist__body">
            <strong>Certains additifs méritent attention</strong>
            <p>Colorants azoïques (E102, E110, E122, E124, E129), conservateurs comme les nitrates (E249–E252), édulcorants intenses (E950–E962) : ces catégories présentent des preuves de risque plus consistantes, surtout pour les enfants.</p>
          </div>
        </li>
      </ul>
    </section>

    <hr class="guide-divider" />

    <!-- SECTION 2 -->
    <section id="cosa-sono-ultraprocessati" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">2</div>
        <h2 class="guide-section-title">Qu'est-ce que les aliments ultra-transformés</h2>
      </div>

      <p>
        Le terme « ultra-transformé » vient de la <strong>classification NOVA</strong>, développée par l'Université de São Paulo. Il ne concerne pas les nutriments, mais le <em>procédé industriel</em> qui a produit l'aliment.
      </p>

      <p>
        Un aliment est ultra-transformé (NOVA 4) lorsqu'il contient des ingrédients qu'on ne trouve pas dans une cuisine domestique : protéines isolées, amidons modifiés, hydrolysats de protéines, sirops de glucose-fructose, arômes « nature-identiques », émulsifiants, colorants, stabilisants — souvent en combinaisons complexes.
      </p>

      <div class="signal-cards">
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🚩</span>
          <div class="signal-card__content">
            <strong>Exemples d'ultra-transformés courants</strong>
            <p>Gâteaux industriels, snacks emballés, céréales sucrées du matin, boissons gazeuses et jus « à base de », saucisses et charcuteries reconstituées, plats surgelés prêts à l'emploi avec sauces, yaourts aromatisés avec épaississants, pain de mie industriel avec de nombreux additifs.</p>
          </div>
        </div>
        <div class="signal-card signal-card--ok">
          <span class="signal-card__emoji">✅</span>
          <div class="signal-card__content">
            <strong>Ce qui N'est PAS ultra-transformé</strong>
            <p>Fromages affinés (peu d'additifs), charcuteries entières de qualité (même si transformées), pain artisanal, pâtes sèches traditionnelles, conserves de tomates sans additifs, vinaigre, huile d'olive extra vierge.</p>
          </div>
        </div>
      </div>
    </section>

    <hr class="guide-divider" />

    <!-- SECTION 3 -->
    <section id="quando-coincidono" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">3</div>
        <h2 class="guide-section-title">Quand ils se recoupent — et quand non</h2>
      </div>

      <p>
        C'est le point qui surprend le plus : le chevauchement entre « avec additifs » et « ultra-transformé » n'est pas total.
      </p>

      <div class="rating-grid">
        <div class="rating-block rating-block--green">
          <div class="rating-block__dot">✅</div>
          <div class="rating-block__body">
            <strong>Additifs mais PAS ultra-transformé</strong>
            <ul>
              <li>Fromage avec conservateur E252 (nitrate de potassium) : transformé mais pas NOVA 4</li>
              <li>Vin avec sulfites (E220) : additif naturel, produit traditionnel</li>
              <li>Huile avec vitamine E (E306) comme antioxydant : transformation minimale</li>
            </ul>
          </div>
        </div>
        <div class="rating-block rating-block--red">
          <div class="rating-block__dot">🚩</div>
          <div class="rating-block__body">
            <strong>Ultra-transformé avec PEU d'additifs E</strong>
            <ul>
              <li>Pain de mie avec amidon modifié et arômes : peu de codes E mais NOVA 4</li>
              <li>Barres protéinées avec protéines isolées et sirop de riz : peu de codes, mais fortement transformées</li>
              <li>Surimi : presque aucun E dans la liste, mais produit à partir de protéines de poisson reconstituées</li>
            </ul>
          </div>
        </div>
        <div class="rating-block rating-block--yellow">
          <div class="rating-block__dot">⚠️</div>
          <div class="rating-block__body">
            <strong>Ultra-transformé ET avec de nombreux additifs (le pire cas)</strong>
            <ul>
              <li>Gâteaux industriels : amidons modifiés + colorants + émulsifiants + arômes + édulcorants</li>
              <li>Sauces industrielles : conservateurs + stabilisants + exhausteurs de goût + colorants</li>
              <li>Boissons énergisantes : édulcorants + colorants + acidifiants + arômes complexes</li>
            </ul>
          </div>
        </div>
      </div>
    </section>

