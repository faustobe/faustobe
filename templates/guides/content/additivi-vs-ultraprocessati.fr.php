    <!-- SECTION 4 -->
    <section id="doppio-problema" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">4</div>
        <h2 class="guide-section-title">Le double problème : pourquoi les deux comptent</h2>
      </div>

      <p>
        Se concentrer uniquement sur les additifs ou uniquement sur le degré de transformation mène à des évaluations partielles. Les risques pour la santé viennent des deux directions.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🧬</span>
          <div class="guide-checklist__body">
            <strong>Risques liés à des additifs spécifiques</strong>
            <p>Certains additifs ont des effets documentés : les colorants azoïques et l'hyperactivité chez les enfants (EFSA 2007), les nitrates dans la viande transformée et les nitrosamines cancérigènes, les édulcorants intenses et la possible altération du microbiome intestinal.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🏭</span>
          <div class="guide-checklist__body">
            <strong>Risques liés à l'ultra-transformation en elle-même</strong>
            <p>Des études à grande échelle (NutriNet-Santé, UK Biobank) associent une consommation élevée de NOVA 4 à un risque accru d'obésité, de diabète de type 2, de maladies cardiovasculaires et de certains cancers — indépendamment de la qualité nutritionnelle.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🔁</span>
          <div class="guide-checklist__body">
            <strong>L'effet de combinaison</strong>
            <p>Les études sur les additifs testent presque toujours une substance à la fois. Mais dans la vie réelle, nous consommons des combinaisons de 10 à 20 additifs différents au cours du même repas. Les effets synergiques sont encore peu étudiés.</p>
          </div>
        </li>
      </ul>
    </section>

