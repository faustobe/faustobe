    <section id="regola-del-5" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">5</div>
        <h2 class="guide-section-title">Die 5er-Regel: der schnellste Filter</h2>
      </div>

      <p>
        Du musst kein Biochemie-Experte werden, um bessere Entscheidungen zu treffen. Die &bdquo;5er-Regel&ldquo; ist ein praktischer Filter, der in wenigen Sekunden vor dem Regal angewendet werden kann.
      </p>

      <ol class="guide-step-list">
        <li>
          <span class="guide-step-num">1</span>
          <div class="guide-step-body">
            <strong>Mehr als 5 Zutaten? Dann genauer lesen</strong>
            <p>Es ist keine absolute Regel (eine gute handwerkliche Granola kann 7&ndash;8 Zutaten haben), aber ein erster Filter: Die meisten einfachen und wenig verarbeiteten Produkte haben weniger als 5 Zutaten.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">2</span>
          <div class="guide-step-body">
            <strong>Zucker &uuml;ber 5 g/100 g? Pr&uuml;fen, ob er an erster oder zweiter Stelle steht</strong>
            <p>5 g/100 g ist die EU-Grenze f&uuml;r &bdquo;geringen Zuckergehalt&ldquo;. Dar&uuml;ber ist es nur eine Information &mdash; kein Verbot &mdash; aber ein Hinweis, den Ursprung des Zuckers zu pr&uuml;fen.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">3</span>
          <div class="guide-step-body">
            <strong>Salz &uuml;ber 0,5 g/100 g in einem s&uuml;&szlig;en Produkt? Signal f&uuml;r Ultra-Verarbeitung</strong>
            <p>Kekse, M&uuml;sli und s&uuml;&szlig;e Snacks sollten nicht viel Salz enthalten. Wenn doch, dient es oft dazu, den k&uuml;nstlichen Geschmack von Zuckerersatzstoffen zu &uuml;berdecken oder den Mangel an echten Zutaten auszugleichen.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">4</span>
          <div class="guide-step-body">
            <strong>Mehr als 5 E-Zus&auml;tze? Das Produkt ist wahrscheinlich ultra-verarbeitet</strong>
            <p>Es z&auml;hlt nicht die Art des E, sondern die Menge. Ein Produkt mit 7&ndash;8 verschiedenen Zus&auml;tzen (Farbstoffe + Emulgatoren + S&uuml;&szlig;ungsmittel + Konservierungsstoffe + Aromen) ben&ouml;tigt all diese Chemie nicht, wenn die Qualit&auml;t der Grundzutaten hoch ist.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">5</span>
          <div class="guide-step-body">
            <strong>5 oder mehr Zutaten nicht erkannt? Zur&uuml;ck ins Regal damit</strong>
            <p>Wenn du nicht wei&szlig;t, was &bdquo;Maltodextrine&ldquo;, &bdquo;Calciumcaseinat&ldquo;, &bdquo;modifizierte Tapiokast&auml;rke&ldquo;, &bdquo;E471&ldquo; und &bdquo;naturidentische Aromen&ldquo; sind &mdash; und sie alle im selben Produkt erscheinen &mdash; hast du ein Ultra-Verarbeitetes gefunden, das man einschr&auml;nken sollte.</p>
          </div>
        </li>
      </ol>

      <div class="guide-insight">
        <strong>Beachte:</strong> Die 5er-Regel ist eine Orientierung, kein Urteil. Eine ausgezeichnete zertifizierte Vollkornnudel kann mehr als 5 Zutaten haben; ein billiger Fertigsnack kann knapp darunter liegen. Verwende sie als Ausgangspunkt, nicht als einziges Kriterium.
      </div>
    </section>

