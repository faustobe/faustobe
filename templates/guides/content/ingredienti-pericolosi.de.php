            <p>Brot (die gr&ouml;&szlig;te Natriumquelle in der Ern&auml;hrung), K&auml;se, Aufschnitt, Fertiggerichte, So&szlig;en im Glas, W&uuml;rfelbrr&uuml;he und Instant-Brr&uuml;hen, Sojasosse, Ketchup, Mayonnaise, salzige Snacks, industrielle Fr&uuml;hst&uuml;cksflocken.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🔢</span>
          <div class="guide-checklist__body">
