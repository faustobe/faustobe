        <h2 class="guide-section-title">Fette, die man meiden sollte: hydrierte Fette und Transfetts&auml;uren</h2>
      </div>

      <p>
        Nicht alle Fette sind gleich. Transfetts&auml;uren (TFA), die bei der industriellen Hydrierung pflanzlicher &Ouml;le entstehen, sind mit einem erh&ouml;hten Herz-Kreislauf-Risiko verbunden und wurden in vielen L&auml;ndern verboten oder stark eingeschr&auml;nkt.
      </p>

      <div class="signal-cards">
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🚩</span>
          <div class="signal-card__content">
            <strong>Hydrierte und teilweise hydrierte Fette</strong>
            <p>Auf dem Etikett suchen nach: &bdquo;teilweise hydriertes Pflanzen&ouml;l&ldquo;, &bdquo;hydriertes Pflanzenfett&ldquo;, &bdquo;Shortening&ldquo;. In Europa sind sie seit 2021 &uuml;ber 2 g/100 g Fett verboten, aber einige Ausnahmen bleiben. Auf jeden Fall meiden.</p>
          </div>
        </div>
        <div class="signal-card signal-card--warning">
          <span class="signal-card__emoji">⚠️</span>
          <div class="signal-card__content">
            <strong>Palmfett: nicht trans, aber&hellip;</strong>
            <p>Palmfett ist ges&auml;ttigt, aber nicht hydriert. Das Problem ist ein anderes: Bei der Raffination bei hohen Temperaturen entstehen m&ouml;glicherweise krebserregende Verbindungen (3-MCPD, Glycidol). Es ist nicht verboten, aber besser zu begrenzen.</p>
          </div>
        </div>
        <div class="signal-card signal-card--ok">
          <span class="signal-card__emoji">✅</span>
          <div class="signal-card__content">
            <strong>Unbedenkliche Fette</strong>
            <p>Natives Oliven&ouml;l extra, Lein&ouml;l, N&uuml;sse und &Ouml;lsaaten, Avocado, Fette aus fettem Fisch. Diese enthalten einfach und mehrfach unges&auml;ttigte Fetts&auml;uren und Omega-3, die dokumentierte sch&uuml;tzende Wirkungen haben.</p>
          </div>
        </div>
      </div>
    </section>

