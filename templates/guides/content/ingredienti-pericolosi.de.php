            <p>Wenn auf dem Etikett &bdquo;Natrium&ldquo; statt &bdquo;Salz&ldquo; steht: den Wert mit 2,5 multiplizieren. Beispiel: 0,6 g Natrium = 1,5 g Salz. Die WHO-Grenze liegt bei 5 g Salz pro Tag (etwa 2 g Natrium).</p>
          </div>
