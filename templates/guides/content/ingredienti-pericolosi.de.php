        <h2 class="guide-section-title">Zucker: die 10 versteckten Namen auf Etiketten</h2>
      </div>

      <p>
        Zucker hei&szlig;t nicht immer &bdquo;Zucker&ldquo;. Hersteller verwenden Dutzende von Bezeichnungen, um ihn auf mehrere Positionen in der Zutatenliste zu verteilen &mdash; und ihn weniger bedeutsam erscheinen zu lassen. Wer diese Namen kennt, l&auml;sst sich nicht mehr t&auml;uschen.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🔍</span>
          <div class="guide-checklist__body">
            <strong>Die 10 h&auml;ufigsten Synonyme</strong>
            <p>
              <strong>Glukose-Fruktose-Sirup</strong> (der schlimmste: wird anders metabolisiert als Haushaltszucker) &middot;
              <strong>Maltodextrine</strong> (fast vollst&auml;ndig abgebaute St&auml;rke) &middot;
              <strong>Dextrose</strong> &middot;
              <strong>Fruktose</strong> &middot;
              <strong>Konzentrierter Rohrzuckersaft</strong> &middot;
              <strong>Maissirup</strong> &middot;
              <strong>Reissirup</strong> &middot;
              <strong>Getrockneter Honig</strong> &middot;
              <strong>Kokoszucker</strong> &middot;
              <strong>Agavennektar</strong>
            </p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">⚠️</span>
          <div class="guide-checklist__body">
            <strong>Die Falle &bdquo;ohne Zuckerzusatz&ldquo;</strong>
            <p>Ein Saft &bdquo;100 % Frucht ohne Zuckerzusatz&ldquo; kann 10&ndash;12 g Zucker pro 100 ml enthalten &mdash; mehr als eine Cola (10,6 g/100 ml). Der Fruktosegehalt aus konzentriertem Saft ist chemisch identisch mit zugesetztem Zucker.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">📊</span>
          <div class="guide-checklist__body">
            <strong>Der Richtwert</strong>
            <p>Die WHO empfiehlt, den Anteil freier Zucker (zugesetzt + aus S&auml;ften) unter <strong>10 % der t&auml;glichen Kalorien</strong> zu halten &mdash; etwa 50 g pro Tag f&uuml;r einen Erwachsenen mit 2000 kcal. Vorzugsweise unter 5 % (25 g).</p>
          </div>
        </li>
      </ul>
