            <p>Mononatriumglutamat (E621), Natriumbenzoat (E211), Natriumnitrit (E250), Natriumbicarbonat, diNatriumhydrogenphosphat &mdash; all diese tragen zum Gesamtnatrium bei, auch wenn sie auf dem Etikett nicht als &bdquo;Salz&ldquo; ausgewiesen werden.</p>
