      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">1</div>
        <h2 class="guide-section-title">Avant de scanner, faites trois vérifications rapides</h2>
      </div>

      <p>
        Avant même d'ouvrir l'app, observez l'emballage. Les étiquettes recto sont souvent conçues pour faire paraître les produits plus sains qu'ils ne le sont vraiment.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">👁️</span>
          <div class="guide-checklist__body">
            <strong>Regardez le recto de l'emballage</strong>
            <p>Méfiez-vous des allégations comme « sans sucre mais avec édulcorants », « sans matières grasses mais avec émulsifiants », « goût extra » ou « aromatisé ». Elles indiquent souvent que le produit a été fortement modifié.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">📱</span>
          <div class="guide-checklist__body">
            <strong>Ouvrez l'app et scannez le produit</strong>
            <p>Utilisez l'appareil photo pour scanner le code-barres ou importez une photo de l'étiquette. L'app vous affichera immédiatement la liste des ingrédients et des additifs présents.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🔍</span>
          <div class="guide-checklist__body">
            <strong>Vérifiez trois points clés</strong>
            <p>Combien d'<strong>additifs</strong> apparaissent (surtout les codes E) ? La liste des ingrédients est-elle <strong>longue et complexe</strong> ? Quelle quantité de <strong>sucres, sel et graisses</strong> est déclarée ?</p>
          </div>
        </li>
      </ul>

      <div class="guide-insight">
        Si vous répondez « beaucoup » ou « longue/compliquée » à plus d'une question, vous êtes probablement face à un aliment très transformé.
      </div>
    </section>

