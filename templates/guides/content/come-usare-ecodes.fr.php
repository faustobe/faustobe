      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">4</div>
        <h2 class="guide-section-title">Comment utiliser l'app concrètement en faisant ses courses</h2>
      </div>

      <p>
        Intégrer l'app dans votre routine de courses est plus simple qu'il n'y paraît. Suivez ces quatre étapes la première fois : elles deviendront vite un réflexe naturel.
      </p>

      <ol class="guide-step-list">
        <li>
          <span class="guide-step-num">1</span>
          <div class="guide-step-body">
            <strong>Commencez par le rayon frais et petit-déjeuner</strong>
            <p>Scannez yaourts, pain, lait, boissons. Cherchez des produits avec peu d'ingrédients, peu de codes E, et des sucres simples (fruits, lait, sucre de canne) plutôt que des sirops et édulcorants.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">2</span>
          <div class="guide-step-body">
            <strong>Passez ensuite aux sucreries et snacks</strong>
            <p>Vérifiez les gâteaux, snacks salés, biscuits, pâtisseries industrielles. Si la liste est très longue et pleine de E, l'app affichera du rouge. Dans ce cas, choisissez toujours l'alternative plus simple (fruits, pain complet, noix).</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">3</span>
          <div class="guide-step-body">
            <strong>Utilisez l'app pour comparer des produits similaires</strong>
            <p>Cherchez deux produits du même type (ex. yaourt grec, pain, snacks) et comparez leurs fiches dans l'app. Souvent celui qui a moins d'ingrédients est juste légèrement moins attrayant en rayon, mais bien plus naturel.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">4</span>
          <div class="guide-step-body">
            <strong>Réservez les « rouges » à l'exception, pas à la règle</strong>
            <p>Inutile d'éliminer tout ce qui est ultra-transformé : il suffit de le réduire. Fixez-vous un seuil — par exemple, maximum 1–2 produits « rouges » par semaine — tandis que le reste de vos courses se compose d'aliments simples.</p>
          </div>
        </li>
      </ol>
    </section>

