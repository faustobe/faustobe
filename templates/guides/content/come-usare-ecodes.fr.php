            <p>Des termes comme « amidons modifiés », « protéines isolées », « mono- et diglycérides d'acides gras », « exhausteurs de goût » ou « arômes complexes » signalent clairement un aliment très transformé.</p>
          </div>
        </div>
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🚩</span>
          <div class="signal-card__content">
            <strong>Beaucoup de codes E et d'additifs</strong>
            <p>Tous les E ne sont pas dangereux, mais leur présence en masse — colorants, conservateurs, stabilisants, émulsifiants — indique un degré élevé de transformation industrielle.</p>
          </div>
        </div>
      </div>
