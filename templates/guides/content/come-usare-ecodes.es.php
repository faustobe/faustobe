          <span class="guide-checklist__icon">👁️</span>
          <div class="guide-checklist__body">
            <strong>Mira el frente del envase</strong>
            <p>Presta atención a reclamos como "sin azúcar pero con edulcorantes", "sin grasas pero con emulsionantes", "sabor extra" o "aromatizado". Suelen indicar que el producto ha sido muy modificado.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">📱</span>
          <div class="guide-checklist__body">
            <strong>Abre la app y escanea el producto</strong>
            <p>Usa la cámara para escanear el código de barras o sube una foto de la etiqueta. La app te mostrará de inmediato la lista de ingredientes y los aditivos presentes.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🔍</span>
          <div class="guide-checklist__body">
            <strong>Comprueba tres puntos clave</strong>
            <p>¿Cuántos <strong>aditivos</strong> aparecen (sobre todo los códigos E)? ¿Qué tan <strong>larga y compleja</strong> es la lista de ingredientes? ¿Cuántos <strong>azúcares, sal y grasas</strong> se declaran?</p>
          </div>
        </li>
      </ul>

      <div class="guide-insight">
