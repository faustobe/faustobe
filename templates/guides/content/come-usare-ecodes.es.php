      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">4</div>
        <h2 class="guide-section-title">Cómo usar la app de forma práctica en el supermercado</h2>
      </div>

      <p>
        Integrar la app en tu rutina de compra es más sencillo de lo que parece. Sigue estos cuatro pasos la primera vez: se convertirán en un hábito natural.
      </p>

      <ol class="guide-step-list">
        <li>
          <span class="guide-step-num">1</span>
          <div class="guide-step-body">
            <strong>Empieza por la sección de refrigerados y desayuno</strong>
            <p>Escanea yogures, pan, leche, bebidas. Busca productos con pocos ingredientes, pocos E, y azúcares simples (fruta, leche, azúcar de caña) en lugar de jarabes y edulcorantes.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">2</span>
          <div class="guide-step-body">
            <strong>Luego pasa a los dulces y snacks</strong>
            <p>Revisa bollería, snacks salados, galletas, dulces envasados. Si la lista es larguísima y está llena de E, la app marcará en rojo. En esos casos, elige siempre la alternativa más sencilla (fruta, pan integral, frutos secos).</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">3</span>
          <div class="guide-step-body">
            <strong>Usa la app para comparar productos similares</strong>
            <p>Busca dos productos del mismo tipo (p. ej. yogur griego, pan, snacks) y compara sus fichas en la app. A menudo el que tiene menos ingredientes es solo un poco menos llamativo en el estante, pero mucho más natural.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">4</span>
          <div class="guide-step-body">
            <strong>Deja los "rojos" como excepción, no como norma</strong>
            <p>No hace falta eliminar todo lo ultraprocesado: basta con reducirlo. Fíjate un límite — por ejemplo, máximo 1–2 productos "rojos" a la semana — mientras el resto de la compra se base en alimentos sencillos.</p>
          </div>
        </li>
      </ol>
    </section>

