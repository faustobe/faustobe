    <section id="infiammazione-immunita" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">2</div>
        <h2 class="guide-section-title">Infiammazioni e disfunzioni del sistema immunitario</h2>
      </div>

      <p>
        Il consumo frequente di ultra-processati è associato a un aumento delle risposte immunitarie infiammatorie nell'organismo — un effetto che va ben oltre il semplice profilo nutrizionale del prodotto.
      </p>

      <p>
        Alcune sostanze comuni negli UPF — emulsionanti, addensanti e microparticelle come il <strong>biossido di titanio (E171)</strong> — possono alterare il microbiota intestinale e aumentare la permeabilità della mucosa intestinale. Questo fenomeno, noto come <em>leaky gut</em> o "intestino permeabile", permette a batteri patogeni e sostanze nocive di entrare nel sangue, innescando infiammazione sistemica cronica.
      </p>

      <div class="signal-cards">
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🚩</span>
          <div class="signal-card__content">
            <strong>Additivi che aumentano la permeabilità intestinale</strong>
            <p>Emulsionanti come la <strong>carbossimetilcellulosa (E466)</strong> e il <strong>polisorbato 80 (E433)</strong> alterano il muco protettivo intestinale anche a dosi consentite dalla normativa, secondo studi su modelli animali e colture cellulari.</p>
          </div>
        </div>
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🚩</span>
          <div class="signal-card__content">
            <strong>Biomarcatori infiammatori elevati</strong>
            <p>Chi consuma molti UPF presenta livelli più alti di <strong>proteina C-reattiva</strong> e altri biomarcatori proteici infiammatori circolanti, associati a un maggiore rischio di mortalità cardiovascolare.</p>
          </div>
        </div>
      </div>

      <p>Le disfunzioni immunitarie croniche indotte dagli UPF sono associate a un rischio maggiore di sviluppare malattie autoimmuni:</p>

      <div class="rating-grid">
        <div class="rating-block rating-block--red">
          <div class="rating-block__dot">⚠️</div>
          <div class="rating-block__body">
            <strong>Malattie autoimmuni associate al consumo elevato di UPF</strong>
            <ul>
              <li>Celiachia</li>
              <li>Tiroidite di Hashimoto</li>
              <li>Sclerosi multipla</li>
              <li>Lupus eritematoso sistemico</li>
              <li>Diabete di tipo 1</li>
            </ul>
          </div>
        </div>
      </div>

      <div class="guide-insight">
        Il legame tra UPF e malattie autoimmuni non è ancora completamente compreso, e la correlazione non implica causalità diretta. Tuttavia la convergenza di più studi indipendenti su popolazioni diverse rende l'associazione degna di attenzione clinica.
      </div>
    </section>

