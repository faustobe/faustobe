      </div>

      <p>
        Le evidenze scientifiche sono solide, ma il messaggio pratico non è "elimina tutto". È <strong>riduci la quota di UPF nella dieta quotidiana</strong> e sostituiscili con alternative semplici, quando disponibili e accessibili.
      </p>

      <ol class="guide-step-list">
        <li>
          <span class="guide-step-num">1</span>
          <div class="guide-step-body">
            <strong>Individua i tuoi UPF abituali</strong>
            <p>Cereali da colazione dolci, snack confezionati, pane da cassetta con additivi, piatti pronti, bevande aromatizzate: sono questi i prodotti che, consumati quotidianamente, fanno la differenza. Non il gelato una volta al mese.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">2</span>
          <div class="guide-step-body">
            <strong>Sostituisci uno alla volta</strong>
            <p>Non serve una rivoluzione. Avena in fiocchi al posto dei cereali dolci. Yogurt greco naturale al posto di quello aromatizzato con addensanti. Pane artigianale al posto del pane da cassetta con conservanti. Ogni sostituzione riduce il carico cumulativo.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">3</span>
          <div class="guide-step-body">
            <strong>Aumenta le fibre — sono la priorità</strong>
            <p>Legumi, verdure, cereali integrali in chicco, frutta intera: le fibre nutrono i batteri benefici del microbiota e contrastano direttamente molti degli effetti negativi degli UPF. Non richiedono di "eliminare" nulla, solo di aggiungere.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">4</span>
          <div class="guide-step-body">
            <strong>Usa l'app per identificare gli UPF nascosti</strong>
