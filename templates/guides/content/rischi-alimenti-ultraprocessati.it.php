    <section id="obesita-metabolismo" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">1</div>
        <h2 class="guide-section-title">Obesità e alterazione del metabolismo</h2>
      </div>

      <p>
        Gli alimenti ultra-processati non fanno ingrassare semplicemente perché sono calorici. Il meccanismo è più insidioso: sono progettati per essere <strong>iper-appetibili</strong> e interferiscono attivamente con i segnali naturali di sazietà del corpo.
      </p>

      <div class="guide-insight">
        <strong>Lo studio di Kevin Hall (NIH, 2019):</strong> in un trial clinico controllato, i partecipanti esposti a una dieta a base di ultra-processati consumavano in media <strong>500 calorie in più al giorno</strong> rispetto a chi mangiava cibi minimamente lavorati — a parità di accessibilità al cibo e libertà di scelta. In sole due settimane, il gruppo UPF aveva guadagnato quasi un chilo.
      </div>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🔥</span>
          <div class="guide-checklist__body">
            <strong>Alta densità energetica</strong>
            <p>Gli UPF contengono in media <strong>2,15 kcal per grammo</strong> — quasi il doppio rispetto ai cibi freschi. Lo stesso volume di cibo fornisce molte più calorie, senza la sazietà corrispondente.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🧠</span>
          <div class="guide-checklist__body">
            <strong>Stimolazione eccessiva dei centri del piacere</strong>
            <p>La combinazione industriale di zucchero, sale, grassi e aromi attiva il sistema dopaminergico in modo simile a quello di sostanze d'abuso. Il risultato: si mangia anche in assenza di fame reale.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">📉</span>
          <div class="guide-checklist__body">
            <strong>Riduzione del peptide YY</strong>
            <p>Gli UPF abbassano i livelli del <strong>peptide YY</strong>, l'ormone intestinale che segnala al cervello la sazietà. Chi mangia molti ultra-processati riceve meno segnali di "stop" dopo i pasti.</p>
          </div>
        </li>
      </ul>

      <div class="signal-cards">
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">📊</span>
          <div class="signal-card__content">
            <strong>+41% di rischio di obesità addominale</strong>
            <p>Chi segue una dieta ad alto contenuto di UPF presenta un rischio superiore del 41% di sviluppare obesità o obesità addominale rispetto a chi ne consuma pochi.</p>
          </div>
        </div>
      </div>
    </section>

