          </div>
        </li>
        <li>
          <span class="guide-step-num">5</span>
          <div class="guide-step-body">
            <strong>Non puntare alla perfezione</strong>
            <p>L'obiettivo non è una dieta "pura". È che la maggior parte di quello che mangi ogni settimana sia cibo vero — e che gli ultra-processati rimangano l'eccezione, non la base. Anche un miglioramento del 20-30% nella qualità della dieta produce effetti misurabili sulla salute nel medio termine.</p>
