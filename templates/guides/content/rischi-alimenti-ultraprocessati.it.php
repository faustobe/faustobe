    <section id="microbiota-intestino" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">3</div>
        <h2 class="guide-section-title">Salute dell'intestino e microbiota</h2>
      </div>

      <p>
        Il processo di ultra-trasformazione industriale priva gli alimenti di <strong>fibre alimentari</strong> e <strong>composti vegetali bioattivi</strong> (polifenoli, antiossidanti) necessari per nutrire i batteri "buoni" dell'intestino. Al loro posto, abbondano zuccheri aggiunti, sale, dolcificanti artificiali e additivi che favoriscono uno squilibrio del microbiota — la cosiddetta <strong>disbiosi</strong>.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🦠</span>
          <div class="guide-checklist__body">
            <strong>Disbiosi: meno diversità batterica, più rischi</strong>
            <p>Un microbiota impoverito — con meno specie batteriche benefiche — è associato a insulino-resistenza, infiammazione cronica e maggiore vulnerabilità a infezioni e malattie metaboliche. Gli UPF riducono attivamente questa diversità.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🧱</span>
          <div class="guide-checklist__body">
            <strong>Indebolimento dello strato mucoso protettivo</strong>
            <p>L'abbondanza di emulsionanti e dolcificanti artificiali indebolisce lo strato di muco che riveste l'intestino, riducendo la barriera fisica tra i batteri intestinali e la parete del colon.</p>
          </div>
        </li>
      </ul>

      <p>Studi osservazionali mostrano che chi consuma elevate quantità di UPF soffre più frequentemente di:</p>

      <div class="signal-cards">
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🔴</span>
          <div class="signal-card__content">
            <strong>Malattie infiammatorie croniche intestinali</strong>
            <p>Morbo di Crohn e colite ulcerosa — due condizioni in forte aumento nei paesi a più alto consumo di ultra-processati.</p>
          </div>
        </div>
        <div class="signal-card signal-card--warning">
          <span class="signal-card__emoji">⚠️</span>
          <div class="signal-card__content">
            <strong>Disturbi funzionali e precancerosi</strong>
            <p>Sindrome dell'intestino irritabile, ulcere gastriche e polipi precancerosi del colon sono più frequenti tra i consumatori abituali di UPF.</p>
          </div>
        </div>
      </div>
    </section>

