      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">4</div>
        <h2 class="guide-section-title">Un rischio sistemico: più meccanismi insieme</h2>
      </div>

      <p>
        Ciò che rende gli UPF particolarmente preoccupanti non è un singolo effetto negativo, ma la convergenza di più meccanismi che si rinforzano a vicenda.
      </p>

      <div class="guide-table-wrap">
        <table>
          <thead>
            <tr>
              <th>Meccanismo</th>
              <th>Effetto</th>
              <th>Conseguenza a lungo termine</th>
            </tr>
          </thead>
          <tbody>
            <tr>
              <td>Iper-appetibilità</td>
              <td>Consumo eccessivo di calorie</td>
              <td>Obesità, diabete tipo 2</td>
            </tr>
            <tr>
              <td>Carenza di fibre</td>
              <td>Disbiosi intestinale</td>
              <td>Infiammazione cronica, IBD</td>
            </tr>
            <tr>
              <td>Additivi (emulsionanti)</td>
              <td>Permeabilità intestinale</td>
              <td>Malattie autoimmuni, cardiovascolari</td>
            </tr>
            <tr>
              <td>Zuccheri e dolcificanti</td>
              <td>Insulino-resistenza, disbiosi</td>
              <td>Diabete tipo 2, sindrome metabolica</td>
            </tr>
            <tr>
              <td>Sostituzione del cibo fresco</td>
              <td>Carenza di micronutrienti</td>
              <td>Stress ossidativo, immunodepressione</td>
            </tr>
          </tbody>
        </table>
      </div>

      <p>
        In sintesi, gli UPF agiscono come un <strong>fattore di rischio sistemico</strong> che promuove contemporaneamente insulino-resistenza, stress ossidativo e infiammazione cronica di basso grado — tre condizioni che stanno alla base di quasi tutte le malattie croniche non trasmissibili più diffuse in Occidente.
      </p>

      <div class="guide-insight">
        <strong>I grandi studi epidemiologici lo confermano:</strong> NutriNet-Santé (Francia, oltre 100.000 partecipanti) e UK Biobank (UK, oltre 500.000 partecipanti) associano il consumo elevato di UPF a maggiori rischi di cancro, malattie cardiovascolari, depressione e mortalità per tutte le cause — indipendentemente dalla qualità nutrizionale dei singoli prodotti.
      </div>
    </section>

