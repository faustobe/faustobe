    <section id="piu-vulnerabili" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">1</div>
        <h2 class="guide-section-title">Pourquoi les enfants et les personnes âgées sont plus vulnérables</h2>
      </div>

      <p>
        La même dose d'additif qu'un adulte en bonne santé métabolise sans problème peut avoir des effets différents sur un jeune enfant ou une personne âgée. Ce n'est pas de l'alarmisme : c'est de la physiologie.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">👶</span>
          <div class="guide-checklist__body">
            <strong>Enfants : poids corporel faible, systèmes immatures</strong>
            <p>La dose journalière admissible (DJA) des additifs est calculée en mg par kg de poids corporel. Un enfant de 15 kg qui mange un goûter de 30 g avec des colorants reçoit une dose relative 4 à 5 fois supérieure à celle d'un adulte de 70 kg. De plus, le foie et les reins des jeunes enfants métabolisent certaines substances plus lentement.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">👴</span>
          <div class="guide-checklist__body">
            <strong>Personnes âgées : fonctions rénale et hépatique réduites</strong>
            <p>Avec l'âge, la capacité d'éliminer certaines substances diminue. La polymédication peut interagir avec certains additifs. Le sodium en excès a plus d'impact chez ceux qui souffrent d'hypertension ou d'insuffisance rénale.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🔬</span>
          <div class="guide-checklist__body">
            <strong>Effets cumulatifs : l'alimentation habituelle compte plus qu'un repas isolé</strong>
            <p>Une glace avec des colorants une fois par mois n'est pas un problème. Si les enfants consomment quotidiennement des produits avec E102, E110, E122 au petit-déjeuner, au goûter, au déjeuner et dans leur boisson de l'après-midi — l'effet cumulatif devient significatif.</p>
          </div>
        </li>
      </ul>
    </section>

