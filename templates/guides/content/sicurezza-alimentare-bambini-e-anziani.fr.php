    <section id="prodotti-bambini" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">4</div>
        <h2 class="guide-section-title">Produits « pour enfants » à regarder attentivement</h2>
      </div>

      <p>
        Le marketing « pour enfants » est souvent le contraire de ce qu'il semble. Les produits avec des personnages colorés, des mascottes et des mentions comme « avec des vitamines » ou « énergie pour grandir » sont souvent parmi les plus transformés du rayon.
      </p>

      <div class="signal-cards">
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🚩</span>
          <div class="signal-card__content">
            <strong>Céréales du matin « pour enfants »</strong>
            <p>Elles contiennent souvent 25–35 g de sucres pour 100 g (soit 7–9 cuillères à café pour 100 g), des colorants artificiels, des arômes complexes et des vitamines ajoutées pour compenser la faible qualité nutritionnelle des ingrédients de base. Alternative : flocons d'avoine + fruits frais + miel.</p>
          </div>
        </div>
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🚩</span>
          <div class="signal-card__content">
            <strong>Boissons « aux jus de fruits »</strong>
            <p>Des produits comme « boisson à l'orange » ou « jus multifruits » peuvent ne contenir que 10–30 % de jus réel, le reste étant de l'eau, des sucres, des acidifiants et des colorants. Ils ne sont pas équivalents aux jus à 100 %. Vérifiez toujours le pourcentage de fruits sur l'étiquette.</p>
          </div>
        </div>
        <div class="signal-card signal-card--warning">
          <span class="signal-card__emoji">⚠️</span>
          <div class="signal-card__content">
            <strong>Yaourts « pour enfants » aux fruits</strong>
            <p>Beaucoup contiennent plus de sucre qu'un yaourt adulte standard, plus d'épaississants (pectine, amidon modifié) et des arômes artificiels. Le yaourt grec nature avec des fruits frais ajoutés au moment est toujours un meilleur choix — et coûte souvent moins cher.</p>
          </div>
        </div>
      </div>
    </section>

