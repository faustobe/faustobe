    <!-- SECTION 2 -->
    <section id="additivi-bambini" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">2</div>
        <h2 class="guide-section-title">Additifs critiques pour les enfants</h2>
      </div>

      <p>
        Tous les codes E ne sont pas problématiques pour les enfants. Les catégories les plus pertinentes, avec les preuves scientifiques les plus solides, sont les suivantes.
      </p>

      <div class="rating-grid">
        <div class="rating-block rating-block--red">
          <div class="rating-block__dot">🔴</div>
          <div class="rating-block__body">
            <strong>Colorants azoïques — à éviter avec les enfants</strong>
            <ul>
              <li>E102 (Tartrazine), E104 (Jaune de quinoléine), E110 (Jaune orangé S FCF)</li>
              <li>E122 (Carmoisine), E124 (Rouge Ponceau 4R), E129 (Rouge Allura AC)</li>
              <li>En Europe, il est obligatoire d'indiquer sur l'étiquette : <em>« peut avoir des effets indésirables sur l'activité et l'attention chez les enfants »</em></li>
              <li>Où les trouver : bonbons colorés, boissons oranges/rouges, gelées, confiseries industrielles, céréales du matin colorées</li>
            </ul>
          </div>
        </div>
        <div class="rating-block rating-block--yellow">
          <div class="rating-block__dot">🟡</div>
          <div class="rating-block__body">
            <strong>Édulcorants intenses — non approuvés pour les jeunes enfants</strong>
            <ul>
              <li>E951 (Aspartame), E950 (Acésulfame K), E955 (Sucralose), E954 (Saccharine)</li>
              <li>Non autorisés dans les aliments spécifiquement destinés aux nourrissons et aux jeunes enfants (Règl. UE 1333/2008)</li>
              <li>Où les trouver souvent : yaourts « 0 % sucre », boissons « light » ou « zéro », sirops pour la toux, bonbons sans sucre, certains jus de fruits</li>
            </ul>
          </div>
        </div>
        <div class="rating-block rating-block--yellow">
          <div class="rating-block__dot">🟡</div>
          <div class="rating-block__body">
            <strong>Caféine — souvent cachée</strong>
            <ul>
              <li>Ce n'est pas un additif E, mais elle est présente dans les energy drinks, certains sodas et les snacks « au café »</li>
              <li>L'EFSA recommande que les enfants ne consomment pas plus de 3 mg/kg/jour de caféine</li>
              <li>Une canette de 250 ml d'energy drink peut contenir jusqu'à 80 mg de caféine — presque le double de la limite pour un enfant de 15 kg</li>
            </ul>
          </div>
        </div>
      </div>
    </section>

