    <!-- SECTION 3 -->
    <section id="anziani" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">3</div>
        <h2 class="guide-section-title">Ce qu'il faut limiter chez les personnes âgées</h2>
      </div>

      <p>
        Pour les personnes âgées, les priorités sont différentes de celles des enfants. Les principales problématiques concernent le sodium, les phosphates et certains conservateurs.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🧂</span>
          <div class="guide-checklist__body">
            <strong>Sodium : le risque principal</strong>
            <p>L'hypertension et l'insuffisance rénale sont parmi les affections les plus courantes chez les personnes âgées, et le sodium les aggrave. Les plats préparés, les charcuteries, les fromages affinés et les bouillons industriels en sont les principaux vecteurs. Objectif : moins de 5 g de sel par jour, et encore moins pour ceux qui ont des problèmes rénaux ou une hypertension documentée.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🦴</span>
          <div class="guide-checklist__body">
            <strong>Phosphates : ils interfèrent avec le calcium et les os</strong>
            <p>Les phosphates (E338–E341, E450–E452) sont courants dans les produits à base de viande transformée, les fromages fondus, les viandes en conserve et les plats préparés. Une consommation élevée de phosphates d'origine additive interfère avec l'absorption du calcium — un problème pour ceux qui sont à risque d'ostéoporose.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">💊</span>
          <div class="guide-checklist__body">
            <strong>Interactions avec les médicaments courants</strong>
            <p>Certaines substances dans les aliments peuvent interagir avec des médicaments courants chez les personnes âgées : la vitamine K dans les légumes à feuilles vertes avec les anticoagulants de type warfarine, le pamplemousse avec les statines, le jus de cranberry avec certains anticoagulants. Il ne s'agit pas d'éviter ces aliments, mais de maintenir une alimentation constante et de signaler ses habitudes alimentaires au médecin.</p>
          </div>
        </li>
      </ul>
    </section>

