    <section id="lista-sicura" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">5</div>
        <h2 class="guide-section-title">Liste sécurisée pour les courses</h2>
      </div>

      <p>
        Un guide pratique pour faire des achats sûrs quand on prépare les courses pour des enfants ou des personnes âgées.
      </p>

      <ol class="guide-step-list">
        <li>
          <span class="guide-step-num">1</span>
          <div class="guide-step-body">
            <strong>Pour les enfants : choisissez des produits avec peu d'ingrédients reconnaissables</strong>
            <p>Plus la liste des ingrédients est courte, mieux c'est. Évitez les produits avec plus de 2–3 colorants, des édulcorants artificiels, ou la mention « peut avoir des effets indésirables sur l'activité et l'attention chez les enfants ».</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">2</span>
          <div class="guide-step-body">
            <strong>Vérifiez toujours les sucres dans les produits « pour enfants »</strong>
            <p>Seuil : moins de 10 g/100 g pour les produits du petit-déjeuner et les encas. Au-dessus, c'est déjà un dessert, pas un aliment quotidien adapté aux enfants.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">3</span>
          <div class="guide-step-body">
            <strong>Pour les personnes âgées : priorité à la faible teneur en sodium</strong>
            <p>Privilégiez les produits avec moins de 0,3 g de sel/100 g. Choisissez le thon au naturel plutôt qu'à l'huile de graines salée, les légumes surgelés sans sel ajouté, des bouillons faits maison ou à faible teneur en sodium.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">4</span>
          <div class="guide-step-body">
            <strong>Scannez avec l'application avant d'acheter</strong>
            <p>E-Codes Reader vous indique immédiatement si un produit contient les colorants azoïques avec l'avertissement européen, les édulcorants non adaptés aux enfants, et le niveau de sodium — sans avoir à lire chaque ligne de l'étiquette.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">5</span>
          <div class="guide-step-body">
            <strong>Préférez toujours le Groupe 1–2 (NOVA) pour les repas principaux</strong>
            <p>Légumes, légumineuses, œufs, poisson, yaourt nature, fruits entiers. C'est la base la plus sûre pour les deux catégories vulnérables — sans avoir besoin de déchiffrer des étiquettes complexes.</p>
          </div>
        </li>
      </ol>

