    <section id="grassi-da-evitare" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">2</div>
        <h2 class="guide-section-title">Grasas que evitar: hidrogenadas y trans</h2>
      </div>

      <p>
        No todas las grasas son iguales. Los ácidos grasos trans (AGT), producidos por la hidrogenación industrial de los aceites vegetales, se asocian a un mayor riesgo cardiovascular y están prohibidos o muy limitados en muchos países.
      </p>

      <div class="signal-cards">
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🚩</span>
          <div class="signal-card__content">
            <strong>Grasas hidrogenadas y parcialmente hidrogenadas</strong>
            <p>Busca en la etiqueta: "aceite vegetal parcialmente hidrogenado", "grasa vegetal hidrogenada", "shortening". En Europa están prohibidas por encima de 2 g/100 g de grasa desde 2021, aunque algunas excepciones persisten. Evítalas de todas formas.</p>
          </div>
        </div>
        <div class="signal-card signal-card--warning">
          <span class="signal-card__emoji">⚠️</span>
          <div class="signal-card__content">
            <strong>Aceite de palma: no es trans, pero...</strong>
            <p>El aceite de palma es saturado pero no hidrogenado. El problema es diferente: durante el refinado a altas temperaturas se forman compuestos (3-MCPD, glicidol) potencialmente cancerígenos. No está prohibido, pero mejor limitarlo.</p>
          </div>
        </div>
        <div class="signal-card signal-card--ok">
          <span class="signal-card__emoji">✅</span>
          <div class="signal-card__content">
            <strong>Grasas que sí son buenas</strong>
            <p>Aceite de oliva virgen extra, aceite de lino, nueces y semillas oleaginosas, aguacate, grasas del pescado azul. Contienen ácidos grasos monoinsaturados y omega-3 con efectos protectores documentados.</p>
          </div>
        </div>
      </div>
    </section>

