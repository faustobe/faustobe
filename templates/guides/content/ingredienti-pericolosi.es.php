    <section id="additivi-controversi" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">3</div>
        <h2 class="guide-section-title">Aditivos controvertidos: los códigos E que debes conocer</h2>
      </div>

      <p>
        La mayoría de los códigos E en la etiqueta son seguros. Pero algunas categorías tienen un perfil de riesgo más relevante, especialmente para niños y consumidores habituales. No se trata de eliminarlos por completo, sino de ser consciente de ellos.
      </p>

      <div class="rating-grid">
        <div class="rating-block rating-block--red">
          <div class="rating-block__dot">🔴</div>
          <div class="rating-block__body">
            <strong>Colorantes azoicos — máxima precaución con niños</strong>
            <ul>
              <li>E102 (Tartrazina), E104 (Amarillo de quinoleína), E110 (Amarillo ocaso FCF)</li>
              <li>E122 (Azorrubina), E124 (Rojo Ponceau 4R), E129 (Rojo Allura AC)</li>
              <li>Estudio EFSA 2007: correlación con hiperactividad en niños. En Europa es obligatorio el aviso en la etiqueta: "puede afectar a la actividad y la atención de los niños"</li>
            </ul>
          </div>
        </div>
        <div class="rating-block rating-block--yellow">
          <div class="rating-block__dot">🟡</div>
          <div class="rating-block__body">
            <strong>Nitratos y nitritos — carne procesada</strong>
            <ul>
              <li>E249–E252 (nitritos y nitratos de sodio y potasio)</li>
              <li>Usados como conservantes en embutidos, salchichas, bacon, hot dog</li>
              <li>En presencia de proteínas animales forman nitrosaminas, clasificadas como probables cancerígenos (IARC grupo 2A). Limitar el consumo de carne procesada es el consejo principal</li>
            </ul>
          </div>
        </div>
        <div class="rating-block rating-block--yellow">
          <div class="rating-block__dot">🟡</div>
          <div class="rating-block__body">
            <strong>Edulcorantes intensivos — consumo habitual</strong>
            <ul>
              <li>E950 (Acesulfamo K), E951 (Aspartamo), E952 (Ciclamato), E954 (Sacarina), E955 (Sucralosa)</li>
              <li>Seguros en las dosis aprobadas para adultos. Estudios recientes sugieren posibles efectos sobre el microbioma intestinal con consumo crónico elevado</li>
              <li>La OMS (2023) ha expresado cautela sobre el uso crónico para el control del peso: no recomendados como alternativa al azúcar a largo plazo</li>
            </ul>
          </div>
        </div>
      </div>
    </section>

