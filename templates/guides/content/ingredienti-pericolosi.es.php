    <section id="zuccheri-nascosti" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">1</div>
        <h2 class="guide-section-title">Azúcares: los 10 nombres ocultos en las etiquetas</h2>
      </div>

      <p>
        El azúcar no siempre se llama "azúcar". Las empresas usan decenas de variantes para distribuirlo en más entradas de la lista de ingredientes — y hacer que parezca menos relevante. Si conoces estos nombres, dejarás de ser engañado.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🔍</span>
          <div class="guide-checklist__body">
            <strong>Los 10 alias más comunes</strong>
            <p>
              <strong>Jarabe de glucosa-fructosa</strong> (el peor: se metaboliza de forma diferente al azúcar de mesa) ·
              <strong>Maltodextrinas</strong> (almidón casi completamente descompuesto) ·
              <strong>Dextrosa</strong> ·
              <strong>Fructosa</strong> ·
              <strong>Zumo de caña concentrado</strong> ·
              <strong>Jarabe de maíz</strong> ·
              <strong>Jarabe de arroz</strong> ·
              <strong>Miel deshidratada</strong> ·
              <strong>Azúcar de coco</strong> ·
              <strong>Néctar de agave</strong>
            </p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">⚠️</span>
          <div class="guide-checklist__body">
            <strong>La trampa del "sin azúcares añadidos"</strong>
            <p>Un zumo "100% fruta sin azúcares añadidos" puede contener 10–12 g de azúcares por 100 ml — más que una Coca-Cola (10,6 g/100 ml). La fructosa del zumo concentrado es químicamente idéntica al azúcar añadido.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">📊</span>
          <div class="guide-checklist__body">
            <strong>El umbral que debes tener en mente</strong>
            <p>La OMS recomienda mantener los azúcares libres (añadidos + de zumos) por debajo del <strong>10% de las calorías diarias</strong> — unos 50 g al día para un adulto de 2000 kcal. Preferiblemente por debajo del 5% (25 g).</p>
          </div>
        </li>
      </ul>
    </section>

