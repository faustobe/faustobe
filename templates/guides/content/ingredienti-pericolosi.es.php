    <section id="regola-del-5" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">5</div>
        <h2 class="guide-section-title">La regla del 5: el filtro más rápido</h2>
      </div>

      <p>
        No tienes que convertirte en un experto en bioquímica para tomar mejores decisiones. La "regla del 5" es un filtro práctico que puedes aplicar en pocos segundos delante de la estantería.
      </p>

      <ol class="guide-step-list">
        <li>
          <span class="guide-step-num">1</span>
          <div class="guide-step-body">
            <strong>¿Más de 5 ingredientes? Empieza a leer con atención</strong>
            <p>No es una regla absoluta (una buena granola artesanal puede tener 7–8), pero es un primer filtro: la mayoría de los productos simples y poco procesados tiene menos de 5 ingredientes.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">2</span>
          <div class="guide-step-body">
            <strong>¿Azúcares por encima de 5 g/100 g? Comprueba si es el primero o segundo ingrediente</strong>
            <p>5 g/100 g es el umbral de la UE para "bajo contenido en azúcares". Por encima es solo información — no una prohibición — pero te indica que mires de dónde vienen esos azúcares.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">3</span>
          <div class="guide-step-body">
            <strong>¿Sal por encima de 0,5 g/100 g en un producto dulce? Señal de ultraprocesamiento</strong>
            <p>Las galletas, los cereales y los snacks dulces no deberían tener mucha sal. Cuando la tienen, a menudo sirve para enmascarar el sabor artificial de los sustitutos del azúcar o compensar la falta de ingredientes reales.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">4</span>
          <div class="guide-step-body">
            <strong>¿Más de 5 aditivos E? El producto probablemente es ultraprocesado</strong>
            <p>No cuenta el tipo de E, sino la cantidad. Un producto con 7–8 aditivos distintos (colorantes + emulsionantes + edulcorantes + conservantes + aromas) no necesita toda esa química si la calidad de los ingredientes base es alta.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">5</span>
          <div class="guide-step-body">
            <strong>¿No reconoces 5 o más ingredientes? Devuélvelo a la estantería</strong>
            <p>Si no sabes qué son "maltodextrinas", "caseinato de calcio", "almidón de tapioca modificado", "E471" y "aromas idénticos a los naturales" — y aparecen todos en el mismo producto — has encontrado un ultraprocesado que conviene limitar.</p>
          </div>
        </li>
      </ol>

      <div class="guide-insight">
        <strong>Recuerda:</strong> la regla del 5 es una orientación, no una sentencia. Una buena pasta integral con ingredientes certificados puede tener más de 5; una bollería barata puede quedarse justo por debajo. Úsala como punto de partida, no como único criterio.
      </div>
    </section>

