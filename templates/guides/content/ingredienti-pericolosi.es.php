    <section id="sodio-nascosto" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">4</div>
        <h2 class="guide-section-title">Sodio oculto: más allá de la sal de cocina</h2>
      </div>

      <p>
        El exceso de sodio eleva la presión arterial y aumenta el riesgo cardiovascular. El problema es que alrededor del 75% del sodio en la dieta occidental no se añade durante la cocción — ya viene en el producto envasado.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🧂</span>
          <div class="guide-checklist__body">
            <strong>Las principales fuentes de sodio oculto</strong>
            <p>Pan (la primera fuente de sodio en la dieta española), quesos, embutidos, platos preparados, salsas en bote, caldos en pastilla, salsas tipo soja, ketchup, mayonesa, snacks salados, cereales de desayuno industriales.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🔢</span>
          <div class="guide-checklist__body">
            <strong>Cómo convertir sodio en sal en la etiqueta</strong>
            <p>Si la etiqueta indica "sodio" en lugar de "sal": multiplica el valor por 2,5. Ejemplo: 0,6 g de sodio = 1,5 g de sal. El límite de la OMS es 5 g de sal al día (unos 2 g de sodio).</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🏷️</span>
          <div class="guide-checklist__body">
            <strong>Aditivos que contienen sodio (no solo la sal)</strong>
            <p>Glutamato monosódico (E621), benzoato de sodio (E211), nitrito de sodio (E250), bicarbonato de sodio, fosfato disódico — todos contribuyen al sodio total, aunque no aparezcan como "sal" en la etiqueta.</p>
          </div>
        </li>
      </ul>
    </section>

