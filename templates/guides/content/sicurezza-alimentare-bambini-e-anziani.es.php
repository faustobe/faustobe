    <section id="piu-vulnerabili" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">1</div>
        <h2 class="guide-section-title">Por qué los niños y los mayores son más vulnerables</h2>
      </div>

      <p>
        La misma dosis de aditivo que un adulto sano metaboliza sin problemas puede tener efectos distintos en un niño pequeño o en una persona mayor. No es alarmismo: es fisiología.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">👶</span>
          <div class="guide-checklist__body">
            <strong>Niños: bajo peso corporal, sistemas inmaduros</strong>
            <p>La dosis diaria aceptable (DDA) de los aditivos se calcula en mg por kg de peso corporal. Un niño de 15 kg que come una merienda de 30 g con colorantes recibe una dosis relativa 4–5 veces superior a la de un adulto de 70 kg. Además, el hígado y los riñones de los niños pequeños metabolizan algunas sustancias más lentamente.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">👴</span>
          <div class="guide-checklist__body">
            <strong>Mayores: función renal y hepática reducida</strong>
            <p>Con la edad disminuye la capacidad de eliminar ciertas sustancias. El uso de múltiples medicamentos (polifarmacia) puede interactuar con determinados aditivos. El exceso de sodio afecta más a quienes sufren hipertensión o insuficiencia renal.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🔬</span>
          <div class="guide-checklist__body">
            <strong>Efectos acumulativos: la dieta habitual importa más que la comida concreta</strong>
            <p>Un helado con colorantes una vez al mes no es un problema. Si los niños consumen a diario productos con E102, E110, E122 en el desayuno, la merienda, la comida y la bebida de la tarde — el efecto acumulativo se vuelve relevante.</p>
          </div>
        </li>
      </ul>
    </section>

