    <section id="additivi-bambini" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">2</div>
        <h2 class="guide-section-title">Aditivos críticos para los niños</h2>
      </div>

      <p>
        No todos los códigos E son problemáticos para los niños. Las categorías más relevantes, con evidencias científicas más sólidas, son estas.
      </p>

      <div class="rating-grid">
        <div class="rating-block rating-block--red">
          <div class="rating-block__dot">🔴</div>
          <div class="rating-block__body">
            <strong>Colorantes azoicos — evitar en niños</strong>
            <ul>
              <li>E102 (Tartrazina), E104 (Amarillo de quinoleína), E110 (Amarillo ocaso FCF)</li>
              <li>E122 (Azorrubina), E124 (Rojo Ponceau 4R), E129 (Rojo Allura AC)</li>
              <li>En Europa es obligatorio indicar en la etiqueta: <em>"puede afectar a la actividad y la atención de los niños"</em></li>
              <li>Dónde se encuentran: caramelos de colores, bebidas naranjas/rojas, gelatinas, dulces industriales, cereales de desayuno de colores</li>
            </ul>
          </div>
        </div>
        <div class="rating-block rating-block--yellow">
          <div class="rating-block__dot">🟡</div>
          <div class="rating-block__body">
            <strong>Edulcorantes intensivos — no aprobados para niños pequeños</strong>
            <ul>
              <li>E951 (Aspartamo), E950 (Acesulfamo K), E955 (Sucralosa), E954 (Sacarina)</li>
              <li>No están autorizados en alimentos específicamente destinados a lactantes y niños pequeños (Reg. UE 1333/2008)</li>
              <li>Dónde se encuentran a menudo: yogures "0% azúcar", bebidas "light" o "zero", jarabes para la tos, caramelos sin azúcar, ciertos zumos de fruta</li>
            </ul>
          </div>
        </div>
        <div class="rating-block rating-block--yellow">
          <div class="rating-block__dot">🟡</div>
          <div class="rating-block__body">
            <strong>Cafeína — a menudo oculta</strong>
            <ul>
              <li>No es un aditivo E, pero está presente en las bebidas energéticas, en ciertos refrescos y en snacks "al café"</li>
              <li>La EFSA recomienda que los niños no consuman más de 3 mg/kg/día de cafeína</li>
              <li>Una lata de 250 ml de bebida energética puede contener hasta 80 mg de cafeína — casi el doble del límite para un niño de 15 kg</li>
            </ul>
          </div>
        </div>
      </div>
    </section>

