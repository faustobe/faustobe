      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">4</div>
        <h2 class="guide-section-title">Productos "para niños" que hay que mirar con atención</h2>
      </div>

      <p>
        El marketing "para niños" suele ser lo contrario de lo que parece. Los productos con personajes coloridos, mascotas y frases como "con vitaminas" o "energía para crecer" son a menudo de los más procesados de la estantería.
      </p>

      <div class="signal-cards">
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🚩</span>
          <div class="signal-card__content">
            <strong>Cereales de desayuno "para niños"</strong>
            <p>A menudo contienen 25–35 g de azúcares por 100 g (equivalente a 7–9 cucharaditas por 100 g), colorantes artificiales, aromas complejos y vitaminas añadidas para compensar la escasa calidad nutricional de los ingredientes base. Alternativa: avena en copos + fruta fresca + miel.</p>
          </div>
        </div>
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🚩</span>
          <div class="signal-card__content">
            <strong>Bebidas "a base de zumos de fruta"</strong>
            <p>Productos como "bebida de naranja" o "zumo multifruta" pueden contener solo el 10–30% de zumo real; el resto es agua, azúcares, acidulantes y colorantes. No son equivalentes a los zumos al 100%. Comprueba siempre el porcentaje de fruta en la etiqueta.</p>
          </div>
        </div>
        <div class="signal-card signal-card--warning">
          <span class="signal-card__emoji">⚠️</span>
          <div class="signal-card__content">
            <strong>Yogures "para niños" con fruta</strong>
            <p>Muchos contienen más azúcar que un yogur adulto estándar, más espesantes (pectina, almidón modificado) y aromas artificiales. El yogur griego natural con fruta fresca añadida en el momento siempre es una opción mejor — y a menudo cuesta menos.</p>
          </div>
        </div>
      </div>
    </section>

