      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">5</div>
        <h2 class="guide-section-title">Lista segura para la compra</h2>
      </div>

      <p>
        Una guía práctica de compra segura para quienes hacen la compra pensando en niños y personas mayores.
      </p>

      <ol class="guide-step-list">
        <li>
          <span class="guide-step-num">1</span>
          <div class="guide-step-body">
            <strong>Para los niños: elige productos con pocos ingredientes reconocibles</strong>
            <p>Cuanto más corta sea la lista de ingredientes, mejor. Evita productos con más de 2–3 colorantes, edulcorantes artificiales, o la frase "puede afectar a la actividad y la atención de los niños".</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">2</span>
          <div class="guide-step-body">
            <strong>Comprueba siempre los azúcares en los productos "para niños"</strong>
            <p>Umbral: menos de 10 g/100 g para los productos de desayuno y snacks. Por encima ya es un dulce, no un alimento cotidiano adecuado para niños.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">3</span>
          <div class="guide-step-body">
            <strong>Para los mayores: prioridad al bajo contenido en sodio</strong>
            <p>Elige productos con menos de 0,3 g de sal/100 g. Opta por atún al natural en lugar de atún en aceite de semillas con sal, verduras congeladas sin sal añadida, caldos caseros o con bajo contenido en sodio.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">4</span>
          <div class="guide-step-body">
            <strong>Escanea con la app antes de comprar</strong>
            <p>E-Codes Reader te muestra de inmediato si un producto contiene los colorantes azoicos con el aviso europeo, los edulcorantes no aptos para niños, y el nivel de sodio — sin tener que leer cada línea de la etiqueta.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">5</span>
          <div class="guide-step-body">
            <strong>Prioriza siempre el Grupo 1–2 (NOVA) para las comidas principales</strong>
            <p>Verduras, legumbres, huevos, pescado, yogur natural, fruta entera. Son la base más segura para ambas categorías vulnerables — sin necesidad de descifrar etiquetas complejas.</p>
          </div>
        </li>
      </ol>

