    <section id="anziani" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">3</div>
        <h2 class="guide-section-title">Qué limitar en personas mayores</h2>
      </div>

      <p>
        Para las personas mayores las prioridades son distintas a las de los niños. Las principales preocupaciones tienen que ver con el sodio, los fosfatos y ciertos conservantes.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🧂</span>
          <div class="guide-checklist__body">
            <strong>Sodio: el principal riesgo</strong>
            <p>La hipertensión y la insuficiencia renal son algunas de las condiciones más frecuentes en las personas mayores, y el sodio las agrava. Los platos preparados, los embutidos, los quesos curados y los caldos industriales son los principales vehículos. Objetivo: menos de 5 g de sal al día, y menos para quienes tienen problemas renales o hipertensión documentada.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🦴</span>
          <div class="guide-checklist__body">
            <strong>Fosfatos: interfieren con el calcio y los huesos</strong>
            <p>Los fosfatos (E338–E341, E450–E452) son comunes en productos cárnicos procesados, quesos fundidos, carne en conserva y platos preparados. Un consumo elevado de fosfatos de origen aditivo interfiere con la absorción de calcio — un problema para quienes tienen riesgo de osteoporosis.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">💊</span>
          <div class="guide-checklist__body">
            <strong>Interacciones con medicamentos comunes</strong>
            <p>Algunas sustancias en los alimentos pueden interactuar con medicamentos habituales en personas mayores: la vitamina K en las verduras de hoja verde con los anticoagulantes orales, el pomelo con las estatinas, el zumo de arándanos con ciertos anticoagulantes. No se trata de evitar estos alimentos, sino de mantener una dieta constante y comunicar los hábitos alimentarios al médico.</p>
          </div>
        </li>
      </ul>
    </section>

