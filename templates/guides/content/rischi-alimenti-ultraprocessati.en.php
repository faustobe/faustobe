    <section id="obesita-metabolismo" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">1</div>
        <h2 class="guide-section-title">Obesity and metabolic disruption</h2>
      </div>

      <p>
        Ultra-processed foods don't cause weight gain simply because they are high in calories. The mechanism is more insidious: they are engineered to be <strong>hyper-palatable</strong> and actively interfere with the body's natural satiety signals.
      </p>

      <div class="guide-insight">
        <strong>Kevin Hall's study (NIH, 2019):</strong> in a controlled clinical trial, participants exposed to an ultra-processed diet consumed an average of <strong>500 extra calories per day</strong> compared to those eating minimally processed foods — with equal access to food and freedom of choice. In just two weeks, the UPF group had gained nearly one kilogram.
      </div>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🔥</span>
          <div class="guide-checklist__body">
            <strong>High energy density</strong>
            <p>UPFs contain on average <strong>2.15 kcal per gram</strong> — almost double that of fresh foods. The same volume of food delivers far more calories, without the corresponding feeling of fullness.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🧠</span>
          <div class="guide-checklist__body">
            <strong>Excessive stimulation of pleasure centres</strong>
            <p>The industrial combination of sugar, salt, fat, and flavourings activates the dopaminergic system in a manner similar to addictive substances. The result: eating continues even in the absence of real hunger.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">📉</span>
          <div class="guide-checklist__body">
            <strong>Reduction of peptide YY</strong>
            <p>UPFs lower levels of <strong>peptide YY</strong>, the gut hormone that signals fullness to the brain. People who eat a lot of ultra-processed foods receive fewer "stop" signals after meals.</p>
          </div>
        </li>
      </ul>

      <div class="signal-cards">
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">📊</span>
          <div class="signal-card__content">
            <strong>+41% risk of abdominal obesity</strong>
            <p>People who follow a diet high in UPFs have a 41% higher risk of developing obesity or abdominal obesity compared to those who consume few of them.</p>
          </div>
        </div>
      </div>
    </section>

