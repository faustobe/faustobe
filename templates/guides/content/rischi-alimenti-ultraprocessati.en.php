    <!-- SEZIONE 2 -->
    <section id="infiammazione-immunita" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">2</div>
        <h2 class="guide-section-title">Inflammation and immune system dysfunction</h2>
      </div>

      <p>
        Frequent consumption of ultra-processed foods is associated with an increase in inflammatory immune responses in the body — an effect that goes well beyond the simple nutritional profile of the product.
      </p>

      <p>
        Some substances commonly found in UPFs — emulsifiers, thickeners, and microparticles such as <strong>titanium dioxide (E171)</strong> — can alter the gut microbiota and increase the permeability of the intestinal mucosa. This phenomenon, known as <em>leaky gut</em>, allows pathogenic bacteria and harmful substances to enter the bloodstream, triggering chronic systemic inflammation.
      </p>

      <div class="signal-cards">
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🚩</span>
          <div class="signal-card__content">
            <strong>Additives that increase intestinal permeability</strong>
            <p>Emulsifiers such as <strong>carboxymethylcellulose (E466)</strong> and <strong>polysorbate 80 (E433)</strong> alter the protective intestinal mucus even at doses permitted by regulations, according to studies on animal models and cell cultures.</p>
          </div>
        </div>
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🚩</span>
          <div class="signal-card__content">
            <strong>Elevated inflammatory biomarkers</strong>
            <p>People who consume many UPFs show higher levels of <strong>C-reactive protein</strong> and other circulating inflammatory protein biomarkers, associated with a greater risk of cardiovascular mortality.</p>
          </div>
        </div>
      </div>

      <p>Chronic immune dysfunctions induced by UPFs are associated with a higher risk of developing autoimmune diseases:</p>

      <div class="rating-grid">
        <div class="rating-block rating-block--red">
          <div class="rating-block__dot">⚠️</div>
          <div class="rating-block__body">
            <strong>Autoimmune diseases associated with high UPF consumption</strong>
            <ul>
              <li>Coeliac disease</li>
              <li>Hashimoto's thyroiditis</li>
              <li>Multiple sclerosis</li>
              <li>Systemic lupus erythematosus</li>
              <li>Type 1 diabetes</li>
            </ul>
          </div>
        </div>
      </div>

      <div class="guide-insight">
        The link between UPFs and autoimmune diseases is not yet fully understood, and correlation does not imply direct causation. However, the convergence of multiple independent studies across different populations makes the association worthy of clinical attention.
      </div>
    </section>

