    <section id="rischio-sistemico" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">4</div>
        <h2 class="guide-section-title">A systemic risk: multiple mechanisms at once</h2>
      </div>

      <p>
        What makes UPFs particularly concerning is not a single negative effect, but the convergence of multiple mechanisms that reinforce one another.
      </p>

      <div class="guide-table-wrap">
        <table>
          <thead>
            <tr>
              <th>Mechanism</th>
              <th>Effect</th>
              <th>Long-term consequence</th>
            </tr>
          </thead>
          <tbody>
            <tr>
              <td>Hyper-palatability</td>
              <td>Excessive calorie intake</td>
              <td>Obesity, type 2 diabetes</td>
            </tr>
            <tr>
              <td>Fibre deficiency</td>
              <td>Intestinal dysbiosis</td>
              <td>Chronic inflammation, IBD</td>
            </tr>
            <tr>
              <td>Additives (emulsifiers)</td>
              <td>Intestinal permeability</td>
              <td>Autoimmune, cardiovascular diseases</td>
            </tr>
            <tr>
              <td>Sugars and sweeteners</td>
              <td>Insulin resistance, dysbiosis</td>
              <td>Type 2 diabetes, metabolic syndrome</td>
            </tr>
            <tr>
              <td>Replacement of fresh food</td>
              <td>Micronutrient deficiency</td>
              <td>Oxidative stress, immunosuppression</td>
            </tr>
          </tbody>
        </table>
      </div>

      <p>
        In summary, UPFs act as a <strong>systemic risk factor</strong> that simultaneously promotes insulin resistance, oxidative stress, and low-grade chronic inflammation — three conditions that underlie almost all the most prevalent chronic non-communicable diseases in the Western world.
      </p>

      <div class="guide-insight">
        <strong>Large epidemiological studies confirm this:</strong> NutriNet-Santé (France, over 100,000 participants) and UK Biobank (UK, over 500,000 participants) associate high UPF consumption with greater risks of cancer, cardiovascular disease, depression, and all-cause mortality — independently of the nutritional quality of individual products.
      </div>
    </section>

