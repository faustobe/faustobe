    <!-- SEZIONE 3 -->
    <section id="microbiota-intestino" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">3</div>
        <h2 class="guide-section-title">Gut health and microbiota</h2>
      </div>

      <p>
        The industrial ultra-processing method strips foods of <strong>dietary fibre</strong> and <strong>bioactive plant compounds</strong> (polyphenols, antioxidants) needed to nourish the "good" bacteria in the gut. In their place, added sugars, salt, artificial sweeteners, and additives abound, promoting an imbalance in the microbiota — known as <strong>dysbiosis</strong>.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🦠</span>
          <div class="guide-checklist__body">
            <strong>Dysbiosis: less bacterial diversity, more risks</strong>
            <p>An impoverished microbiota — with fewer beneficial bacterial species — is associated with insulin resistance, chronic inflammation, and greater vulnerability to infections and metabolic diseases. UPFs actively reduce this diversity.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🧱</span>
          <div class="guide-checklist__body">
            <strong>Weakening of the protective mucous layer</strong>
            <p>The abundance of emulsifiers and artificial sweeteners weakens the mucus layer lining the intestine, reducing the physical barrier between gut bacteria and the colon wall.</p>
          </div>
        </li>
      </ul>

      <p>Observational studies show that people who consume large quantities of UPFs more frequently suffer from:</p>

      <div class="signal-cards">
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🔴</span>
          <div class="signal-card__content">
            <strong>Chronic inflammatory bowel diseases</strong>
            <p>Crohn's disease and ulcerative colitis — two conditions on the rise in countries with the highest consumption of ultra-processed foods.</p>
          </div>
        </div>
        <div class="signal-card signal-card--warning">
          <span class="signal-card__emoji">⚠️</span>
          <div class="signal-card__content">
            <strong>Functional and precancerous disorders</strong>
            <p>Irritable bowel syndrome, gastric ulcers, and precancerous colon polyps are more frequent among habitual UPF consumers.</p>
          </div>
        </div>
      </div>
    </section>

