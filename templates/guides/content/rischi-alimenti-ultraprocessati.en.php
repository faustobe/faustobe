    <section id="cosa-fare" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">5</div>
        <h2 class="guide-section-title">What to do in practice</h2>
      </div>

      <p>
        The scientific evidence is solid, but the practical message is not "eliminate everything". It is <strong>reduce the share of UPFs in your daily diet</strong> and replace them with simple alternatives, where available and accessible.
      </p>

      <ol class="guide-step-list">
        <li>
          <span class="guide-step-num">1</span>
          <div class="guide-step-body">
            <strong>Identify your habitual UPFs</strong>
            <p>Sweetened breakfast cereals, packaged snacks, sliced bread with additives, ready meals, flavoured drinks: these are the products that, consumed daily, make the difference. Not ice cream once a month.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">2</span>
          <div class="guide-step-body">
            <strong>Substitute one at a time</strong>
            <p>No revolution needed. Rolled oats instead of sweet cereals. Plain Greek yogurt instead of flavoured yogurt with thickeners. Artisan bread instead of sliced bread with preservatives. Each substitution reduces the cumulative burden.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">3</span>
          <div class="guide-step-body">
            <strong>Increase fibre — it's the priority</strong>
            <p>Legumes, vegetables, whole-grain cereals, whole fruit: fibre feeds the beneficial bacteria in the microbiota and directly counters many of the negative effects of UPFs. It doesn't require "eliminating" anything — only adding.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">4</span>
          <div class="guide-step-body">
            <strong>Use the app to identify hidden UPFs</strong>
            <p>Many products that appear healthy ("protein" bars, "0%" yogurt, "fruit" juices) are actually NOVA 4. Scanning the barcode with E-Codes Reader instantly shows the NOVA classification and the additives present.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">5</span>
          <div class="guide-step-body">
            <strong>Don't aim for perfection</strong>
            <p>The goal is not a "pure" diet. It's that the majority of what you eat each week is real food — and that ultra-processed foods remain the exception, not the foundation. Even a 20–30% improvement in diet quality produces measurable health effects in the medium term.</p>
          </div>
        </li>
      </ol>
    </section>

