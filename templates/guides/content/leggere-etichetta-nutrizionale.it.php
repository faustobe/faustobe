    <section id="ingredienti" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">1</div>
        <h2 class="guide-section-title">La lista degli ingredienti: dove guardare per prima cosa</h2>
      </div>

      <p>
        Per legge, gli ingredienti sono elencati in <strong>ordine decrescente di peso</strong>: ciò che compare prima è presente in quantità maggiore. È il punto di partenza più importante dell'etichetta.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🥇</span>
          <div class="guide-checklist__body">
            <strong>Il primo ingrediente conta di più</strong>
            <p>Se il primo ingrediente è "zucchero", "sciroppo di glucosio" o "olio di palma", il prodotto è costruito attorno a quell'elemento. Nei biscotti al cioccolato, spesso il cioccolato compare solo al quarto o quinto posto.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">📏</span>
          <div class="guide-checklist__body">
            <strong>Lunghezza della lista = grado di lavorazione</strong>
            <p>Pane fatto in casa: farina, acqua, sale, lievito — 4 ingredienti. Pane industriale da scaffale: può averne 15 o più. La lunghezza non è un crimine in sé, ma è sempre un segnale da considerare.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🔍</span>
          <div class="guide-checklist__body">
            <strong>Cerca i nomi difficili da pronunciare</strong>
            <p>Termini come "amido modificato", "mono- e digliceridi degli acidi grassi", "carboxymethyl cellulose" o "idrogenato" indicano ingredienti di origine industriale, spesso assenti nelle ricette casalinghe.</p>
          </div>
        </li>
      </ul>

      <div class="guide-insight">
        <strong>Regola pratica:</strong> se non riusciresti a trovare tutti gli ingredienti in un negozio di alimentari normale, probabilmente il prodotto è molto lavorato.
      </div>
    </section>

    <hr class="guide-divider" />

    <!-- SEZIONE 2 -->
    <section id="tabella-nutrizionale" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">2</div>
        <h2 class="guide-section-title">La tabella nutrizionale: cosa guardare (e cosa ignorare)</h2>
      </div>

      <p>
        La tabella nutrizionale è obbligatoria su quasi tutti i prodotti confezionati. Contiene molte informazioni, ma bastano tre valori per capire il quadro generale.
      </p>

      <div class="signal-cards">
        <div class="signal-card signal-card--ok">
          <span class="signal-card__emoji">✅</span>
          <div class="signal-card__content">
            <strong>Zuccheri: meno di 5 g per 100 g</strong>
            <p>Valori sotto i 5 g/100 g indicano un prodotto a basso contenuto di zuccheri. Tra 5 e 22,5 g è medio; sopra 22,5 g è alto. Per le bevande, il limite "basso" è 2,5 g/100 ml.</p>
          </div>
        </div>
        <div class="signal-card signal-card--warning">
          <span class="signal-card__emoji">⚠️</span>
          <div class="signal-card__content">
            <strong>Sale: meno di 0,3 g per 100 g</strong>
            <p>Sotto 0,3 g/100 g è basso, sopra 1,5 g/100 g è alto. Molti prodotti salati come crackers, formaggi e salumi superano facilmente 1 g/100 g. Attenzione: il sodio in etichetta va moltiplicato per 2,5 per ottenere il sale.</p>
          </div>
        </div>
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🚩</span>
          <div class="signal-card__content">
            <strong>Grassi saturi: meno di 1,5 g per 100 g</strong>
            <p>Sono il tipo di grasso da monitorare con più attenzione. Valori sopra 5 g/100 g sono considerati alti. I prodotti da forno industriali, le merendine e i piatti pronti spesso li superano abbondantemente.</p>
          </div>
        </div>
      </div>
    </section>

    <hr class="guide-divider" />

    <!-- SEZIONE 3 -->
    <section id="porzioni" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">3</div>
        <h2 class="guide-section-title">Porzioni vs 100 g: il trucco che confonde</h2>
      </div>

      <p>
        I valori nutrizionali vengono spesso indicati sia per 100 g che "per porzione". Le aziende tendono a mettere in evidenza i valori per porzione, che sembrano più bassi — ma le porzioni sono spesso sottostimate.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🎭</span>
          <div class="guide-checklist__body">
            <strong>Cereali da colazione: porzione dichiarata 30 g, ma ne mangi 60</strong>
            <p>Se la confezione dichiara 10 g di zuccheri per porzione (30 g) e tu ne mangi una ciotola da 60 g, stai assumendo 20 g di zuccheri — quasi la metà della soglia giornaliera raccomandata.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">📊</span>
          <div class="guide-checklist__body">
            <strong>Usa sempre i valori per 100 g per confrontare</strong>
            <p>Vuoi capire qual è la pasta migliore tra due marche? Confronta i valori per 100 g, non per porzione (che varia da marca a marca). È l'unico confronto equo.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🧮</span>
          <div class="guide-checklist__body">
            <strong>Calcola la porzione reale</strong>
            <p>Guarda quante porzioni ci sono nella confezione. Se una busta di patatine contiene "3,5 porzioni" e la mangi tutta, moltiplica tutti i valori per 3,5.</p>
          </div>
        </li>
      </ul>
    </section>

    <hr class="guide-divider" />

    <!-- SEZIONE 4 -->
    <section id="sale-nascosto" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">4</div>
        <h2 class="guide-section-title">Il sale nascosto: dove si nasconde davvero</h2>
      </div>

      <p>
        Il sale non si trova solo nei prodotti salati. In molti alimenti apparentemente dolci — biscotti, cereali, pane — il contenuto di sale è sorprendentemente alto.
      </p>

      <div class="signal-cards">
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🍞</span>
          <div class="signal-card__content">
            <strong>Pane e prodotti da forno</strong>
            <p>Due fette di pane industriale possono contenere 0,5–0,8 g di sale. Moltiplicato per 4 pasti al giorno, è già metà del limite giornaliero raccomandato (5 g).</p>
          </div>
        </div>
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🥣</span>
          <div class="signal-card__content">
            <strong>Cereali da colazione</strong>
            <p>Anche quelli "sani" e "integrali" possono contenere 0,8–1,2 g di sale per 100 g — più di molti snack salati. Leggere l'etichetta in questo caso sorprende quasi sempre.</p>
          </div>
        </div>
        <div class="signal-card signal-card--warning">
          <span class="signal-card__emoji">🫙</span>
          <div class="signal-card__content">
            <strong>Salse, sughi e condimenti</strong>
            <p>Il sugo di pomodoro pronto, la maionese, le salse per insalata: possono contenere tra 1 e 3 g di sale per 100 g. Uno dei casi in cui il fai-da-te batte sempre l'industriale.</p>
          </div>
        </div>
      </div>

      <div class="guide-insight">
        <strong>Nota bene:</strong> in etichetta potresti trovare "sodio" invece di "sale". Per convertire: <strong>sale = sodio × 2,5</strong>.
      </div>
    </section>

