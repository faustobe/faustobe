    <!-- SEZIONE 6 -->
    <section id="checklist" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">6</div>
        <h2 class="guide-section-title">Checklist rapida da usare al supermercato</h2>
      </div>

      <p>
        Non devi fare tutto ogni volta. Inizia con i controlli più veloci e aggiungi gli altri man mano che diventa un'abitudine.
      </p>

      <ol class="guide-step-list">
        <li>
          <span class="guide-step-num">1</span>
          <div class="guide-step-body">
            <strong>Guarda il primo ingrediente</strong>
            <p>È un alimento vero (farina, latte, pomodoro, olio)? Bene. È uno zucchero, sciroppo o grasso lavorato? Valuta se hai un'alternativa.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">2</span>
          <div class="guide-step-body">
            <strong>Conta gli ingredienti</strong>
            <p>Meno di 5: ottimo. Da 5 a 10: accettabile. Oltre 10–12: controlla bene cosa contengono.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">3</span>
          <div class="guide-step-body">
            <strong>Controlla i codici E</strong>
            <p>Pochi o nessuno? Bene. Tanti (soprattutto coloranti, dolcificanti, conservanti)? Il prodotto è molto lavorato.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">4</span>
          <div class="guide-step-body">
            <strong>Guarda sale e zuccheri per 100 g</strong>
            <p>Sale oltre 1,5 g/100 g = alto. Zuccheri oltre 22,5 g/100 g = alto. Grassi saturi oltre 5 g/100 g = alto.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">5</span>
          <div class="guide-step-body">
            <strong>Ignora i claim in grande sul fronte</strong>
            <p>"Integrale", "naturale", "0% grassi" non significano quasi mai quello che sembrano. Fida sempre della lista ingredienti, non delle scritte promozionali.</p>
          </div>
        </li>
      </ol>

      <div class="guide-insight">
        <strong>Il trucco più semplice di tutti:</strong> se riconosci tutti gli ingredienti e potresti cucinarli a casa, sei sulla strada giusta. Se no, hai trovato un ultra-processato.
      </div>
    </section>

