    <!-- SEZIONE 5 -->
    <section id="claim" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">5</div>
        <h2 class="guide-section-title">I claim nutrizionali: cosa significano davvero</h2>
      </div>

      <p>
        I claim nutrizionali sono le scritte come "senza grassi", "light", "ricco di fibre" o "0% zuccheri" che compaiono in grande sul fronte della confezione. Sono regolamentati dalla legge, ma possono trarre in inganno.
      </p>

      <div class="rating-grid">
        <div class="rating-block rating-block--yellow">
          <div class="rating-block__dot">🏷️</div>
          <div class="rating-block__body">
            <strong>"Senza zuccheri aggiunti"</strong>
            <ul>
              <li>Non significa senza zuccheri: può contenerne di naturali (fruttosio del succo, lattosio)</li>
              <li>Può contenere dolcificanti artificiali o polialcoli</li>
              <li>Non equivale a "sano" o "adatto ai diabetici"</li>
            </ul>
          </div>
        </div>
        <div class="rating-block rating-block--yellow">
          <div class="rating-block__dot">🏷️</div>
          <div class="rating-block__body">
            <strong>"Light" o "ridotto"</strong>
            <ul>
              <li>Significa ridotto del 30% rispetto al prodotto di riferimento</li>
              <li>Se il prodotto base aveva molte calorie, il light ne ha ancora molte</li>
              <li>Spesso il grasso tolto è sostituito con zuccheri o addensanti</li>
            </ul>
          </div>
        </div>
        <div class="rating-block rating-block--yellow">
          <div class="rating-block__dot">🏷️</div>
          <div class="rating-block__body">
            <strong>"Ricco di fibre" o "fonte di proteine"</strong>
            <ul>
              <li>Richiede soglie minime di legge (fibre: min. 6 g/100 g; proteine: min. 20% delle calorie)</li>
              <li>Non dice nulla sugli altri ingredienti: un biscotto "ricco di fibre" può contenere molto zucchero</li>
              <li>Sempre leggere la lista ingredienti, non solo il claim</li>
            </ul>
          </div>
        </div>
      </div>
    </section>

