    <!-- SECTION 6 -->
    <section id="checklist" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">6</div>
        <h2 class="guide-section-title">Quick checklist to use at the supermarket</h2>
      </div>

      <p>
        You don't have to do everything every time. Start with the quickest checks and add the others as they become habit.
      </p>

      <ol class="guide-step-list">
        <li>
          <span class="guide-step-num">1</span>
          <div class="guide-step-body">
            <strong>Look at the first ingredient</strong>
            <p>Is it a real food (flour, milk, tomato, oil)? Good. Is it a sugar, syrup or processed fat? Consider whether you have an alternative.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">2</span>
          <div class="guide-step-body">
            <strong>Count the ingredients</strong>
            <p>Fewer than 5: great. 5 to 10: acceptable. More than 10–12: take a closer look at what they are.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">3</span>
          <div class="guide-step-body">
            <strong>Check the E codes</strong>
            <p>Few or none? Good. Many (especially colorings, sweeteners, preservatives)? The product is highly processed.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">4</span>
          <div class="guide-step-body">
            <strong>Check salt and sugars per 100 g</strong>
            <p>Salt above 1.5 g/100 g = high. Sugars above 22.5 g/100 g = high. Saturated fat above 5 g/100 g = high.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">5</span>
          <div class="guide-step-body">
            <strong>Ignore the big claims on the front</strong>
            <p>"Wholegrain", "natural", "0% fat" almost never mean what they appear to. Always trust the ingredients list, not the promotional text.</p>
          </div>
        </li>
      </ol>

      <div class="guide-insight">
        <strong>The simplest trick of all:</strong> if you recognise every ingredient and could cook with them at home, you're on the right track. If not, you've found an ultra-processed product.
      </div>
    </section>

