    <!-- SECTION 1 -->
    <section id="ingredienti" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">1</div>
        <h2 class="guide-section-title">The ingredients list: where to look first</h2>
      </div>

      <p>
        By law, ingredients are listed in <strong>descending order by weight</strong>: what appears first is present in the greatest amount. This is the most important starting point on any label.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🥇</span>
          <div class="guide-checklist__body">
            <strong>The first ingredient matters most</strong>
            <p>If the first ingredient is "sugar", "glucose syrup" or "palm oil", the product is built around that element. In chocolate cookies, the chocolate often shows up only in fourth or fifth place.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">📏</span>
          <div class="guide-checklist__body">
            <strong>List length = degree of processing</strong>
            <p>Homemade bread: flour, water, salt, yeast — 4 ingredients. Industrial supermarket bread: can have 15 or more. Length isn't a crime in itself, but it's always a signal worth considering.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🔍</span>
          <div class="guide-checklist__body">
            <strong>Look for names that are hard to pronounce</strong>
            <p>Terms like "modified starch", "mono- and diglycerides of fatty acids", "carboxymethyl cellulose" or "hydrogenated" indicate industrially derived ingredients, typically absent from home recipes.</p>
          </div>
        </li>
      </ul>

      <div class="guide-insight">
        <strong>Practical rule:</strong> if you couldn't find all the ingredients at a regular grocery store, the product is probably highly processed.
      </div>
    </section>

    <hr class="guide-divider" />

    <!-- SECTION 2 -->
    <section id="tabella-nutrizionale" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">2</div>
        <h2 class="guide-section-title">The nutrition table: what to look at (and what to ignore)</h2>
      </div>

      <p>
        The nutrition table is mandatory on almost all packaged products. It contains a lot of information, but just three values are enough to get the overall picture.
      </p>

      <div class="signal-cards">
        <div class="signal-card signal-card--ok">
          <span class="signal-card__emoji">✅</span>
          <div class="signal-card__content">
            <strong>Sugars: less than 5 g per 100 g</strong>
            <p>Values below 5 g/100 g indicate a product with low sugar content. Between 5 and 22.5 g is medium; above 22.5 g is high. For drinks, the "low" threshold is 2.5 g/100 ml.</p>
          </div>
        </div>
        <div class="signal-card signal-card--warning">
          <span class="signal-card__emoji">⚠️</span>
          <div class="signal-card__content">
            <strong>Salt: less than 0.3 g per 100 g</strong>
            <p>Below 0.3 g/100 g is low, above 1.5 g/100 g is high. Many savory products like crackers, cheeses and cured meats easily exceed 1 g/100 g. Note: sodium on the label must be multiplied by 2.5 to get the salt content.</p>
          </div>
        </div>
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🚩</span>
          <div class="signal-card__content">
            <strong>Saturated fat: less than 1.5 g per 100 g</strong>
            <p>This is the type of fat to monitor most carefully. Values above 5 g/100 g are considered high. Industrial baked goods, snack cakes and ready meals often exceed this by a wide margin.</p>
          </div>
        </div>
      </div>
    </section>

    <hr class="guide-divider" />

    <!-- SECTION 3 -->
    <section id="porzioni" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">3</div>
        <h2 class="guide-section-title">Servings vs 100 g: the trick that confuses everyone</h2>
      </div>

      <p>
        Nutritional values are often shown both per 100 g and "per serving". Companies tend to highlight the per-serving values, which look lower — but serving sizes are frequently underestimated.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🎭</span>
          <div class="guide-checklist__body">
            <strong>Breakfast cereals: stated serving 30 g, but you eat 60</strong>
            <p>If the pack declares 10 g of sugar per serving (30 g) and you eat a 60 g bowl, you're actually taking in 20 g of sugar — nearly half the recommended daily limit.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">📊</span>
          <div class="guide-checklist__body">
            <strong>Always use per-100-g values for comparison</strong>
            <p>Want to figure out which of two pasta brands is better? Compare the values per 100 g, not per serving (which varies from brand to brand). It's the only fair comparison.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🧮</span>
          <div class="guide-checklist__body">
            <strong>Calculate your actual serving</strong>
            <p>Check how many servings are in the package. If a bag of crisps contains "3.5 servings" and you eat the whole thing, multiply all the values by 3.5.</p>
          </div>
        </li>
      </ul>
    </section>

    <hr class="guide-divider" />

    <!-- SECTION 4 -->
    <section id="sale-nascosto" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">4</div>
        <h2 class="guide-section-title">Hidden salt: where it really lurks</h2>
      </div>

      <p>
        Salt isn't only found in savory products. In many apparently sweet foods — cookies, cereals, bread — the salt content is surprisingly high.
      </p>

      <div class="signal-cards">
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🍞</span>
          <div class="signal-card__content">
            <strong>Bread and baked goods</strong>
            <p>Two slices of industrial bread can contain 0.5–0.8 g of salt. Multiplied across 4 meals a day, that's already half the recommended daily limit (5 g).</p>
          </div>
        </div>
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🥣</span>
          <div class="signal-card__content">
            <strong>Breakfast cereals</strong>
            <p>Even the "healthy" and "wholegrain" varieties can contain 0.8–1.2 g of salt per 100 g — more than many savory snacks. Reading the label here is almost always a surprise.</p>
          </div>
        </div>
        <div class="signal-card signal-card--warning">
          <span class="signal-card__emoji">🫙</span>
          <div class="signal-card__content">
            <strong>Sauces, condiments and dressings</strong>
            <p>Ready-made tomato sauce, mayonnaise, salad dressings: these can contain between 1 and 3 g of salt per 100 g. One of the cases where homemade always beats store-bought.</p>
          </div>
        </div>
      </div>

      <div class="guide-insight">
        <strong>Important note:</strong> on the label you may find "sodium" instead of "salt". To convert: <strong>salt = sodium × 2.5</strong>.
      </div>
    </section>

