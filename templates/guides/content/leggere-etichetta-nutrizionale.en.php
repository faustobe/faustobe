    <!-- SECTION 5 -->
    <section id="claim" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">5</div>
        <h2 class="guide-section-title">Nutrition claims: what they really mean</h2>
      </div>

      <p>
        Nutrition claims are phrases like "fat-free", "light", "high in fibre" or "0% sugar" that appear prominently on the front of the pack. They are regulated by law, but they can still be misleading.
      </p>

      <div class="rating-grid">
        <div class="rating-block rating-block--yellow">
          <div class="rating-block__dot">🏷️</div>
          <div class="rating-block__body">
            <strong>"No added sugars"</strong>
            <ul>
              <li>Doesn't mean sugar-free: it may still contain naturally occurring sugars (fructose from juice, lactose)</li>
              <li>May contain artificial sweeteners or polyols</li>
              <li>Does not mean "healthy" or "suitable for diabetics"</li>
            </ul>
          </div>
        </div>
        <div class="rating-block rating-block--yellow">
          <div class="rating-block__dot">🏷️</div>
          <div class="rating-block__body">
            <strong>"Light" or "reduced"</strong>
            <ul>
              <li>Means reduced by 30% compared to the reference product</li>
              <li>If the original product was high in calories, the light version still has plenty</li>
              <li>Often, the removed fat is replaced with sugar or thickeners</li>
            </ul>
          </div>
        </div>
        <div class="rating-block rating-block--yellow">
          <div class="rating-block__dot">🏷️</div>
          <div class="rating-block__body">
            <strong>"High in fibre" or "source of protein"</strong>
            <ul>
              <li>Requires minimum legal thresholds (fibre: at least 6 g/100 g; protein: at least 20% of calories)</li>
              <li>Says nothing about the other ingredients: a "high-fibre" cookie can still contain a lot of sugar</li>
              <li>Always read the ingredients list, not just the claim</li>
            </ul>
          </div>
        </div>
      </div>
    </section>

