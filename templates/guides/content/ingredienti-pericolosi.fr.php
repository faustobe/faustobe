    <section id="additivi-controversi" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">3</div>
        <h2 class="guide-section-title">Additifs controversés : les codes E à connaître</h2>
      </div>

      <p>
        La plupart des codes E sur les étiquettes est sûre. Mais certaines catégories présentent un profil de risque plus significatif, notamment pour les enfants et les consommateurs réguliers. Il ne s'agit pas de les éliminer complètement, mais d'être conscient.
      </p>

      <div class="rating-grid">
        <div class="rating-block rating-block--red">
          <div class="rating-block__dot">🔴</div>
          <div class="rating-block__body">
            <strong>Colorants azoïques — vigilance maximale avec les enfants</strong>
            <ul>
              <li>E102 (Tartrazine), E104 (Jaune de quinoléine), E110 (Jaune orangé S)</li>
              <li>E122 (Carmoisine), E124 (Rouge Ponceau 4R), E129 (Rouge Allura AC)</li>
              <li>Étude EFSA 2007 : corrélation avec l'hyperactivité chez les enfants. En Europe, mention obligatoire sur l'étiquette : « peut avoir des effets indésirables sur l'activité et l'attention chez les enfants »</li>
            </ul>
          </div>
        </div>
        <div class="rating-block rating-block--yellow">
          <div class="rating-block__dot">🟡</div>
          <div class="rating-block__body">
            <strong>Nitrates et nitrites — viandes transformées</strong>
            <ul>
              <li>E249–E252 (nitrites et nitrates de sodium et potassium)</li>
              <li>Utilisés comme conservateurs dans les charcuteries, saucisses, bacon, hot-dogs</li>
              <li>En présence de protéines animales, ils forment des nitrosamines, classées comme probables cancérigènes (IARC groupe 2A). Limiter la consommation de viande transformée est le conseil principal</li>
            </ul>
          </div>
        </div>
        <div class="rating-block rating-block--yellow">
          <div class="rating-block__dot">🟡</div>
          <div class="rating-block__body">
            <strong>Édulcorants intenses — consommation habituelle</strong>
            <ul>
              <li>E950 (Acésulfame K), E951 (Aspartame), E952 (Cyclamate), E954 (Saccharine), E955 (Sucralose)</li>
              <li>Sûrs aux doses approuvées pour les adultes. Des études récentes suggèrent des effets possibles sur le microbiome intestinal avec une consommation chronique élevée</li>
              <li>L'OMS (2023) a exprimé une mise en garde sur l'usage chronique pour le contrôle du poids : non recommandé comme substitut au sucre à long terme</li>
            </ul>
          </div>
        </div>
      </div>
    </section>

