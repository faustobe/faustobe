    <section id="grassi-da-evitare" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">2</div>
        <h2 class="guide-section-title">Graisses à éviter : hydrogénées et trans</h2>
      </div>

      <p>
        Toutes les graisses ne sont pas égales. Les acides gras trans (AGT), produits par l'hydrogénation industrielle des huiles végétales, sont associés à un risque cardiovasculaire accru et ont été interdits ou fortement limités dans de nombreux pays.
      </p>

      <div class="signal-cards">
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🚩</span>
          <div class="signal-card__content">
            <strong>Graisses hydrogénées et partiellement hydrogénées</strong>
            <p>Cherchez sur l'étiquette : « huile végétale partiellement hydrogénée », « graisse végétale hydrogénée », « shortening ». En Europe, ils sont interdits au-dessus de 2 g/100 g de matière grasse depuis 2021, mais certaines exemptions subsistent. Évitez-les quoi qu'il en soit.</p>
          </div>
        </div>
        <div class="signal-card signal-card--warning">
          <span class="signal-card__emoji">⚠️</span>
          <div class="signal-card__content">
            <strong>Huile de palme : pas trans, mais...</strong>
            <p>L'huile de palme est saturée mais non hydrogénée. Le problème est différent : lors du raffinage à hautes températures, des composés potentiellement cancérigènes se forment (3-MCPD, glycidol). Elle n'est pas interdite, mais mieux vaut la limiter.</p>
          </div>
        </div>
        <div class="signal-card signal-card--ok">
          <span class="signal-card__emoji">✅</span>
          <div class="signal-card__content">
            <strong>Les graisses qui conviennent</strong>
            <p>Huile d'olive extra vierge, huile de lin, noix et oléagineux, avocat, graisses des poissons gras. Ces aliments contiennent des acides gras mono-insaturés et des oméga-3 aux effets protecteurs documentés.</p>
          </div>
        </div>
      </div>
    </section>

