    <section id="regola-del-5" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">5</div>
        <h2 class="guide-section-title">La règle des 5 : le filtre le plus rapide</h2>
      </div>

      <p>
        Vous n'avez pas besoin de devenir un expert en biochimie pour faire de meilleurs choix. La « règle des 5 » est un filtre pratique utilisable en quelques secondes devant le rayon.
      </p>

      <ol class="guide-step-list">
        <li>
          <span class="guide-step-num">1</span>
          <div class="guide-step-body">
            <strong>Plus de 5 ingrédients ? Commencez à lire attentivement</strong>
            <p>Ce n'est pas une règle absolue (un bon granola artisanal peut en avoir 7–8), mais c'est un premier filtre : la plupart des produits simples et peu transformés tiennent en dessous de 5 ingrédients.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">2</span>
          <div class="guide-step-body">
            <strong>Sucres supérieurs à 5 g/100 g ? Vérifiez s'il est le premier ou deuxième ingrédient</strong>
            <p>5 g/100 g est le seuil UE pour « faible teneur en sucres ». Au-dessus de ce seuil, c'est une information — pas une interdiction — mais cela vous signale de regarder d'où proviennent ces sucres.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">3</span>
          <div class="guide-step-body">
            <strong>Sel supérieur à 0,5 g/100 g dans un produit sucré ? Signal d'ultra-transformation</strong>
            <p>Biscuits, céréales et snacks sucrés ne devraient pas contenir beaucoup de sel. Quand c'est le cas, il sert souvent à masquer le goût artificiel des substituts du sucre ou à compenser le manque d'ingrédients vrais.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">4</span>
          <div class="guide-step-body">
            <strong>Plus de 5 additifs E ? Le produit est probablement ultra-transformé</strong>
            <p>Ce n'est pas le type de E qui compte, c'est la quantité. Un produit avec 7–8 additifs différents (colorants + émulsifiants + édulcorants + conservateurs + arômes) n'a pas besoin de toute cette chimie si la qualité des ingrédients de base est élevée.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">5</span>
          <div class="guide-step-body">
            <strong>Vous ne reconnaissez pas 5 ingrédients ou plus ? Reposez-le sur l'étagère</strong>
            <p>Si vous ne savez pas ce que sont « maltodextrines », « caséinate de calcium », « amidon de tapioca modifié », « E471 » et « arômes nature-identiques » — et qu'ils apparaissent tous dans le même produit — vous avez trouvé un ultra-transformé à limiter.</p>
          </div>
        </li>
      </ol>

      <div class="guide-insight">
        <strong>Rappelez-vous :</strong> la règle des 5 est une orientation, pas un verdict. De très bonnes pâtes complètes aux ingrédients certifiés peuvent en avoir plus de 5 ; un gâteau bon marché peut juste en être en dessous. Utilisez-la comme point de départ, pas comme unique critère.
      </div>
    </section>

