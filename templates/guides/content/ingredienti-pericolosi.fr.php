    <!-- SECTION 4 -->
    <section id="sodio-nascosto" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">4</div>
        <h2 class="guide-section-title">Sodium caché : au-delà du sel de cuisine</h2>
      </div>

      <p>
        Un excès de sodium élève la tension artérielle et augmente le risque cardiovasculaire. Le problème est qu'environ 75 % du sodium dans l'alimentation occidentale n'est pas ajouté pendant la cuisson — il est déjà présent dans les produits emballés.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🧂</span>
          <div class="guide-checklist__body">
            <strong>Les principaux vecteurs de sodium caché</strong>
            <p>Pain (première source de sodium dans l'alimentation), fromages, charcuteries, plats préparés, sauces en bocal, bouillon cube, sauces soja, ketchup, mayonnaise, snacks salés, céréales du matin industrielles.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🔢</span>
          <div class="guide-checklist__body">
            <strong>Comment convertir sodium en sel sur l'étiquette</strong>
            <p>Si l'étiquette indique « sodium » plutôt que « sel » : multipliez la valeur par 2,5. Exemple : 0,6 g de sodium = 1,5 g de sel. La limite OMS est de 5 g de sel par jour (environ 2 g de sodium).</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🏷️</span>
          <div class="guide-checklist__body">
            <strong>Additifs contenant du sodium (pas seulement le sel)</strong>
            <p>Glutamate monosodique (E621), benzoate de sodium (E211), nitrite de sodium (E250), bicarbonate de sodium, phosphate disodique — tous contribuent au sodium total, même s'ils n'apparaissent pas comme « sel » sur l'étiquette.</p>
          </div>
        </li>
      </ul>
    </section>

