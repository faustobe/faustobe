    <section id="zuccheri-nascosti" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">1</div>
        <h2 class="guide-section-title">Sucres : les 10 noms cachés sur les étiquettes</h2>
      </div>

      <p>
        Le sucre ne s'appelle pas toujours « sucre ». Les entreprises utilisent des dizaines de variantes pour le répartir dans plusieurs entrées de la liste des ingrédients — et le faire paraître moins important. Si vous connaissez ces noms, vous ne vous laisserez plus tromper.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🔍</span>
          <div class="guide-checklist__body">
            <strong>Les 10 alias les plus courants</strong>
            <p>
              <strong>Sirop de glucose-fructose</strong> (le pire : métabolisé différemment du sucre de table) ·
              <strong>Maltodextrines</strong> (amidon quasi entièrement décomposé) ·
              <strong>Dextrose</strong> ·
              <strong>Fructose</strong> ·
              <strong>Jus de canne concentré</strong> ·
              <strong>Sirop de maïs</strong> ·
              <strong>Sirop de riz</strong> ·
              <strong>Miel déshydraté</strong> ·
              <strong>Sucre de coco</strong> ·
              <strong>Nectar d'agave</strong>
            </p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">⚠️</span>
          <div class="guide-checklist__body">
            <strong>Le piège du « sans sucres ajoutés »</strong>
            <p>Un jus « 100 % fruit sans sucres ajoutés » peut contenir 10–12 g de sucres pour 100 ml — plus qu'un Coca-Cola (10,6 g/100 ml). Le fructose du jus concentré est chimiquement identique au sucre ajouté.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">📊</span>
          <div class="guide-checklist__body">
            <strong>Le seuil à garder en tête</strong>
            <p>L'OMS recommande de maintenir les sucres libres (ajoutés + issus des jus) sous <strong>10 % des calories journalières</strong> — environ 50 g par jour pour un adulte à 2000 kcal. De préférence sous 5 % (25 g).</p>
          </div>
        </li>
      </ul>
    </section>

