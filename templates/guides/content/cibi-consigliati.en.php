    <section id="principio-base" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">1</div>
        <h2 class="guide-section-title">The basic principle: substitute, don't eliminate</h2>
      </div>

      <p>
        This isn't about strict diets or feeling guilty every time you buy something packaged. It's about knowing the alternatives — and choosing them when they're as available and practical as the product they replace.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🔄</span>
          <div class="guide-checklist__body">
            <strong>Same effort, less processing</strong>
            <p>Plain yoghurt instead of flavoured yoghurt with thickeners. Brown rice instead of pre-cooked rice in a pouch. Canned pulses in water instead of a ready meal with legumes. The convenience is almost identical; the quality is not.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🎯</span>
          <div class="guide-checklist__body">
            <strong>The 80/20 rule works</strong>
            <p>If 80% of what you eat is Group 1–2 (see NOVA classification), the 20% of more processed products makes no real difference. Dietary health is measured on a weekly average, not on a single meal.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">💡</span>
          <div class="guide-checklist__body">
            <strong>"Recommended" foods aren't all organic or expensive</strong>
            <p>Canned chickpeas, eggs, wholemeal flour, plain yoghurt, oats, lentils, seasonal vegetables, sardines in oil — these are among the healthiest foods available and cost less than almost any equivalent packaged product.</p>
          </div>
        </li>
      </ul>
    </section>

    <hr class="guide-divider" />

    <!-- SECTION 2 -->
    <section id="cereali" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">2</div>
        <h2 class="guide-section-title">Grains and carbohydrates: the best choices</h2>
      </div>

      <p>
        Carbohydrates are not the problem — the problem is the degree of refining and processing. A whole grain is radically different from a refined grain puffed with sugar syrup.
      </p>

      <div class="signal-cards">
        <div class="signal-card signal-card--ok">
          <span class="signal-card__emoji">✅</span>
          <div class="signal-card__content">
            <strong>Recommended grains</strong>
            <p>
              <strong>Rolled oats</strong> (no added ingredients) ·
              <strong>Brown rice</strong> or semi-wholegrain ·
              <strong>Spelt grain</strong> ·
              <strong>Pearl barley</strong> ·
              <strong>Buckwheat</strong> ·
              <strong>Wholemeal pasta</strong> made only from wholemeal semolina ·
              <strong>Bread made only from wholemeal flour</strong>, water, salt, sourdough (maximum 4–5 ingredients)
            </p>
          </div>
        </div>
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🚩</span>
          <div class="signal-card__content">
            <strong>Limit these</strong>
            <p>Sugary breakfast cereals (including "wholegrain" ones with syrup), sandwich bread with modified starches and preservatives, crackers with palm oil and flavourings, pre-cooked rice and pasta in pouches with additives, industrial snack cakes and biscuits.</p>
          </div>
        </div>
      </div>

      <div class="guide-insight">
        <strong>Quick tip for breakfast cereals:</strong> read the nutrition table. If sugars are above 10 g/100 g, you're eating a dessert product — not a healthy breakfast.
      </div>
    </section>

    <hr class="guide-divider" />

    <!-- SECTION 3 -->
    <section id="proteine" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">3</div>
        <h2 class="guide-section-title">Protein: the best sources</h2>
      </div>

      <p>
        Good-quality protein is found in both the animal and plant worlds. The point is not the source, but the degree of processing: a grilled chicken breast and a chicken nugget are two completely different things.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🐟</span>
          <div class="guide-checklist__body">
            <strong>Fish and seafood</strong>
            <p>Fresh or frozen fish (without batter), sardines and mackerel in olive oil, tuna in water, salt-packed anchovies (rinsed). Oily fish is one of the most affordable and nutritious protein sources available.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🥚</span>
          <div class="guide-checklist__body">
            <strong>Eggs and dairy</strong>
            <p>Whole eggs (not just whites), plain Greek yoghurt, ricotta, cottage cheese, aged cheeses in moderation. Avoid industrial egg substitutes, "protein" yoghurts with thickeners and sweeteners, processed cheese slices with phosphates.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🌿</span>
          <div class="guide-checklist__body">
            <strong>Legumes: the most underrated plant protein</strong>
            <p>Lentils, chickpeas, beans, peas, edamame. Canned in water, they're already cooked and can be added to any meal in 30 seconds. They're inexpensive, high in fibre, and have an excellent amino acid profile when combined with grains.</p>
          </div>
        </li>
      </ul>
    </section>

    <hr class="guide-divider" />

    <!-- SECTION 4 -->
    <section id="grassi-buoni" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">4</div>
        <h2 class="guide-section-title">Good fats: the easiest ones to add</h2>
      </div>

      <p>
        Fat doesn't inherently make you gain weight. Quality fats — extra-virgin olive oil, nuts, avocado, oily fish — are among the most protective components for the cardiovascular system.
      </p>

      <div class="signal-cards">
        <div class="signal-card signal-card--ok">
          <span class="signal-card__emoji">🫒</span>
          <div class="signal-card__content">
            <strong>Recommended sources of unsaturated fats</strong>
            <p>Extra-virgin olive oil (raw or for brief cooking), raw linseed oil, walnuts, almonds, hazelnuts, flaxseeds, chia seeds, avocado, oily fish (salmon, mackerel, sardines, tuna), whole eggs.</p>
          </div>
        </div>
        <div class="signal-card signal-card--warning">
          <span class="signal-card__emoji">⚠️</span>
          <div class="signal-card__content">
            <strong>In moderation</strong>
            <p>Butter (saturated but natural), coconut oil (high in saturated fat), whole milk. These are not ultra-processed, but excessive consumption of saturated fat remains unfavourable for cardiovascular health.</p>
          </div>
        </div>
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🚩</span>
          <div class="signal-card__content">
            <strong>Fats to avoid</strong>
            <p>Margarines with partially hydrogenated fats, vegetable shortening, refined palm oil in industrial baked goods, vegetable cream with emulsifiers, seed oils rectified at high temperatures.</p>
          </div>
        </div>
      </div>
    </section>

