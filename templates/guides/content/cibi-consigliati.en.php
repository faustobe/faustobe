    <!-- SECTION 5 -->
    <section id="spuntini" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">5</div>
        <h2 class="guide-section-title">Smart snacks: what to always keep on hand</h2>
      </div>

      <p>
        Industrial snacks (snack cakes, packaged treats, ultra-processed protein bars) are among the main sources of ultra-processed foods in the daily diet. Here's what to keep instead.
      </p>

      <ol class="guide-step-list">
        <li>
          <span class="guide-step-num">🥜</span>
          <div class="guide-step-body">
            <strong>Plain nuts and seeds</strong>
            <p>Walnuts, almonds, hazelnuts, cashews with no added salt. A handful (30 g) provides protein, healthy fats and minerals. Avoid the "honey-roasted" versions with syrups and additives.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">🍎</span>
          <div class="guide-step-body">
            <strong>Whole fresh fruit</strong>
            <p>Whole fruit has fibre that slows sugar absorption. An apple, a pear, a kiwi — not fruit juice, which has lost almost all its fibre and is often classified as ultra-processed.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">🥛</span>
          <div class="guide-step-body">
            <strong>Plain Greek yoghurt</strong>
            <p>High protein content, low glycaemic index, no additives. Add your own honey or fresh fruit if you want sweetness — that way you control the sugar.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">🧀</span>
          <div class="guide-step-body">
            <strong>Hard cheese + wholegrain crackers</strong>
            <p>A little parmesan or pecorino on wholegrain crackers with a short ingredients list (wholemeal flour, oil, salt, yeast). Long-lasting satiety, simple ingredients.</p>
          </div>
        </li>
      </ol>
    </section>

