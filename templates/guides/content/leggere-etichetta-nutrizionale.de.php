      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">1</div>
        <h2 class="guide-section-title">Die Zutatenliste: worauf man zuerst schauen sollte</h2>
      </div>

      <p>
        Per Gesetz sind die Zutaten in <strong>absteigender Reihenfolge nach Gewicht</strong> aufgef&uuml;hrt: Was zuerst erscheint, ist in gr&ouml;&szlig;ter Menge enthalten. Das ist der wichtigste Ausgangspunkt beim Lesen eines Etiketts.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🥇</span>
          <div class="guide-checklist__body">
            <strong>Die erste Zutat ist die wichtigste</strong>
            <p>Wenn die erste Zutat &bdquo;Zucker&ldquo;, &bdquo;Glukose-Fruktose-Sirup&ldquo; oder &bdquo;Palmfett&ldquo; ist, ist das Produkt rund um diese Zutat gebaut. Bei Schokoladenkeksen steht die Schokolade oft erst an vierter oder f&uuml;nfter Stelle.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">📏</span>
          <div class="guide-checklist__body">
            <strong>L&auml;nge der Liste = Grad der Verarbeitung</strong>
            <p>Selbst gebackenes Brot: Mehl, Wasser, Salz, Hefe &mdash; 4 Zutaten. Industriebrot aus dem Supermarkt: kann 15 oder mehr Zutaten haben. Die L&auml;nge ist kein Vergehen f&uuml;r sich, aber immer ein Signal, das man beachten sollte.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🔍</span>
          <div class="guide-checklist__body">
            <strong>Auf schwer auszusprechende Namen achten</strong>
            <p>Begriffe wie &bdquo;modifizierte St&auml;rke&ldquo;, &bdquo;Mono- und Diglyceride der Speisefetts&auml;uren&ldquo;, &bdquo;Carboxymethylcellulose&ldquo; oder &bdquo;hydriert&ldquo; deuten auf industrielle Zutaten hin, die in hausgemachten Rezepten kaum vorkommen.</p>
          </div>
        </li>
      </ul>

      <div class="guide-insight">
        <strong>Praktische Faustregel:</strong> Wenn du nicht alle Zutaten in einem normalen Lebensmittelgesch&auml;ft finden w&uuml;rdest, ist das Produkt wahrscheinlich stark verarbeitet.
      </div>
    </section>

