    <section id="sale-nascosto" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">4</div>
        <h2 class="guide-section-title">Verstecktes Salz: wo es sich wirklich verbirgt</h2>
      </div>

      <p>
        Salz findet sich nicht nur in salzigen Produkten. In vielen scheinbar s&uuml;&szlig;en Lebensmitteln &mdash; Keksen, M&uuml;sli, Brot &mdash; ist der Salzgehalt &uuml;berraschend hoch.
      </p>

      <div class="signal-cards">
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🍞</span>
          <div class="signal-card__content">
            <strong>Brot und Backwaren</strong>
            <p>Zwei Scheiben Industriebrot k&ouml;nnen 0,5&ndash;0,8 g Salz enthalten. Multipliziert man das mit 4 Mahlzeiten pro Tag, kommt man bereits auf die H&auml;lfte der empfohlenen Tagesmenge (5 g).</p>
          </div>
        </div>
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🥣</span>
          <div class="signal-card__content">
            <strong>Fr&uuml;hst&uuml;cksflocken</strong>
            <p>Selbst &bdquo;gesunde&ldquo; und &bdquo;Vollkorn&ldquo;-Varianten k&ouml;nnen 0,8&ndash;1,2 g Salz pro 100 g enthalten &mdash; mehr als viele salzige Snacks. Das Lesen des Etiketts &uuml;berrascht hier fast immer.</p>
          </div>
        </div>
        <div class="signal-card signal-card--warning">
          <span class="signal-card__emoji">🫙</span>
          <div class="signal-card__content">
            <strong>So&szlig;en, Dressings und W&uuml;rzmittel</strong>
            <p>Fertige Tomatensauce, Mayonnaise, Salatdressings: Sie k&ouml;nnen zwischen 1 und 3 g Salz pro 100 g enthalten. Hier schl&auml;gt das Selbermachen das Industrieprodukt immer.</p>
          </div>
        </div>
      </div>

      <div class="guide-insight">
        <strong>Hinweis:</strong> Auf dem Etikett steht m&ouml;glicherweise &bdquo;Natrium&ldquo; statt &bdquo;Salz&ldquo;. Zum Umrechnen gilt: <strong>Salz = Natrium &times; 2,5</strong>.
      </div>
    </section>

