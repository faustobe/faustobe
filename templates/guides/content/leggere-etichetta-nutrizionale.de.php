    <section id="claim" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">5</div>
        <h2 class="guide-section-title">N&auml;hrwertbezogene Angaben: was sie wirklich bedeuten</h2>
      </div>

      <p>
        N&auml;hrwertbezogene Angaben sind Aussagen wie &bdquo;fettfrei&ldquo;, &bdquo;light&ldquo;, &bdquo;ballaststoffreich&ldquo; oder &bdquo;0 % Zucker&ldquo;, die auf der Vorderseite der Verpackung gro&szlig; gedruckt stehen. Sie sind gesetzlich geregelt, k&ouml;nnen aber irref&uuml;hrend sein.
      </p>

      <div class="rating-grid">
        <div class="rating-block rating-block--yellow">
          <div class="rating-block__dot">🏷️</div>
          <div class="rating-block__body">
            <strong>&bdquo;Ohne Zuckerzusatz&ldquo;</strong>
            <ul>
              <li>Bedeutet nicht zuckerfrei: kann nat&uuml;rliche Zucker enthalten (Fruchtzucker aus Saft, Laktose)</li>
              <li>Kann k&uuml;nstliche S&uuml;&szlig;ungsmittel oder Zuckeralkohole enthalten</li>
              <li>Entspricht nicht &bdquo;gesund&ldquo; oder &bdquo;f&uuml;r Diabetiker geeignet&ldquo;</li>
            </ul>
          </div>
        </div>
        <div class="rating-block rating-block--yellow">
          <div class="rating-block__dot">🏷️</div>
          <div class="rating-block__body">
            <strong>&bdquo;Light&ldquo; oder &bdquo;reduziert&ldquo;</strong>
            <ul>
              <li>Bedeutet um 30 % reduziert im Vergleich zum Referenzprodukt</li>
              <li>Wenn das Ausgangsprodukt sehr kalorienreich war, ist die Light-Version immer noch kalorienreich</li>
              <li>Oft wird das entfernte Fett durch Zucker oder Verdickungsmittel ersetzt</li>
            </ul>
          </div>
        </div>
        <div class="rating-block rating-block--yellow">
          <div class="rating-block__dot">🏷️</div>
          <div class="rating-block__body">
            <strong>&bdquo;Ballaststoffreich&ldquo; oder &bdquo;Proteinquelle&ldquo;</strong>
            <ul>
              <li>Erfordert gesetzliche Mindestwerte (Ballaststoffe: mind. 6 g/100 g; Proteine: mind. 20 % der Kalorien)</li>
              <li>Sagt nichts &uuml;ber die anderen Zutaten aus: ein &bdquo;ballaststoffreicher&ldquo; Keks kann viel Zucker enthalten</li>
              <li>Immer die Zutatenliste lesen, nicht nur die Angabe auf der Vorderseite</li>
            </ul>
          </div>
        </div>
      </div>
    </section>

