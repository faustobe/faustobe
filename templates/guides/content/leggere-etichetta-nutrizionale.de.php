    <section id="checklist" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">6</div>
        <h2 class="guide-section-title">Schnell-Checkliste f&uuml;r den Supermarkt</h2>
      </div>

      <p>
        Du musst nicht jedes Mal alles pr&uuml;fen. Fang mit den schnellsten Kontrollen an und erg&auml;nze die anderen, sobald es zur Gewohnheit wird.
      </p>

      <ol class="guide-step-list">
        <li>
          <span class="guide-step-num">1</span>
          <div class="guide-step-body">
            <strong>Die erste Zutat ansehen</strong>
            <p>Ist es ein echtes Lebensmittel (Mehl, Milch, Tomate, &Ouml;l)? Gut. Ist es ein Zucker, Sirup oder verarbeitetes Fett? &Uuml;berlege, ob es eine Alternative gibt.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">2</span>
          <div class="guide-step-body">
            <strong>Zutaten z&auml;hlen</strong>
            <p>Weniger als 5: ausgezeichnet. Von 5 bis 10: akzeptabel. &Uuml;ber 10&ndash;12: genau pr&uuml;fen, was darin steckt.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">3</span>
          <div class="guide-step-body">
            <strong>E-Nummern pr&uuml;fen</strong>
            <p>Wenige oder keine? Gut. Viele (vor allem Farbstoffe, S&uuml;&szlig;ungsmittel, Konservierungsstoffe)? Das Produkt ist stark verarbeitet.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">4</span>
          <div class="guide-step-body">
            <strong>Salz und Zucker pro 100 g ansehen</strong>
            <p>Salz &uuml;ber 1,5 g/100 g = hoch. Zucker &uuml;ber 22,5 g/100 g = hoch. Ges&auml;ttigte Fetts&auml;uren &uuml;ber 5 g/100 g = hoch.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">5</span>
          <div class="guide-step-body">
            <strong>Die gro&szlig;en Aufdrucke auf der Vorderseite ignorieren</strong>
            <p>&bdquo;Vollkorn&ldquo;, &bdquo;nat&uuml;rlich&ldquo;, &bdquo;0 % Fett&ldquo; bedeuten fast nie das, was sie suggerieren. Vertraue immer der Zutatenliste, nicht den Werbebotschaften.</p>
          </div>
        </li>
      </ol>

      <div class="guide-insight">
        <strong>Der einfachste Trick von allen:</strong> Wenn du alle Zutaten erkennst und sie zu Hause kochen k&ouml;nntest, bist du auf dem richtigen Weg. Wenn nicht, hast du ein Ultra-Verarbeitetes gefunden.
      </div>
    </section>

