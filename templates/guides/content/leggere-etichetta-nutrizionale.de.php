    <section id="porzioni" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">3</div>
        <h2 class="guide-section-title">Portionen vs. 100 g: der Trick, der verwirrt</h2>
      </div>

      <p>
        N&auml;hrwertangaben werden h&auml;ufig sowohl pro 100 g als auch &bdquo;pro Portion&ldquo; angegeben. Hersteller neigen dazu, die Portionswerte hervorzuheben, da sie niedriger wirken &mdash; doch die Portionsangaben sind oft zu niedrig angesetzt.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🎭</span>
          <div class="guide-checklist__body">
            <strong>Fr&uuml;hst&uuml;cksflocken: deklarierte Portion 30 g, gegessen werden 60 g</strong>
            <p>Wenn die Packung 10 g Zucker pro Portion (30 g) angibt und du eine Sch&uuml;ssel von 60 g isst, nimmst du 20 g Zucker zu dir &mdash; fast die H&auml;lfte der empfohlenen Tagesmenge.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">📊</span>
          <div class="guide-checklist__body">
            <strong>Immer die Werte pro 100 g zum Vergleich verwenden</strong>
            <p>Du m&ouml;chtest wissen, welche Pasta von zwei Marken besser ist? Vergleiche die Werte pro 100 g, nicht pro Portion (die von Marke zu Marke variiert). Nur so ist der Vergleich fair.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🧮</span>
          <div class="guide-checklist__body">
            <strong>Die tats&auml;chliche Portion berechnen</strong>
            <p>Schau nach, wie viele Portionen in der Packung enthalten sind. Wenn eine T&uuml;te Chips &bdquo;3,5 Portionen&ldquo; enth&auml;lt und du sie ganz isst, multipliziere alle Werte mit 3,5.</p>
          </div>
        </li>
      </ul>
    </section>

