    <section id="tabella-nutrizionale" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">2</div>
        <h2 class="guide-section-title">Die N&auml;hrwerttabelle: worauf man achten sollte (und was man ignorieren kann)</h2>
      </div>

      <p>
        Die N&auml;hrwerttabelle ist auf fast allen verpackten Produkten Pflicht. Sie enth&auml;lt viele Informationen, aber drei Werte gen&uuml;gen, um ein allgemeines Bild zu erhalten.
      </p>

      <div class="signal-cards">
        <div class="signal-card signal-card--ok">
          <span class="signal-card__emoji">✅</span>
          <div class="signal-card__content">
            <strong>Zucker: weniger als 5 g pro 100 g</strong>
            <p>Werte unter 5 g/100 g deuten auf einen geringen Zuckergehalt hin. Zwischen 5 und 22,5 g ist er mittel; &uuml;ber 22,5 g ist er hoch. F&uuml;r Getr&auml;nke gilt eine Grenze von 2,5 g/100 ml als &bdquo;niedrig&ldquo;.</p>
          </div>
        </div>
        <div class="signal-card signal-card--warning">
          <span class="signal-card__emoji">⚠️</span>
          <div class="signal-card__content">
            <strong>Salz: weniger als 0,3 g pro 100 g</strong>
            <p>Unter 0,3 g/100 g ist niedrig, &uuml;ber 1,5 g/100 g ist hoch. Viele salzige Produkte wie Cracker, K&auml;se und Wurstwaren &uuml;berschreiten leicht 1 g/100 g. Achtung: Der auf dem Etikett angegebene Natriumwert muss mit 2,5 multipliziert werden, um den Salzgehalt zu erhalten.</p>
          </div>
        </div>
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🚩</span>
          <div class="signal-card__content">
            <strong>Ges&auml;ttigte Fetts&auml;uren: weniger als 1,5 g pro 100 g</strong>
            <p>Dies ist die Fettart, die am meisten Beachtung verdient. Werte &uuml;ber 5 g/100 g gelten als hoch. Industrielles Geb&auml;ck, Fertigsnacks und Fertiggerichte &uuml;berschreiten diesen Wert h&auml;ufig deutlich.</p>
          </div>
        </div>
      </div>
    </section>

