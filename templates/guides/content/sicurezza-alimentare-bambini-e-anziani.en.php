    <!-- SECTION 4 -->
    <section id="prodotti-bambini" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">4</div>
        <h2 class="guide-section-title">"For children" products to look at closely</h2>
      </div>

      <p>
        "For children" marketing is often the opposite of what it suggests. Products featuring colourful characters, mascots and claims like "with vitamins" or "energy for growing" are frequently among the most processed items on the shelf.
      </p>

      <div class="signal-cards">
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🚩</span>
          <div class="signal-card__content">
            <strong>Breakfast cereals "for children"</strong>
            <p>These often contain 25–35 g of sugars per 100 g (equivalent to 7–9 teaspoons per 100 g), artificial colorings, complex flavourings and added vitamins to compensate for the poor nutritional quality of the base ingredients. Alternative: rolled oats + fresh fruit + honey.</p>
          </div>
        </div>
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🚩</span>
          <div class="signal-card__content">
            <strong>"Fruit juice" drinks</strong>
            <p>Products like "orange drink" or "multifruit juice" may contain only 10–30% real juice; the rest is water, sugars, acidifiers and colorings. They are not equivalent to 100% juice. Always check the percentage of fruit on the label.</p>
          </div>
        </div>
        <div class="signal-card signal-card--warning">
          <span class="signal-card__emoji">⚠️</span>
          <div class="signal-card__content">
            <strong>"Children's" fruit yoghurts</strong>
            <p>Many contain more sugar than a standard adult yoghurt, plus thickeners (pectin, modified starch) and artificial flavourings. Plain Greek yoghurt with fresh fruit added at the moment of eating is always a better choice — and often costs less.</p>
          </div>
        </div>
      </div>
    </section>

