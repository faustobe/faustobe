    <section id="piu-vulnerabili" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">1</div>
        <h2 class="guide-section-title">Why children and the elderly are more vulnerable</h2>
      </div>

      <p>
        The same dose of an additive that a healthy adult metabolises without issue can have different effects on a young child or an elderly person. This isn't alarmism: it's physiology.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">👶</span>
          <div class="guide-checklist__body">
            <strong>Children: low body weight, immature systems</strong>
            <p>The acceptable daily intake (ADI) for additives is calculated in mg per kg of body weight. A 15 kg child eating a 30 g snack containing colorings receives a relative dose 4–5 times higher than a 70 kg adult. In addition, the liver and kidneys of young children metabolise certain substances more slowly.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">👴</span>
          <div class="guide-checklist__body">
            <strong>The elderly: reduced kidney and liver function</strong>
            <p>With age, the capacity to eliminate certain substances decreases. Multiple medications (polypharmacy) can interact with some additives. Excess sodium has a greater impact on those with hypertension or kidney insufficiency.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🔬</span>
          <div class="guide-checklist__body">
            <strong>Cumulative effects: habitual diet matters more than a single meal</strong>
            <p>An ice cream with colorings once a month is not a problem. If children daily eat products containing E102, E110, E122 at breakfast, as a snack, at lunch and in their afternoon drink — the cumulative effect becomes significant.</p>
          </div>
        </li>
      </ul>
    </section>

    <hr class="guide-divider" />

    <!-- SECTION 2 -->
    <section id="additivi-bambini" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">2</div>
        <h2 class="guide-section-title">Critical additives for children</h2>
      </div>

      <p>
        Not all E codes are problematic for children. The most relevant categories, with stronger scientific evidence, are these.
      </p>

      <div class="rating-grid">
        <div class="rating-block rating-block--red">
          <div class="rating-block__dot">🔴</div>
          <div class="rating-block__body">
            <strong>Azo dyes — avoid with children</strong>
            <ul>
              <li>E102 (Tartrazine), E104 (Quinoline Yellow), E110 (Sunset Yellow FCF)</li>
              <li>E122 (Carmoisine), E124 (Ponceau 4R), E129 (Allura Red AC)</li>
              <li>In Europe a mandatory label warning is required: <em>"may have an adverse effect on activity and attention in children"</em></li>
              <li>Where to find them: coloured sweets, orange/red fizzy drinks, jellies, industrial cakes, coloured breakfast cereals</li>
            </ul>
          </div>
        </div>
        <div class="rating-block rating-block--yellow">
          <div class="rating-block__dot">🟡</div>
          <div class="rating-block__body">
            <strong>Intense sweeteners — not approved for young children</strong>
            <ul>
              <li>E951 (Aspartame), E950 (Acesulfame K), E955 (Sucralose), E954 (Saccharin)</li>
              <li>Not permitted in foods specifically intended for infants and young children (EU Reg. 1333/2008)</li>
              <li>Where they often appear: "0% sugar" yoghurts, "light" or "zero" drinks, cough syrups, sugar-free sweets, some fruit juices</li>
            </ul>
          </div>
        </div>
        <div class="rating-block rating-block--yellow">
          <div class="rating-block__dot">🟡</div>
          <div class="rating-block__body">
            <strong>Caffeine — often hidden</strong>
            <ul>
              <li>Not an E additive, but present in energy drinks, some soft drinks and "coffee-flavoured" snacks</li>
              <li>EFSA recommends children consume no more than 3 mg/kg/day of caffeine</li>
              <li>A 250 ml can of energy drink can contain up to 80 mg of caffeine — nearly double the limit for a 15 kg child</li>
            </ul>
          </div>
        </div>
      </div>
    </section>

