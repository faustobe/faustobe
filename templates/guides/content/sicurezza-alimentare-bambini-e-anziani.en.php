      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">5</div>
        <h2 class="guide-section-title">A safe shopping guide</h2>
      </div>

      <p>
        A practical guide to safe purchases for anyone shopping for children and older adults.
      </p>

      <ol class="guide-step-list">
        <li>
          <span class="guide-step-num">1</span>
          <div class="guide-step-body">
            <strong>For children: choose products with few recognisable ingredients</strong>
            <p>The shorter the ingredients list, the better. Avoid products with more than 2–3 colorings, artificial sweeteners, or the wording "may have an adverse effect on activity and attention in children".</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">2</span>
          <div class="guide-step-body">
            <strong>Always check sugars in "for children" products</strong>
            <p>Threshold: less than 10 g/100 g for breakfast items and snacks. Above that it's already a treat, not a food suitable for children's daily consumption.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">3</span>
          <div class="guide-step-body">
            <strong>For the elderly: prioritise low sodium content</strong>
            <p>Favour products with less than 0.3 g of salt/100 g. Choose tuna in water rather than in salted seed oil, frozen vegetables with no added salt, homemade or low-sodium stocks.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">4</span>
          <div class="guide-step-body">
            <strong>Scan with the app before you buy</strong>
            <p>E-Codes Reader instantly shows whether a product contains azo dyes with the European warning, sweeteners unsuitable for children, and the sodium level — without having to read every line of the label.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">5</span>
          <div class="guide-step-body">
            <strong>Always prefer Group 1–2 (NOVA) foods for main meals</strong>
            <p>Vegetables, legumes, eggs, fish, plain yoghurt, whole fruit. These are the safest foundation for both vulnerable groups — with no need to check complex labels.</p>
          </div>
        </li>
      </ol>

