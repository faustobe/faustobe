    <section id="anziani" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">3</div>
        <h2 class="guide-section-title">What to limit for the elderly</h2>
      </div>

      <p>
        For older people the priorities are different from those for children. The main concerns relate to sodium, phosphates and certain preservatives.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🧂</span>
          <div class="guide-checklist__body">
            <strong>Sodium: the main risk</strong>
            <p>Hypertension and kidney insufficiency are among the most common conditions in older adults, and sodium worsens both. Ready meals, cured meats, aged cheeses and industrial stock are the main sources. Target: less than 5 g of salt per day, and less for those with kidney problems or documented hypertension.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🦴</span>
          <div class="guide-checklist__body">
            <strong>Phosphates: they interfere with calcium and bones</strong>
            <p>Phosphates (E338–E341, E450–E452) are common in processed meat products, processed cheese, canned meat and ready meals. High consumption of phosphates from additives interferes with calcium absorption — a concern for those at risk of osteoporosis.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">💊</span>
          <div class="guide-checklist__body">
            <strong>Interactions with common medications</strong>
            <p>Some substances in foods can interact with medications commonly taken by older adults: vitamin K in leafy green vegetables with warfarin, grapefruit with statins, cranberry juice with certain anticoagulants. The goal is not to avoid these foods, but to keep the diet consistent and inform your doctor about your eating habits.</p>
          </div>
        </li>
      </ul>
    </section>

