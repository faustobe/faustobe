        <div class="guide-section-number" aria-hidden="true">1</div>
        <h2 class="guide-section-title">Warum Kinder und &auml;ltere Menschen anf&auml;lliger sind</h2>
      </div>

      <p>
        Die gleiche Zusatzstoffdosis, die ein gesunder Erwachsener problemlos metabolisiert, kann bei einem Kleinkind oder einer &auml;lteren Person andere Auswirkungen haben. Das ist kein Alarmismus: Es ist Physiologie.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">👶</span>
          <div class="guide-checklist__body">
            <strong>Kinder: geringes K&ouml;rpergewicht, unreife Systeme</strong>
            <p>Die akzeptable Tagesdosis (ADI) von Zusatzstoffen wird in mg pro kg K&ouml;rpergewicht berechnet. Ein 15 kg schweres Kind, das einen 30-g-Snack mit Farbstoffen isst, erh&auml;lt eine relative Dosis, die 4&ndash;5 Mal h&ouml;her ist als die eines 70 kg schweren Erwachsenen. Au&szlig;erdem metabolisieren Leber und Nieren kleiner Kinder einige Substanzen langsamer.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">👴</span>
          <div class="guide-checklist__body">
            <strong>&Auml;ltere Menschen: eingeschr&auml;nkte Nieren- und Leberfunktion</strong>
            <p>Mit zunehmendem Alter nimmt die F&auml;higkeit ab, bestimmte Substanzen auszuscheiden. Mehrere Medikamente gleichzeitig (Polypharmazie) k&ouml;nnen mit bestimmten Zus&auml;tzen interagieren. &Uuml;berm&auml;&szlig;iges Natrium wirkt sich st&auml;rker auf Menschen mit Bluthochdruck oder Niereninsuffizienz aus.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🔬</span>
          <div class="guide-checklist__body">
            <strong>Kumulative Effekte: die gew&ouml;hnliche Ern&auml;hrung z&auml;hlt mehr als die einzelne Mahlzeit</strong>
            <p>Ein Eis mit Farbstoffen einmal im Monat ist kein Problem. Wenn Kinder t&auml;glich Produkte mit E102, E110, E122 zum Fr&uuml;hst&uuml;ck, als Snack, zum Mittagessen und im Nachmittagsgetr&auml;nk zu sich nehmen, wird der kumulative Effekt relevant.</p>
          </div>
        </li>
      </ul>
    </section>

