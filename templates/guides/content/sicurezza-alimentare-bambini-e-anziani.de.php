        <div class="guide-section-number" aria-hidden="true">3</div>
        <h2 class="guide-section-title">Was &auml;ltere Menschen einschr&auml;nken sollten</h2>
      </div>

      <p>
        Bei &auml;lteren Menschen liegen die Priorit&auml;ten anders als bei Kindern. Die Hauptproblembereiche betreffen Natrium, Phosphate und bestimmte Konservierungsstoffe.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🧂</span>
          <div class="guide-checklist__body">
            <strong>Natrium: das Hauptrisiko</strong>
            <p>Bluthochdruck und Niereninsuffizienz geh&ouml;ren zu den h&auml;ufigsten Erkrankungen bei &auml;lteren Menschen, und Natrium verst&auml;rkt sie. Fertiggerichte, Aufschnitt, Hardk&auml;se und Instant-Br&uuml;hen sind die wichtigsten Vektoren. Ziel: weniger als 5 g Salz pro Tag, und weniger f&uuml;r Menschen mit Nierenproblemen oder dokumentiertem Bluthochdruck.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🦴</span>
          <div class="guide-checklist__body">
            <strong>Phosphate: st&ouml;ren Kalzium und Knochen</strong>
            <p>Phosphate (E338&ndash;E341, E450&ndash;E452) kommen h&auml;ufig in verarbeiteten Fleischprodukten, Schmelzk&auml;se, Fleischkonserven und Fertiggerichten vor. Ein hoher Konsum von Phosphaten aus Zus&auml;tzen st&ouml;rt die Kalziumaufnahme &mdash; ein Problem f&uuml;r Menschen mit Osteoporose-Risiko.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">💊</span>
          <div class="guide-checklist__body">
            <strong>Wechselwirkungen mit g&auml;ngigen Medikamenten</strong>
            <p>Bestimmte Lebensmittelinhaltsstoffe k&ouml;nnen mit bei &auml;lteren Menschen h&auml;ufigen Medikamenten interagieren: Vitamin K in gr&uuml;nem Blattgem&uuml;se mit Cumarinen, Grapefruit mit Statinen, Cranberrysaft mit bestimmten Antikoagulanzien. Es geht nicht darum, diese Lebensmittel zu meiden, sondern die Ern&auml;hrung konstant zu halten und die Essgewohnheiten dem Arzt mitzuteilen.</p>
          </div>
        </li>
      </ul>
    </section>

