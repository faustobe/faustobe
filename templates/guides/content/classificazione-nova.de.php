    <section id="gruppo-4" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">5</div>
        <h2 class="guide-section-title">Gruppe 4 &mdash; Ultra-Verarbeitetes: die Gruppe, die man einschr&auml;nken sollte</h2>
      </div>

      <p>
        Ultra-verarbeitete Lebensmittel sind industrielle Zubereitungen, die keine ganzen Lebensmittel enthalten oder diese nur in minimalem Umfang verwenden. Sie werden aus extrahierten, ger einigten oder chemisch ver&auml;nderten Zutaten sowie gro&szlig;en Mengen technologischer Zus&auml;tze hergestellt.
      </p>

      <p>
        Das Schll&uuml;sselsignal: Sie enthalten Zutaten, die <strong>in einer Haushaltsк&uuml;che nicht vorkommen</strong>.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🚩</span>
          <div class="guide-checklist__body">
            <strong>Typische Zutaten der Gruppe 4</strong>
            <p>Hydrolysierte Proteine, modifizierte St&auml;rken, Glukose-Fruktose-Sirup, hydrierte &Ouml;le, &bdquo;naturidentische&ldquo; Aromen, k&uuml;nstliche Farbstoffe, intensive S&uuml;&szlig;ungsmittel (Aspartam, Saccharin, Sucralose), Emulgatoren (E471, E472), Stabilisatoren, mehrere chemische Triebmittel.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">📦</span>
          <div class="guide-checklist__body">
            <strong>H&auml;ufige Beispiele</strong>
            <p>Industriegeb&auml;ck, gezuckerte Fr&uuml;hst&uuml;cksflocken, Softdrinks und Energy Drinks, Toastbrot mit Zus&auml;tzen, rekonstituierte W&uuml;rstchen und Aufschnitt, Chicken Nuggets und Fischst&auml;bchen, aromatisierter Joghurt mit Verdickungsmitteln, tiefgefrorene Fertiggerichte, Industriepizza, So&szlig;en im Beutel, W&uuml;rfelbrr&uuml;he mit Geschmacksverst&auml;rkern.</p>
          </div>
        </li>
      </ul>

      <div class="guide-insight">
        <strong>Wichtige Daten:</strong> In Deutschland stammen etwa 15&ndash;20 % der t&auml;glichen Kalorien aus ultra-verarbeiteten Lebensmitteln. In Gro&szlig;britannien und den USA liegt der Anteil bei &uuml;ber 50&ndash;60 %. Forschungen zeigen, dass bereits ein Konsum von mehr als 20 % der t&auml;glichen Kalorien aus NOVA 4 mit Gesundheitsrisiken verbunden ist.
      </div>
    </section>

