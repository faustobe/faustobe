      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">6</div>
        <h2 class="guide-section-title">NOVA beim Einkaufen in der Praxis</h2>
      </div>

      <p>
        NOVA im Supermarkt anzuwenden erfordert kein Auswendiglernen der Kategorien. Ein paar mentale Fragen beim Lesen des Etiketts gen&uuml;gen.
      </p>

      <ol class="guide-step-list">
        <li>
          <span class="guide-step-num">1</span>
          <div class="guide-step-body">
            <strong>&bdquo;K&ouml;nnte ich das zu Hause mit normalen Zutaten kochen?&ldquo;</strong>
            <p>Wenn die Antwort ja ist, ist es wahrscheinlich Gruppe 1, 2 oder 3. Wenn nein (weil es Zutaten verwendet, die es in normalen Gesch&auml;ften nicht gibt), ist es fast sicher Gruppe 4.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">2</span>
          <div class="guide-step-body">
            <strong>Nach den &bdquo;Signalw&ouml;rtern&ldquo; der Gruppe 4 suchen</strong>
            <p>&bdquo;Modifizierte St&auml;rke&ldquo;, &bdquo;isoliertes Sojaprotein&ldquo;, &bdquo;Maltodextrine&ldquo;, &bdquo;Sirup aus&ldquo;, &bdquo;komplexe Aromen&ldquo;, &bdquo;hydriertes Palmfett&ldquo;, &bdquo;Natriumcaseinat&ldquo;. Wenn diese erscheinen, bist du in Gruppe 4.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">3</span>
          <div class="guide-step-body">
            <strong>Praktische Regel: mehr als die H&auml;lfte des Tellers aus Gruppe 1</strong>
            <p>Es geht nicht darum, jedes ultra-verarbeitete Produkt zu eliminieren. Das Ziel ist, dass Gem&uuml;se, H&uuml;lsenfr&uuml;chte, Vollkorngetreide, Eier und unverarbeitetes Fleisch/Fisch die Grundlage der Hauptmahlzeiten bilden.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">4</span>
          <div class="guide-step-body">
            <strong>Die App bei Unklarheiten nutzen</strong>
            <p>E-Codes Reader zeigt die NOVA-Klassifizierung f&uuml;r jedes gescannte Produkt. Wenn das Etikett mehrdeutig ist, liefert die App in Sekunden eine Antwort.</p>
          </div>
        </li>
      </ol>

