    <section id="gruppo-1" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">2</div>
        <h2 class="guide-section-title">Gruppe 1 &mdash; Unverarbeitete oder minimal verarbeitete Lebensmittel</h2>
      </div>

      <p>
        Das sind Lebensmittel pflanzlichen oder tierischen Ursprungs, die keiner wesentlichen industriellen Verarbeitung unterzogen wurden, oder nur minimalen physikalischen Prozessen (Trocknen, Mahlen, K&uuml;hlen, Pasteurisieren), die die Natur des Lebensmittels nicht ver&auml;ndern.
      </p>

      <div class="signal-cards">
        <div class="signal-card signal-card--ok">
          <span class="signal-card__emoji">🥦</span>
          <div class="signal-card__content">
            <strong>Beispiele der Gruppe 1</strong>
            <p>Frisches oder tiefgefrorenes Obst und Gem&uuml;se, frisches oder tiefgefrorenes Fleisch und Fisch ohne Zus&auml;tze, Eier, getrocknete H&uuml;lsenfr&uuml;chte, Vollkorngetreide (Reis, Hafer, Dinkel), Vollkornmehl, frische pasteurisierte Milch, Naturjoghurt ohne Zus&auml;tze, Tee, Kaffee, Wasser.</p>
          </div>
        </div>
        <div class="signal-card signal-card--ok">
          <span class="signal-card__emoji">✅</span>
          <div class="signal-card__content">
            <strong>Praktisches Ziel: die Basis der Ern&auml;hrung</strong>
            <p>Lebensmittel der Gruppe 1 sollten den gr&ouml;&szlig;ten Teil jeder Mahlzeit ausmachen. Nicht ausschlie&szlig;lich &mdash; man kocht auch mit Zutaten der Gruppe 2 &mdash; aber der Hauptteil des Tellers.</p>
          </div>
        </div>
      </div>
    </section>

    <hr class="guide-divider" />

    <!-- ABSCHNITT 3 -->
    <section id="gruppo-2" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">3</div>
        <h2 class="guide-section-title">Gruppe 2 &mdash; Verarbeitete Küchenzutaten</h2>
      </div>

      <p>
        Das sind Substanzen, die aus Lebensmitteln der Gruppe 1 oder aus der Natur gewonnen werden und in der K&uuml;che zum Kochen, W&uuml;rzen und Zubereiten verwendet werden. Sie werden nicht allein gegessen, sondern in Kombination mit Gruppe 1.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🫙</span>
          <div class="guide-checklist__body">
            <strong>Beispiele der Gruppe 2</strong>
            <p>Oliven&ouml;l und andere pflanzliche &Ouml;le, Butter, Essig, Salz, Zucker, Honig, Weizenmehl (Typ 405), reines Maisst&auml;rke, Ahornsirup. Auch Milchpulver und traditionelle Hardk&auml;se geh&ouml;ren zur Gruppe 2.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">✅</span>
          <div class="guide-checklist__body">
            <strong>M&auml;&szlig;ig verwenden</strong>
            <p>Sie sind an sich nicht problematisch, aber &uuml;berm&auml;&szlig;iger Zucker, Salz und ges&auml;ttigte Fette bleiben ung&uuml;nstig. Die Tatsache, dass sie Gruppe 2 sind, macht sie nicht &bdquo;unbegrenzt konsumierbar&ldquo;.</p>
          </div>
        </li>
      </ul>
    </section>

    <hr class="guide-divider" />

    <!-- ABSCHNITT 4 -->
    <section id="gruppo-3" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">4</div>
        <h2 class="guide-section-title">Gruppe 3 &mdash; Verarbeitete Lebensmittel</h2>
      </div>

      <p>
        Das sind Produkte, die durch Hinzuf&uuml;gen von Zutaten der Gruppe 2 zu Lebensmitteln der Gruppe 1 entstehen, durch Verfahren wie Einsalzen, R&auml;uchern, Fermentieren oder Reifen. Sie sind erkennbar daran, dass sie oft nur 2&ndash;3 Zutaten enthalten.
      </p>

      <div class="signal-cards">
        <div class="signal-card signal-card--warning">
          <span class="signal-card__emoji">⚠️</span>
          <div class="signal-card__content">
            <strong>Beispiele der Gruppe 3</strong>
            <p>Konserviertes Gem&uuml;se und H&uuml;lsenfr&uuml;chte (mit Wasser und Salz), kandierte und eingelegte Fr&uuml;chte, ger&ouml;stete und gesalzene N&uuml;sse, Sardellen und Sardinen in &Ouml;l, qualit&auml;tsvolles roher Schinken, traditionelle K&auml;sesorten (Parmesan, Pecorino), handwerkliches Brot, Wein, handwerkliches Bier, natives Oliven&ouml;l extra mit S&auml;uerungsmittel.</p>
          </div>
        </div>
        <div class="signal-card signal-card--ok">
          <span class="signal-card__emoji">✅</span>
          <div class="signal-card__content">
            <strong>In der t&auml;glichen Ern&auml;hrung akzeptabel</strong>
            <p>Gruppe 3 muss nicht vermieden werden. Viele traditionelle und kulturell bedeutsame Lebensmittel geh&ouml;ren zu dieser Gruppe. Sie k&ouml;nnen jedoch salzreich sein (Konserven, Aufschnitt) oder zuckerreich (Frucht in Sirup): die Menge im Auge behalten.</p>
          </div>
        </div>
      </div>
    </section>

