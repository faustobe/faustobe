      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">1</div>
        <h2 class="guide-section-title">Was ist die NOVA-Klassifizierung</h2>
      </div>

      <p>
        NOVA ist ein Klassifizierungssystem f&uuml;r Lebensmittel, das von der Forschungsgruppe von Professor Carlos Monteiro an der Universit&auml;t S&atilde;o Paulo (Brasilien) entwickelt wurde und heute von der WHO, der FAO und zahlreichen weltweiten epidemiologischen Studien verwendet wird.
      </p>

      <p>
        Anders als n&auml;hrstoffbasierte Systeme (wie der Nutri-Score, der Kalorien, Fett, Zucker und Ballaststoffe bewertet), <strong>klassifiziert NOVA nach dem Grad der industriellen Verarbeitung</strong>: nicht &bdquo;wie viele Kalorien hat es&ldquo;, sondern &bdquo;wie viel Industrie steckt in seiner Herstellung&ldquo;.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">💡</span>
          <div class="guide-checklist__body">
            <strong>Warum Verarbeitung wichtig ist</strong>
            <p>Moderne Industrieprozesse k&ouml;nnen die Faserstruktur, die Bioverwertbarkeit von N&auml;hrstoffen, das Darmmikrobiom und die hormonelle S&auml;ttigungsreaktion ver&auml;ndern &mdash; unabh&auml;ngig von der endg&uuml;ltigen n&auml;hrstofflichen Zusammensetzung des Produkts.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">📋</span>
          <div class="guide-checklist__body">
            <strong>4 Gruppen, keine kontinuierliche Skala</strong>
            <p>NOVA unterteilt alle Lebensmittel in 4 verschiedene Gruppen. Es ist keine Skala von 1 bis 10, sondern eine kategorische Klassifizierung. Jede Gruppe hat klar definierte Merkmale.</p>
          </div>
        </li>
      </ul>
    </section>

    <hr class="guide-divider" />

