      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">6</div>
        <h2 class="guide-section-title">NOVA nella spesa pratica</h2>
      </div>

      <p>
        Applicare NOVA al supermercato non richiede di imparare a memoria le categorie. Bastano alcune domande mentali mentre leggi l'etichetta.
      </p>

      <ol class="guide-step-list">
        <li>
          <span class="guide-step-num">1</span>
          <div class="guide-step-body">
            <strong>"Potrei cucinarlo a casa con ingredienti normali?"</strong>
            <p>Se la risposta è sì, probabilmente è Gruppo 1, 2 o 3. Se no (perché usa ingredienti che non esistono nei negozi normali), è quasi certamente Gruppo 4.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">2</span>
          <div class="guide-step-body">
            <strong>Cerca le "parole spia" del Gruppo 4</strong>
            <p>"Amido modificato", "proteine di soia isolate", "maltodestrine", "sciroppo di", "aromi complessi", "olio di palma idrogenato", "caseinato di sodio". Se compaiono, sei nel Gruppo 4.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">3</span>
          <div class="guide-step-body">
            <strong>Regola pratica: più della metà del piatto da Gruppo 1</strong>
            <p>Non si tratta di eliminare ogni ultra-processato. L'obiettivo è che verdure, legumi, cereali in chicco, uova e carni/pesci non lavorati costituiscano la base dei tuoi pasti principali.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">4</span>
          <div class="guide-step-body">
            <strong>Usa l'app per i dubbi</strong>
            <p>E-Codes Reader mostra la classificazione NOVA per ogni prodotto scansionato. Nei casi in cui l'etichetta è ambigua, l'app ti dà una risposta in un secondo.</p>
          </div>
        </li>
      </ol>

