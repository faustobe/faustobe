    <section id="cosa-e-nova" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">1</div>
        <h2 class="guide-section-title">Cos'è la classificazione NOVA</h2>
      </div>

      <p>
        NOVA è un sistema di classificazione degli alimenti sviluppato dal gruppo del professor Carlos Monteiro all'Università di San Paolo (Brasile) e oggi adottato dall'OMS, dalla FAO e da numerosi studi epidemiologici globali.
      </p>

      <p>
        A differenza dei sistemi basati sui nutrienti (come il Nutri-Score, che valuta calorie, grassi, zuccheri e fibre), <strong>NOVA classifica in base al grado di trasformazione industriale</strong>: non "quante calorie ha", ma "quanta industria è entrata nella sua produzione".
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">💡</span>
          <div class="guide-checklist__body">
            <strong>Perché la trasformazione conta</strong>
            <p>I processi industriali moderni possono alterare la struttura delle fibre, la biodisponibilità dei nutrienti, il microbioma intestinale e la risposta ormonale alla sazietà — indipendentemente dalla composizione nutrizionale finale del prodotto.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">📋</span>
          <div class="guide-checklist__body">
            <strong>4 gruppi, non una scala continua</strong>
            <p>NOVA divide tutti gli alimenti in 4 gruppi distinti. Non è una scala da 1 a 10, ma una classificazione categorica. Ogni gruppo ha caratteristiche ben definite.</p>
          </div>
        </li>
      </ul>
    </section>

    <hr class="guide-divider" />

    <!-- SEZIONE 2 -->
    <section id="gruppo-1" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">2</div>
        <h2 class="guide-section-title">Gruppo 1 — Cibi non lavorati o minimamente lavorati</h2>
      </div>

      <p>
        Sono alimenti di origine vegetale o animale che non hanno subito nessuna trasformazione industriale significativa, o solo processi fisici minimi (essiccazione, macinazione, raffreddamento, pastorizzazione) che non alterano la natura del cibo.
      </p>

      <div class="signal-cards">
        <div class="signal-card signal-card--ok">
          <span class="signal-card__emoji">🥦</span>
          <div class="signal-card__content">
            <strong>Esempi di Gruppo 1</strong>
            <p>Frutta e verdura fresca o congelata, carne e pesce freschi o congelati senza additivi, uova, legumi secchi, cereali in chicco (riso, avena, farro), farina integrale, latte fresco pastorizzato, yogurt naturale senza additivi, tè, caffè, acqua.</p>
          </div>
        </div>
        <div class="signal-card signal-card--ok">
          <span class="signal-card__emoji">✅</span>
          <div class="signal-card__content">
            <strong>Obiettivo pratico: la base della dieta</strong>
            <p>I cibi del Gruppo 1 dovrebbero costituire la parte maggiore di ogni pasto. Non esclusivamente — si cucina con gli ingredienti del Gruppo 2 — ma la maggior parte di ciò che finisce nel piatto.</p>
          </div>
        </div>
      </div>
    </section>

    <hr class="guide-divider" />

    <!-- SEZIONE 3 -->
    <section id="gruppo-2" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">3</div>
        <h2 class="guide-section-title">Gruppo 2 — Ingredienti culinari lavorati</h2>
      </div>

      <p>
        Sono sostanze estratte da alimenti del Gruppo 1 o dalla natura, usate in cucina per cucinare, condire e preparare il cibo. Non si mangiano da soli ma in combinazione con il Gruppo 1.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🫙</span>
          <div class="guide-checklist__body">
            <strong>Esempi di Gruppo 2</strong>
            <p>Olio d'oliva e altri oli vegetali, burro, aceto, sale, zucchero, miele, farina 00, amido di mais puro, sciroppo d'acero. Anche latte in polvere e formaggi stagionati tradizionali appartengono al Gruppo 2.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">✅</span>
          <div class="guide-checklist__body">
            <strong>Usali con moderazione</strong>
            <p>Non sono problematici in sé, ma zucchero, sale e grassi saturi in eccesso rimangono sfavorevoli. Il fatto che siano Gruppo 2 non li rende "illimitati".</p>
          </div>
        </li>
      </ul>
    </section>

    <hr class="guide-divider" />

    <!-- SEZIONE 4 -->
    <section id="gruppo-3" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">4</div>
        <h2 class="guide-section-title">Gruppo 3 — Cibi lavorati</h2>
      </div>

      <p>
        Sono prodotti ottenuti aggiungendo ingredienti del Gruppo 2 ad alimenti del Gruppo 1, con processi come la conservazione sotto sale, l'affumicatura, la fermentazione o la stagionatura. Riconoscibili perché spesso contengono solo 2–3 ingredienti.
      </p>

      <div class="signal-cards">
        <div class="signal-card signal-card--warning">
          <span class="signal-card__emoji">⚠️</span>
          <div class="signal-card__content">
            <strong>Esempi di Gruppo 3</strong>
            <p>Verdure e legumi in conserva (con acqua e sale), frutta candita e sciroppata, noci tostate e salate, acciughe e sardine in olio, prosciutto crudo di qualità, formaggi tradizionali (parmigiano, pecorino), pane artigianale, vino, birra artigianale, olio extravergine con acidificante.</p>
          </div>
        </div>
        <div class="signal-card signal-card--ok">
          <span class="signal-card__emoji">✅</span>
          <div class="signal-card__content">
            <strong>Sono accettabili nella dieta quotidiana</strong>
            <p>Il Gruppo 3 non va evitato. Molti alimenti tradizionali e culturalmente importanti appartengono a questo gruppo. Possono però essere ricchi di sale (conserve, salumi) o zuccheri (frutta sciroppata): valuta in base alla quantità.</p>
          </div>
        </div>
      </div>
    </section>

    <hr class="guide-divider" />

    <!-- SEZIONE 5 -->
    <section id="gruppo-4" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">5</div>
        <h2 class="guide-section-title">Gruppo 4 — Ultra-processati: il gruppo da limitare</h2>
      </div>

      <p>
        Gli ultra-processati sono formulazioni industriali che non contengono alimenti interi o che li usano in minima parte. Sono costruiti con ingredienti estratti, purificati o modificati chimicamente, più additivi tecnologici in grandi quantità.
      </p>

      <p>
        Il segnale chiave: contengono ingredienti che <strong>non si trovano in una cucina domestica</strong>.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🚩</span>
          <div class="guide-checklist__body">
            <strong>Ingredienti tipici del Gruppo 4</strong>
            <p>Proteine idrolizzate, amidi modificati, sciroppo di glucosio-fruttosio, oli idrogenati, aromi "natura-identici", coloranti artificiali, dolcificanti intensivi (aspartame, saccarina, sucralosio), emulsionanti (E471, E472), stabilizzanti, agenti lievitanti chimici multipli.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">📦</span>
          <div class="guide-checklist__body">
            <strong>Esempi comuni</strong>
            <p>Merendine industriali, cereali da colazione con zucchero, bevande gassate e energy drink, pane da cassetta con additivi, wurstel e salumi ricostruiti, nuggets e bastoncini di pesce, yogurt aromatizzati con addensanti, piatti pronti surgelati, pizza industriale, salse in busta, brodi in cubetto con esaltatori di sapidità.</p>
          </div>
        </li>
      </ul>

      <div class="guide-insight">
        <strong>Dati chiave:</strong> in Italia circa il 14–18% delle calorie giornaliere proviene da ultra-processati. In UK e USA la percentuale supera il 50–60%. La ricerca mostra che già un consumo superiore al 20% delle calorie giornaliere da NOVA 4 è associato a rischi per la salute.
      </div>
    </section>

