    <section id="rischio-sistemico" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">4</div>
        <h2 class="guide-section-title">Un risque systémique : plusieurs mécanismes combinés</h2>
      </div>

      <p>
        Ce qui rend les UPF particulièrement préoccupants n'est pas un seul effet négatif, mais la convergence de plusieurs mécanismes qui se renforcent mutuellement.
      </p>

      <div class="guide-table-wrap">
        <table>
          <thead>
            <tr>
              <th>Mécanisme</th>
              <th>Effet</th>
              <th>Conséquence à long terme</th>
            </tr>
          </thead>
          <tbody>
            <tr>
              <td>Hyper-appétibilité</td>
              <td>Consommation excessive de calories</td>
              <td>Obésité, diabète de type 2</td>
            </tr>
            <tr>
              <td>Carence en fibres</td>
              <td>Dysbiose intestinale</td>
              <td>Inflammation chronique, MICI</td>
            </tr>
            <tr>
              <td>Additifs (émulsifiants)</td>
              <td>Perméabilité intestinale</td>
              <td>Maladies auto-immunes, cardiovasculaires</td>
            </tr>
            <tr>
              <td>Sucres et édulcorants</td>
              <td>Résistance à l'insuline, dysbiose</td>
              <td>Diabète de type 2, syndrome métabolique</td>
            </tr>
            <tr>
              <td>Remplacement des aliments frais</td>
              <td>Carence en micronutriments</td>
              <td>Stress oxydatif, immunodépression</td>
            </tr>
          </tbody>
        </table>
      </div>

      <p>
        En résumé, les UPF agissent comme un <strong>facteur de risque systémique</strong> qui favorise simultanément la résistance à l'insuline, le stress oxydatif et l'inflammation chronique de bas grade — trois conditions qui sont à la base de presque toutes les maladies chroniques non transmissibles les plus répandues en Occident.
      </p>

      <div class="guide-insight">
        <strong>Les grandes études épidémiologiques le confirment :</strong> NutriNet-Santé (France, plus de 100 000 participants) et UK Biobank (Royaume-Uni, plus de 500 000 participants) associent une consommation élevée d'UPF à des risques accrus de cancer, maladies cardiovasculaires, dépression et mortalité toutes causes confondues — indépendamment de la qualité nutritionnelle des produits individuels.
      </div>
    </section>

