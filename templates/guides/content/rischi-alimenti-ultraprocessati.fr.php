    <section id="obesita-metabolismo" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">1</div>
        <h2 class="guide-section-title">Obésité et altération du métabolisme</h2>
      </div>

      <p>
        Les aliments ultra-transformés ne font pas grossir simplement parce qu'ils sont caloriques. Le mécanisme est plus insidieux : ils sont conçus pour être <strong>hyper-appétissants</strong> et interfèrent activement avec les signaux naturels de satiété du corps.
      </p>

      <div class="guide-insight">
        <strong>L'étude de Kevin Hall (NIH, 2019) :</strong> dans un essai clinique contrôlé, les participants exposés à un régime à base d'ultra-transformés consommaient en moyenne <strong>500 calories de plus par jour</strong> par rapport à ceux qui mangeaient des aliments peu transformés — à égalité d'accessibilité à la nourriture et de liberté de choix. En seulement deux semaines, le groupe UPF avait pris près d'un kilo.
      </div>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🔥</span>
          <div class="guide-checklist__body">
            <strong>Haute densité énergétique</strong>
            <p>Les UPF contiennent en moyenne <strong>2,15 kcal par gramme</strong> — presque le double par rapport aux aliments frais. Le même volume de nourriture fournit beaucoup plus de calories, sans la satiété correspondante.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🧠</span>
          <div class="guide-checklist__body">
            <strong>Stimulation excessive des centres du plaisir</strong>
            <p>La combinaison industrielle de sucre, sel, graisses et arômes active le système dopaminergique de façon similaire à celle des substances addictives. Le résultat : on mange même en l'absence de faim réelle.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">📉</span>
          <div class="guide-checklist__body">
            <strong>Réduction du peptide YY</strong>
            <p>Les UPF abaissent les niveaux du <strong>peptide YY</strong>, l'hormone intestinale qui signale au cerveau la satiété. Ceux qui mangent beaucoup d'ultra-transformés reçoivent moins de signaux d'"arrêt" après les repas.</p>
          </div>
        </li>
      </ul>

      <div class="signal-cards">
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">📊</span>
          <div class="signal-card__content">
            <strong>+41 % de risque d'obésité abdominale</strong>
            <p>Ceux qui suivent un régime à haute teneur en UPF présentent un risque supérieur de 41 % de développer une obésité ou une obésité abdominale par rapport à ceux qui en consomment peu.</p>
          </div>
        </div>
      </div>
    </section>

