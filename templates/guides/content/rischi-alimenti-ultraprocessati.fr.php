    <section id="infiammazione-immunita" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">2</div>
        <h2 class="guide-section-title">Inflammations et dysfonctionnements du système immunitaire</h2>
      </div>

      <p>
        La consommation fréquente d'ultra-transformés est associée à une augmentation des réponses immunitaires inflammatoires dans l'organisme — un effet qui va bien au-delà du simple profil nutritionnel du produit.
      </p>

      <p>
        Certaines substances communes dans les UPF — émulsifiants, épaississants et microparticules comme le <strong>dioxyde de titane (E171)</strong> — peuvent altérer le microbiote intestinal et augmenter la perméabilité de la muqueuse intestinale. Ce phénomène, connu sous le nom de <em>leaky gut</em> ou "intestin perméable", permet à des bactéries pathogènes et à des substances nocives de pénétrer dans le sang, déclenchant une inflammation systémique chronique.
      </p>

      <div class="signal-cards">
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🚩</span>
          <div class="signal-card__content">
            <strong>Additifs augmentant la perméabilité intestinale</strong>
            <p>Des émulsifiants comme la <strong>carboxyméthylcellulose (E466)</strong> et le <strong>polysorbate 80 (E433)</strong> altèrent le mucus protecteur intestinal même à des doses autorisées par la réglementation, selon des études sur des modèles animaux et des cultures cellulaires.</p>
          </div>
        </div>
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🚩</span>
          <div class="signal-card__content">
            <strong>Biomarqueurs inflammatoires élevés</strong>
            <p>Ceux qui consomment beaucoup d'UPF présentent des niveaux plus élevés de <strong>protéine C-réactive</strong> et d'autres biomarqueurs protéiques inflammatoires circulants, associés à un risque accru de mortalité cardiovasculaire.</p>
          </div>
        </div>
      </div>

      <p>Les dysfonctionnements immunitaires chroniques induits par les UPF sont associés à un risque accru de développer des maladies auto-immunes :</p>

      <div class="rating-grid">
        <div class="rating-block rating-block--red">
          <div class="rating-block__dot">⚠️</div>
          <div class="rating-block__body">
            <strong>Maladies auto-immunes associées à une consommation élevée d'UPF</strong>
            <ul>
              <li>Maladie cœliaque</li>
              <li>Thyroïdite de Hashimoto</li>
              <li>Sclérose en plaques</li>
              <li>Lupus érythémateux systémique</li>
              <li>Diabète de type 1</li>
            </ul>
          </div>
        </div>
      </div>

      <div class="guide-insight">
        Le lien entre UPF et maladies auto-immunes n'est pas encore complètement compris, et la corrélation n'implique pas une causalité directe. Cependant, la convergence de plusieurs études indépendantes sur des populations différentes rend l'association digne d'attention clinique.
      </div>
    </section>

