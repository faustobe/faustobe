    <section id="cosa-fare" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">5</div>
        <h2 class="guide-section-title">Que faire en pratique</h2>
      </div>

      <p>
        Les preuves scientifiques sont solides, mais le message pratique n'est pas "éliminez tout". C'est <strong>réduisez la part d'UPF dans l'alimentation quotidienne</strong> et remplacez-les par des alternatives simples, quand elles sont disponibles et accessibles.
      </p>

      <ol class="guide-step-list">
        <li>
          <span class="guide-step-num">1</span>
          <div class="guide-step-body">
            <strong>Identifiez vos UPF habituels</strong>
            <p>Céréales de petit-déjeuner sucrées, snacks emballés, pain de mie avec additifs, plats préparés, boissons aromatisées : ce sont ces produits qui, consommés quotidiennement, font la différence. Pas la glace une fois par mois.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">2</span>
          <div class="guide-step-body">
            <strong>Remplacez-les un à un</strong>
            <p>Pas besoin d'une révolution. Flocons d'avoine à la place des céréales sucrées. Yaourt grec nature à la place de celui aromatisé avec des épaississants. Pain artisanal à la place du pain de mie avec conservateurs. Chaque substitution réduit la charge cumulée.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">3</span>
          <div class="guide-step-body">
            <strong>Augmentez les fibres — c'est la priorité</strong>
            <p>Légumineuses, légumes, céréales complètes en grains, fruits entiers : les fibres nourrissent les bactéries bénéfiques du microbiote et contrecarrent directement de nombreux effets négatifs des UPF. Elles ne nécessitent pas d'"éliminer" quoi que ce soit, seulement d'ajouter.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">4</span>
          <div class="guide-step-body">
            <strong>Utilisez l'app pour identifier les UPF cachés</strong>
            <p>De nombreux produits qui semblent sains (barres "protéinées", yaourts "0 %", jus "aux fruits") sont en réalité NOVA 4. Scanner le code-barres avec E-Codes Reader vous montre immédiatement la classification NOVA et les additifs présents.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">5</span>
          <div class="guide-step-body">
            <strong>Ne visez pas la perfection</strong>
            <p>L'objectif n'est pas un régime "pur". C'est que la majeure partie de ce que vous mangez chaque semaine soit de la vraie nourriture — et que les ultra-transformés restent l'exception, pas la base. Même une amélioration de 20-30 % de la qualité de l'alimentation produit des effets mesurables sur la santé à moyen terme.</p>
          </div>
        </li>
      </ol>
    </section>

