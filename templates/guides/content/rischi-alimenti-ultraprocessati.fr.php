    <!-- SEZIONE 3 -->
    <section id="microbiota-intestino" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">3</div>
        <h2 class="guide-section-title">Santé intestinale et microbiote</h2>
      </div>

      <p>
        Le processus d'ultra-transformation industrielle prive les aliments de <strong>fibres alimentaires</strong> et de <strong>composés végétaux bioactifs</strong> (polyphénols, antioxydants) nécessaires pour nourrir les "bonnes" bactéries de l'intestin. À leur place abondent sucres ajoutés, sel, édulcorants artificiels et additifs qui favorisent un déséquilibre du microbiote — ce qu'on appelle la <strong>dysbiose</strong>.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🦠</span>
          <div class="guide-checklist__body">
            <strong>Dysbiose : moins de diversité bactérienne, plus de risques</strong>
            <p>Un microbiote appauvri — avec moins d'espèces bactériennes bénéfiques — est associé à une résistance à l'insuline, une inflammation chronique et une plus grande vulnérabilité aux infections et aux maladies métaboliques. Les UPF réduisent activement cette diversité.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🧱</span>
          <div class="guide-checklist__body">
            <strong>Affaiblissement de la couche muqueuse protectrice</strong>
            <p>L'abondance d'émulsifiants et d'édulcorants artificiels affaiblit la couche de mucus qui tapisse l'intestin, réduisant la barrière physique entre les bactéries intestinales et la paroi du côlon.</p>
          </div>
        </li>
      </ul>

      <p>Des études observationnelles montrent que ceux qui consomment de grandes quantités d'UPF souffrent plus fréquemment de :</p>

      <div class="signal-cards">
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🔴</span>
          <div class="signal-card__content">
            <strong>Maladies inflammatoires chroniques de l'intestin</strong>
            <p>Maladie de Crohn et rectocolite hémorragique — deux affections en forte augmentation dans les pays à plus haute consommation d'ultra-transformés.</p>
          </div>
        </div>
        <div class="signal-card signal-card--warning">
          <span class="signal-card__emoji">⚠️</span>
          <div class="signal-card__content">
            <strong>Troubles fonctionnels et précancéreux</strong>
            <p>Syndrome de l'intestin irritable, ulcères gastriques et polypes précancéreux du côlon sont plus fréquents parmi les consommateurs habituels d'UPF.</p>
          </div>
        </div>
      </div>
    </section>

