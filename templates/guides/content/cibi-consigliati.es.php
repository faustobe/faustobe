    <section id="spuntini" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">5</div>
        <h2 class="guide-section-title">Snacks inteligentes: qué tener siempre a mano</h2>
      </div>

      <p>
        Los snacks industriales (bollería, aperitivos envasados, barritas proteicas ultraprocesadas) son uno de los principales vehículos de ultraprocesados en la dieta diaria. Esto es lo que puedes tener en su lugar.
      </p>

      <ol class="guide-step-list">
        <li>
          <span class="guide-step-num">🥜</span>
          <div class="guide-step-body">
            <strong>Frutos secos al natural</strong>
            <p>Nueces, almendras, avellanas, anacardos sin sal añadida. Un puñado (30 g) aporta proteínas, grasas buenas y minerales. Evita las versiones "tostadas con miel" con jarabes y aditivos.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">🍎</span>
          <div class="guide-step-body">
            <strong>Fruta fresca entera</strong>
            <p>La fruta entera tiene fibra que ralentiza la absorción de los azúcares. Una manzana, una pera, un kiwi — no el zumo de fruta, que ha perdido casi toda la fibra y a menudo se clasifica como ultraprocesado.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">🥛</span>
          <div class="guide-step-body">
            <strong>Yogur griego natural</strong>
            <p>Alto contenido en proteínas, bajo índice glucémico, sin aditivos. Añade tú mismo la miel o la fruta fresca si quieres dulzor — así controlas la cantidad de azúcar.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">🧀</span>
          <div class="guide-step-body">
            <strong>Queso curado + crackers integrales</strong>
            <p>Un poco de manchego o parmesano sobre crackers integrales con lista de ingredientes corta (harina integral, aceite, sal, levadura). Saciedad duradera, ingredientes sencillos.</p>
          </div>
        </li>
      </ol>
    </section>

