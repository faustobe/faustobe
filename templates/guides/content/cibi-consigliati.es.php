    <section id="grassi-buoni" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">4</div>
        <h2 class="guide-section-title">Grasas buenas: las más fáciles de incorporar</h2>
      </div>

      <p>
        Las grasas no engordan por sí solas. Las de calidad — aceite virgen extra, nueces, aguacate, pescado graso — son algunos de los componentes más protectores para el sistema cardiovascular.
      </p>

      <div class="signal-cards">
        <div class="signal-card signal-card--ok">
          <span class="signal-card__emoji">🫒</span>
          <div class="signal-card__content">
            <strong>Fuentes de grasas insaturadas recomendadas</strong>
            <p>Aceite de oliva virgen extra (en crudo o para cocciones breves), aceite de lino en crudo, nueces, almendras, avellanas, semillas de lino, semillas de chía, aguacate, pescado azul (salmón, caballa, sardinas, atún), huevos enteros.</p>
          </div>
        </div>
        <div class="signal-card signal-card--warning">
          <span class="signal-card__emoji">⚠️</span>
          <div class="signal-card__content">
            <strong>Con moderación</strong>
            <p>Mantequilla (saturada pero natural), aceite de coco (rico en saturados), leche entera. No son ultraprocesados, pero un consumo excesivo de grasas saturadas sigue siendo perjudicial para la salud cardiovascular.</p>
          </div>
        </div>
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🚩</span>
          <div class="signal-card__content">
            <strong>Grasas que evitar</strong>
            <p>Margarinas con grasas parcialmente hidrogenadas, shortening vegetal, aceite de palma refinado en productos de bollería industrial, nata vegetal con emulsionantes, aceites de semillas rectificados a altas temperaturas.</p>
          </div>
        </div>
      </div>
    </section>

