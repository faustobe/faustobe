    <!-- SECCIÓN 3 -->
    <section id="proteine" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">3</div>
        <h2 class="guide-section-title">Proteínas: las mejores fuentes</h2>
      </div>

      <p>
        Las proteínas de buena calidad se encuentran tanto en el mundo animal como en el vegetal. La clave no es la fuente, sino el grado de procesamiento: una pechuga de pollo a la plancha y un nugget de pollo son dos cosas completamente distintas.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🐟</span>
          <div class="guide-checklist__body">
            <strong>Pescado y mariscos</strong>
            <p>Pescado fresco o congelado (sin rebozar), sardinas y caballa en aceite de oliva, atún al natural, anchoas en sal (enjuagadas). El pescado azul es una de las fuentes proteicas más económicas y nutritivas disponibles.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🥚</span>
          <div class="guide-checklist__body">
            <strong>Huevos y lácteos</strong>
            <p>Huevos enteros (no solo claras), yogur griego natural, requesón, queso fresco, quesos curados con moderación. Evita sustitutos industriales del huevo, yogures "proteicos" con espesantes y edulcorantes, quesos fundidos con fosfatos.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🌿</span>
          <div class="guide-checklist__body">
            <strong>Legumbres: la proteína vegetal más subestimada</strong>
            <p>Lentejas, garbanzos, alubias, guisantes, soja en edamame. En conserva al natural ya están cocidas y se añaden en 30 segundos a cualquier comida. Son baratas, ricas en fibra y tienen un perfil de aminoácidos excelente si se combinan con cereales.</p>
          </div>
        </li>
      </ul>
    </section>

    <hr class="guide-divider" />

