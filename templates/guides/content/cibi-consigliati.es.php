    <section id="principio-base" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">1</div>
        <h2 class="guide-section-title">El principio básico: sustituir, no eliminar</h2>
      </div>

      <p>
        No se trata de seguir dietas estrictas ni de sentirse culpable cada vez que se compra algo envasado. Se trata de conocer las alternativas — y elegirlas cuando están disponibles y son tan prácticas como el producto que sustituyen.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🔄</span>
          <div class="guide-checklist__body">
            <strong>Mismo tiempo, menos procesamiento</strong>
            <p>Yogur natural en lugar de yogur aromatizado con espesantes. Arroz integral en lugar de arroz precocido en bolsa. Legumbres en conserva al natural en lugar de plato preparado con legumbres. La comodidad es casi la misma, la calidad es diferente.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🎯</span>
          <div class="guide-checklist__body">
            <strong>El 80/20 funciona</strong>
            <p>Si el 80% de lo que comes es Grupo 1–2 (ver clasificación NOVA), el 20% de productos más procesados no marca la diferencia. La salud alimentaria se mide en la media semanal, no en la comida concreta.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">💡</span>
          <div class="guide-checklist__body">
            <strong>Los alimentos "recomendados" no son todos bio ni caros</strong>
            <p>Garbanzos en bote, huevos, harina integral, yogur natural, avena, lentejas, verduras de temporada, sardinas en aceite — son algunos de los alimentos más saludables y cuestan menos que casi cualquier producto envasado equivalente.</p>
          </div>
        </li>
      </ul>
    </section>

    <hr class="guide-divider" />

    <!-- SECCIÓN 2 -->
    <section id="cereali" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">2</div>
        <h2 class="guide-section-title">Cereales e hidratos de carbono: las mejores opciones</h2>
      </div>

      <p>
        Los hidratos de carbono no son el problema — el problema es el grado de refinado y procesamiento. Un cereal integral en grano es radicalmente diferente a un cereal refinado inflado con jarabe de azúcar.
      </p>

      <div class="signal-cards">
        <div class="signal-card signal-card--ok">
          <span class="signal-card__emoji">✅</span>
          <div class="signal-card__content">
            <strong>Cereales recomendados</strong>
            <p>
              <strong>Avena en copos</strong> (sin ingredientes añadidos) ·
              <strong>Arroz integral</strong> o semiintegral ·
              <strong>Espelta en grano</strong> ·
              <strong>Cebada perlada</strong> ·
              <strong>Trigo sarraceno</strong> ·
              <strong>Pasta integral</strong> con solo sémola integral ·
              <strong>Pan de harina integral pura</strong>, agua, sal, masa madre (máximo 4–5 ingredientes)
            </p>
          </div>
        </div>
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🚩</span>
          <div class="signal-card__content">
            <strong>Que conviene limitar</strong>
            <p>Cereales de desayuno con azúcar (también los "integrales" con jarabe), pan de molde con almidones modificados y conservantes, crackers con aceite de palma y aromas, arroz y pasta precocinados en bolsa con aditivos, bollería y galletas industriales.</p>
          </div>
        </div>
      </div>

      <div class="guide-insight">
        <strong>Consejo rápido para los cereales de desayuno:</strong> lee la tabla nutricional. Si los azúcares superan los 10 g/100 g, estás comiendo un producto de postre — no un desayuno saludable.
      </div>
    </section>

