      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">5</div>
        <h2 class="guide-section-title">Come riconoscerli in pratica: la doppia lettura</h2>
      </div>

      <p>
        Per una valutazione completa, applica entrambi i criteri ogni volta che leggi un'etichetta.
      </p>

      <ol class="guide-step-list">
        <li>
          <span class="guide-step-num">1</span>
          <div class="guide-step-body">
            <strong>Guarda se ci sono ingredienti "da laboratorio"</strong>
            <p>Amidi modificati, proteine isolate, idrolizzati, sciroppi di glucosio-fruttosio, aromi "natura-identici", oli parzialmente idrogenati: sono il segnale principale di ultra-lavorazione anche senza tanti codici E.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">2</span>
          <div class="guide-step-body">
            <strong>Conta i codici E e identifica le categorie critiche</strong>
            <p>Un E di antiossidante naturale (E306) è molto diverso da un colorante azoico (E102) o da un dolcificante intensivo (E951). Usa l'app per sapere il rischio di ciascun additivo.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">3</span>
          <div class="guide-step-body">
            <strong>Considera il contesto del pasto</strong>
            <p>Un pasto equilibrato con verdure, legumi e cereali integrali "assorbe" meglio qualche prodotto più lavorato. Il problema è quando gli ultra-processati diventano la base della dieta quotidiana.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">4</span>
          <div class="guide-step-body">
            <strong>Usa l'app come supporto, non come giudice assoluto</strong>
            <p>E-Codes Reader ti mostra la classificazione NOVA e i dettagli sugli additivi. Usala per capire meglio i prodotti, non per creare ansia. L'obiettivo è ridurre gli ultra-processati nel tempo, non eliminarli tutti in un giorno.</p>
          </div>
        </li>
      </ol>

      <div class="guide-insight">
        <strong>Riassunto in una frase:</strong> un prodotto con un E naturale non è un problema. Un prodotto con 15 ingredienti industriali senza nessun E è comunque un ultra-processato. Guarda entrambe le cose.
      </div>
    </section>

