    <section id="quando-coincidono" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">3</div>
        <h2 class="guide-section-title">Quando coincidono — e quando no</h2>
      </div>

      <p>
        Questo è il punto che sorprende di più: la sovrapposizione tra "con additivi" e "ultra-processato" non è totale.
      </p>

      <div class="rating-grid">
        <div class="rating-block rating-block--green">
          <div class="rating-block__dot">✅</div>
          <div class="rating-block__body">
            <strong>Additivi ma NON ultra-processato</strong>
            <ul>
              <li>Formaggio con conservante E252 (nitrato di potassio): lavorato ma non NOVA 4</li>
              <li>Vino con solfiti (E220): additivo naturale, prodotto tradizionale</li>
              <li>Olio con vitamina E (E306) come antiossidante: minima lavorazione</li>
            </ul>
          </div>
        </div>
        <div class="rating-block rating-block--red">
          <div class="rating-block__dot">🚩</div>
          <div class="rating-block__body">
            <strong>Ultra-processato con POCHI additivi E</strong>
            <ul>
              <li>Pane da toast con amido modificato e aromi: non ha tanti E ma è NOVA 4</li>
              <li>Barrette proteiche con proteine isolate e sciroppo di riso: pochi codici, ma fortemente trasformate</li>
              <li>Surimi: quasi nessun E nella lista, ma prodotto da proteine di pesce ricostruite</li>
            </ul>
          </div>
        </div>
        <div class="rating-block rating-block--yellow">
          <div class="rating-block__dot">⚠️</div>
          <div class="rating-block__body">
            <strong>Ultra-processato E con tanti additivi (il caso peggiore)</strong>
            <ul>
              <li>Merendine: amidi modificati + coloranti + emulsionanti + aromi + dolcificanti</li>
              <li>Salse industriali: conservanti + stabilizzanti + esaltatori di sapidità + coloranti</li>
              <li>Bevande energy: dolcificanti + coloranti + acidificanti + aromi complessi</li>
            </ul>
          </div>
        </div>
      </div>
    </section>

