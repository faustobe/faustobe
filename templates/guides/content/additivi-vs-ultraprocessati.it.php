    <section id="cosa-sono-additivi" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">1</div>
        <h2 class="guide-section-title">Cosa sono gli additivi alimentari</h2>
      </div>

      <p>
        Gli additivi alimentari sono sostanze aggiunte intenzionalmente agli alimenti per svolgere una funzione tecnologica: conservare, colorare, addensare, emulsionare, stabilizzare o migliorare il sapore. In Europa, ogni additivo approvato riceve un codice <strong>E</strong> (E100, E200, E300...) e deve superare una valutazione di sicurezza dell'EFSA.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🔵</span>
          <div class="guide-checklist__body">
            <strong>Non tutti gli additivi sono sintetici</strong>
            <p>L'acido citrico (E330), la curcumina (E100), la lecitina di soia (E322) e l'acido ascorbico (E300, vitamina C) sono additivi di origine naturale, presenti anche in moltissimi alimenti non trasformati.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🔵</span>
          <div class="guide-checklist__body">
            <strong>Non tutti gli additivi sono pericolosi</strong>
            <p>La stragrande maggioranza degli E approvati è considerata sicura alle dosi d'uso consentite. Il problema non è la presenza di un E, ma la <em>quantità</em> e la <em>combinazione</em> di molti additivi diversi nello stesso prodotto.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">⚠️</span>
          <div class="guide-checklist__body">
            <strong>Alcuni additivi meritano attenzione</strong>
            <p>Coloranti azoici (E102, E110, E122, E124, E129), conservanti come nitrati (E249–E252), dolcificanti intensivi (E950–E962): queste categorie hanno evidenze di rischio più consistenti, soprattutto per i bambini.</p>
          </div>
        </li>
      </ul>
    </section>

