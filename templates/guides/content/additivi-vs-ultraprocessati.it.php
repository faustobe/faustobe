    <section id="cosa-sono-ultraprocessati" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">2</div>
        <h2 class="guide-section-title">Cosa sono i cibi ultra-processati</h2>
      </div>

      <p>
        Il termine "ultra-processato" viene dalla <strong>classificazione NOVA</strong>, sviluppata dall'Università di San Paolo. Non riguarda i nutrienti, ma il <em>processo industriale</em> che ha prodotto l'alimento.
      </p>

      <p>
        Un cibo è ultra-processato (NOVA 4) quando contiene ingredienti che non si trovano in una cucina domestica: proteine isolate, amidi modificati, idrolizzati proteici, sciroppi di glucosio-fruttosio, aromi "natura-identici", emulsionanti, coloranti, stabilizzanti — spesso in combinazioni complesse.
      </p>

      <div class="signal-cards">
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🚩</span>
          <div class="signal-card__content">
            <strong>Esempi di ultra-processati comuni</strong>
            <p>Merendine, snack confezionati, cereali da colazione dolci, bevande gassate e succhi "a base di", wurstel e salumi ricostruiti, piatti pronti surgelati con salse, yogurt aromatizzati con addensanti, pane da cassetta industriale con molti additivi.</p>
          </div>
        </div>
        <div class="signal-card signal-card--ok">
          <span class="signal-card__emoji">✅</span>
          <div class="signal-card__content">
            <strong>Cosa NON sono ultra-processati</strong>
            <p>Formaggi stagionati (hanno pochi additivi), salumi interi di qualità (anche se lavorati), pane artigianale, pasta secca tradizionale, conserve di pomodoro senza additivi, aceto, olio extravergine.</p>
          </div>
        </div>
      </div>
    </section>

