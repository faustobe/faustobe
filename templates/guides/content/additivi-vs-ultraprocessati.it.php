    <section id="doppio-problema" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">4</div>
        <h2 class="guide-section-title">Il doppio problema: perché entrambi contano</h2>
      </div>

      <p>
        Guardare solo gli additivi o solo il grado di lavorazione porta a valutazioni parziali. I rischi per la salute vengono da entrambe le direzioni.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🧬</span>
          <div class="guide-checklist__body">
            <strong>Rischi legati agli additivi specifici</strong>
            <p>Certi additivi hanno effetti documentati: i coloranti azoici e l'iperattività nei bambini (EFSA 2007), i nitrati nella carne lavorata e i nitrosammine cancerogeni, i dolcificanti intensivi e la possibile alterazione del microbioma intestinale.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🏭</span>
          <div class="guide-checklist__body">
            <strong>Rischi legati all'ultra-lavorazione in sé</strong>
            <p>Studi su larga scala (NutriNet-Santé, UK Biobank) associano il consumo elevato di NOVA 4 a un aumento del rischio di obesità, diabete tipo 2, malattie cardiovascolari e alcuni tumori — indipendentemente dalla qualità nutrizionale.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🔁</span>
          <div class="guide-checklist__body">
            <strong>L'effetto combinazione</strong>
            <p>Gli studi sugli additivi testano quasi sempre una sostanza alla volta. Ma nella vita reale mangiamo combinazioni di 10–20 additivi diversi nello stesso pasto. Gli effetti sinergici sono ancora poco studiati.</p>
          </div>
        </li>
      </ul>
    </section>

