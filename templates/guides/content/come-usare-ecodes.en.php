      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">4</div>
        <h2 class="guide-section-title">How to use the app practically while shopping</h2>
      </div>

      <p>
        Fitting the app into your shopping routine is simpler than it sounds. Follow these four steps the first time — they will quickly become second nature.
      </p>

      <ol class="guide-step-list">
        <li>
          <span class="guide-step-num">1</span>
          <div class="guide-step-body">
            <strong>Start with the chilled and breakfast aisles</strong>
            <p>Scan yogurts, bread, milk, drinks. Look for products with few ingredients, few E numbers and simple sugars (fruit, milk, cane sugar) rather than syrups and sweeteners.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">2</span>
          <div class="guide-step-body">
            <strong>Then move on to sweets and snacks</strong>
            <p>Check cakes, salty snacks, biscuits and packaged desserts. If the list is very long and full of E codes, the app will flag red. In those cases, always choose the simpler alternative (fruit, wholegrain bread, nuts).</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">3</span>
          <div class="guide-step-body">
            <strong>Use the app to compare similar products</strong>
            <p>Find two products of the same type (e.g. Greek yogurt, bread, snacks) and compare their app profiles. Often the one with fewer ingredients is only slightly less eye-catching on the shelf but far more natural.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">4</span>
          <div class="guide-step-body">
            <strong>Keep "reds" as exceptions, not the rule</strong>
            <p>There's no need to eliminate everything ultra-processed — just reduce it. Set yourself a limit — for example, no more than 1–2 "red" products per week — while keeping the rest of your shop focused on simple foods.</p>
          </div>
        </li>
      </ol>
    </section>

