    <section id="regole-semplici" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">2</div>
        <h2 class="guide-section-title">Three rules to quickly spot an "over-processed" food</h2>
      </div>

      <p>
        You can apply these three rules every time you check a product in the app — no special knowledge required.
      </p>

      <div class="signal-cards">
        <div class="signal-card signal-card--ok">
          <span class="signal-card__emoji">✅</span>
          <div class="signal-card__content">
            <strong>Fewer ingredients = better</strong>
            <p>If the list is short and contains familiar names (e.g. "flour, water, salt, oil"), the product is minimally processed and generally more trustworthy.</p>
          </div>
        </div>
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🚩</span>
          <div class="signal-card__content">
            <strong>Many "unpronounceable" ingredients</strong>
            <p>Terms like "modified starches", "isolated proteins", "mono- and diglycerides of fatty acids", "flavour enhancers" or "complex flavourings" are a clear sign of a heavily processed food.</p>
          </div>
        </div>
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🚩</span>
          <div class="signal-card__content">
            <strong>Lots of E codes and additives</strong>
            <p>Not all E numbers are harmful, but their sheer number — colourants, preservatives, stabilisers, emulsifiers — signals a high degree of industrial processing.</p>
          </div>
        </div>
      </div>
    </section>

