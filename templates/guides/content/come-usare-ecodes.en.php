    <section id="tre-controlli" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">1</div>
        <h2 class="guide-section-title">Before scanning, do three quick checks</h2>
      </div>

      <p>
        Even before opening the app, look at the packaging. Front-of-pack labels are often designed to make products appear healthier than they really are.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">👁️</span>
          <div class="guide-checklist__body">
            <strong>Look at the front of the pack</strong>
            <p>Watch out for claims like "sugar-free but with sweeteners", "fat-free but with emulsifiers", "extra flavour" or "flavoured". These often signal that the product has been heavily modified.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">📱</span>
          <div class="guide-checklist__body">
            <strong>Open the app and scan the product</strong>
            <p>Use the camera to scan the barcode or upload a photo of the label. The app will immediately show you the ingredient list and any additives present.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🔍</span>
          <div class="guide-checklist__body">
            <strong>Check three key points</strong>
            <p>How many <strong>additives</strong> appear (especially E codes)? How <strong>long and complex</strong> is the ingredient list? How much <strong>sugar, salt and fat</strong> is declared?</p>
          </div>
        </li>
      </ul>

      <div class="guide-insight">
        If you answer "many" or "long/complicated" to more than one question, you are probably looking at a highly processed food.
      </div>
    </section>

    <hr class="guide-divider" />

