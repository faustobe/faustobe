    <section id="additivi-controversi" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">3</div>
        <h2 class="guide-section-title">Additivi controversi: i codici E da conoscere</h2>
      </div>

      <p>
        La maggior parte dei codici E in etichetta è sicura. Ma alcune categorie hanno un profilo di rischio più rilevante, soprattutto per bambini e consumatori abituali. Non si tratta di eliminarli del tutto, ma di essere consapevoli.
      </p>

      <div class="rating-grid">
        <div class="rating-block rating-block--red">
          <div class="rating-block__dot">🔴</div>
          <div class="rating-block__body">
            <strong>Coloranti azoici — massima attenzione con i bambini</strong>
            <ul>
              <li>E102 (Tartrazina), E104 (Giallo chinolina), E110 (Giallo tramonto)</li>
              <li>E122 (Carmoisina), E124 (Rosso Ponceau 4R), E129 (Rosso allura)</li>
              <li>Studio EFSA 2007: correlazione con iperattività nei bambini. In Europa obbligo di avviso sull'etichetta: "può influire sull'attività e l'attenzione dei bambini"</li>
            </ul>
          </div>
        </div>
        <div class="rating-block rating-block--yellow">
          <div class="rating-block__dot">🟡</div>
          <div class="rating-block__body">
            <strong>Nitrati e nitriti — carne lavorata</strong>
            <ul>
              <li>E249–E252 (nitriti e nitrati di sodio e potassio)</li>
              <li>Usati come conservanti in salumi, wurstel, bacon, hot dog</li>
              <li>In presenza di proteine animali formano nitrosammine, classificate come probabili cancerogeni (IARC gruppo 2A). Limitare il consumo di carne lavorata è il consiglio principale</li>
            </ul>
          </div>
        </div>
        <div class="rating-block rating-block--yellow">
          <div class="rating-block__dot">🟡</div>
          <div class="rating-block__body">
            <strong>Dolcificanti intensivi — consumo abituale</strong>
            <ul>
              <li>E950 (Acesulfame K), E951 (Aspartame), E952 (Ciclamato), E954 (Saccarina), E955 (Sucralosio)</li>
              <li>Sicuri alle dosi approvate per adulti. Studi recenti suggeriscono possibili effetti sul microbioma intestinale con consumo cronico elevato</li>
              <li>L'OMS (2023) ha espresso cautela sull'uso cronico per il controllo del peso: non raccomandato come alternativa allo zucchero nel lungo termine</li>
            </ul>
          </div>
        </div>
      </div>
    </section>

