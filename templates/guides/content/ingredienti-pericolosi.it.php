    <section id="sodio-nascosto" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">4</div>
        <h2 class="guide-section-title">Sodio nascosto: oltre il sale da cucina</h2>
      </div>

      <p>
        Il sodio in eccesso alza la pressione arteriosa e aumenta il rischio cardiovascolare. Il problema è che circa il 75% del sodio nella dieta occidentale non viene aggiunto durante la cottura — viene già nel prodotto confezionato.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🧂</span>
          <div class="guide-checklist__body">
            <strong>I veicoli principali di sodio nascosto</strong>
            <p>Pane (la prima fonte di sodio nella dieta italiana), formaggi, salumi, piatti pronti, sughi in barattolo, dado e brodi in cubetto, salse tipo soia, ketchup, maionese, snack salati, cereali da colazione industriali.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🔢</span>
          <div class="guide-checklist__body">
            <strong>Come convertire sodio in sale sull'etichetta</strong>
            <p>Se l'etichetta riporta "sodio" invece di "sale": moltiplica il valore per 2,5. Esempio: 0,6 g di sodio = 1,5 g di sale. Il limite OMS è 5 g di sale al giorno (circa 2 g di sodio).</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🏷️</span>
          <div class="guide-checklist__body">
            <strong>Additivi che contengono sodio (non solo il sale)</strong>
            <p>Glutammato monosodico (E621), benzoato di sodio (E211), nitrito di sodio (E250), bicarbonato di sodio, fosfato bisodico — tutti contribuiscono al sodio totale, anche se non compaiono come "sale" in etichetta.</p>
          </div>
        </li>
      </ul>
    </section>

