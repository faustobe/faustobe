    <section id="regola-del-5" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">5</div>
        <h2 class="guide-section-title">La regola del 5: il filtro più veloce</h2>
      </div>

      <p>
        Non devi diventare un esperto di biochimica per fare scelte migliori. La "regola del 5" è un filtro pratico usabile in pochi secondi davanti allo scaffale.
      </p>

      <ol class="guide-step-list">
        <li>
          <span class="guide-step-num">1</span>
          <div class="guide-step-body">
            <strong>Più di 5 ingredienti? Inizia a leggere con attenzione</strong>
            <p>Non è una regola assoluta (una buona granola artigianale può averne 7–8), ma è un primo filtro: la maggior parte dei prodotti semplici e poco lavorati sta sotto i 5 ingredienti.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">2</span>
          <div class="guide-step-body">
            <strong>Zuccheri sopra 5 g/100 g? Controlla se è il primo o secondo ingrediente</strong>
            <p>5 g/100 g è la soglia UE per "basso contenuto di zuccheri". Sopra quella soglia è solo informazione — non divieto — ma ti segnala di guardare da dove vengono quegli zuccheri.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">3</span>
          <div class="guide-step-body">
            <strong>Sale sopra 0,5 g/100 g in un prodotto dolce? Segnale di ultra-lavorazione</strong>
            <p>Biscotti, cereali e snack dolci non dovrebbero avere molto sale. Quando lo hanno, spesso serve a nascondere il sapore artificiale dei sostituti dello zucchero o a compensare la mancanza di ingredienti veri.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">4</span>
          <div class="guide-step-body">
            <strong>Più di 5 additivi E? Il prodotto è probabilmente ultra-processato</strong>
            <p>Non conta il tipo di E, conta la quantità. Un prodotto con 7–8 additivi diversi (coloranti + emulsionanti + dolcificanti + conservanti + aromi) non ha bisogno di tutta quella chimica se la qualità degli ingredienti base è alta.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">5</span>
          <div class="guide-step-body">
            <strong>Non riconosci 5 o più ingredienti? Rimettilo sullo scaffale</strong>
            <p>Se non sai cosa sono "maltodestrine", "caseinato di calcio", "amido di tapioca modificato", "E471" e "aromi natura-identici" — e compaiono tutti nello stesso prodotto — hai trovato un ultra-processato da limitare.</p>
          </div>
        </li>
      </ol>

      <div class="guide-insight">
        <strong>Ricorda:</strong> la regola del 5 è un orientamento, non una sentenza. Un'ottima pasta integrale con ingredienti certificati può averne più di 5; una merendina economica può starne appena sotto. Usala come punto di partenza, non come unico criterio.
      </div>
    </section>

