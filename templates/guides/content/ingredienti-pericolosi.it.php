    <section id="zuccheri-nascosti" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">1</div>
        <h2 class="guide-section-title">Zuccheri: i 10 nomi nascosti sulle etichette</h2>
      </div>

      <p>
        Lo zucchero non si chiama sempre "zucchero". Le aziende usano decine di varianti per distribuirlo in più voci della lista ingredienti — e farlo apparire meno rilevante. Se conosci questi nomi, smetti di essere ingannato.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🔍</span>
          <div class="guide-checklist__body">
            <strong>I 10 alias più comuni</strong>
            <p>
              <strong>Sciroppo di glucosio-fruttosio</strong> (il peggiore: metabolizzato diversamente dallo zucchero da tavola) ·
              <strong>Maltodestrine</strong> (amido quasi interamente scomposto) ·
              <strong>Destrosio</strong> ·
              <strong>Fruttosio</strong> ·
              <strong>Succo di canna concentrato</strong> ·
              <strong>Sciroppo di mais</strong> ·
              <strong>Sciroppo di riso</strong> ·
              <strong>Miele disidratato</strong> ·
              <strong>Zucchero di cocco</strong> ·
              <strong>Nettare di agave</strong>
            </p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">⚠️</span>
          <div class="guide-checklist__body">
            <strong>La trappola del "senza zuccheri aggiunti"</strong>
            <p>Un succo "100% frutta senza zuccheri aggiunti" può contenere 10–12 g di zuccheri per 100 ml — più di una Coca-Cola (10,6 g/100 ml). Il fruttosio del succo concentrato è chimicamente identico allo zucchero aggiunto.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">📊</span>
          <div class="guide-checklist__body">
            <strong>La soglia da tenere a mente</strong>
            <p>L'OMS raccomanda di mantenere gli zuccheri liberi (aggiunti + da succhi) sotto il <strong>10% delle calorie giornaliere</strong> — circa 50 g al giorno per un adulto da 2000 kcal. Preferibilmente sotto il 5% (25 g).</p>
          </div>
        </li>
      </ul>
    </section>

