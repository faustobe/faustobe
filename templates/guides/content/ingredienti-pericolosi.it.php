    <section id="grassi-da-evitare" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">2</div>
        <h2 class="guide-section-title">Grassi da evitare: idrogenati e trans</h2>
      </div>

      <p>
        Non tutti i grassi sono uguali. Gli acidi grassi trans (AGT), prodotti dall'idrogenazione industriale degli oli vegetali, sono associati a un aumentato rischio cardiovascolare e sono stati vietati o fortemente limitati in molti paesi.
      </p>

      <div class="signal-cards">
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🚩</span>
          <div class="signal-card__content">
            <strong>Grassi idrogenati e parzialmente idrogenati</strong>
            <p>Cerca in etichetta: "olio vegetale parzialmente idrogenato", "grasso vegetale idrogenato", "shortening". In Europa sono vietati sopra 2 g/100 g di grasso dal 2021, ma alcune esenzioni rimangono. Evitali comunque.</p>
          </div>
        </div>
        <div class="signal-card signal-card--warning">
          <span class="signal-card__emoji">⚠️</span>
          <div class="signal-card__content">
            <strong>Olio di palma: non è trans, ma...</strong>
            <p>L'olio di palma è saturo ma non idrogenato. Il problema è diverso: durante la raffinazione ad alte temperature si formano composti (3-MCPD, glicidolo) potenzialmente cancerogeni. Non è vietato, ma meglio limitarlo.</p>
          </div>
        </div>
        <div class="signal-card signal-card--ok">
          <span class="signal-card__emoji">✅</span>
          <div class="signal-card__content">
            <strong>Grassi che vanno bene</strong>
            <p>Olio d'oliva extravergine, olio di lino, noci e semi oleosi, avocado, grassi del pesce azzurro. Questi contengono acidi grassi monoinsaturi e omega-3 che hanno effetti protettivi documentati.</p>
          </div>
        </div>
      </div>
    </section>

