      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">5</div>
        <h2 class="guide-section-title">Was man in der Praxis tun kann</h2>
      </div>

      <p>
        Die wissenschaftlichen Belege sind solide, aber die praktische Botschaft lautet nicht „eliminiere alles". Sie lautet: <strong>Reduziere den Anteil von UPF in der täglichen Ernährung</strong> und ersetze sie durch einfache Alternativen, wann immer diese verfügbar und zugänglich sind.
      </p>

      <ol class="guide-step-list">
        <li>
          <span class="guide-step-num">1</span>
          <div class="guide-step-body">
            <strong>Identifiziere deine gewohnheitsmäßigen UPF</strong>
            <p>Süße Frühstückszerealien, abgepackte Snacks, Toastbrot mit Zusatzstoffen, Fertiggerichte, aromatisierte Getränke: Das sind die Produkte, die – täglich konsumiert – den Unterschied machen. Nicht das Eis einmal im Monat.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">2</span>
          <div class="guide-step-body">
            <strong>Ersetze eines nach dem anderen</strong>
            <p>Eine Revolution ist nicht nötig. Haferflocken statt süßer Cerealien. Naturjoghurt statt aromatisiertem Joghurt mit Verdickungsmitteln. Handwerksbrot statt Toastbrot mit Konservierungsstoffen. Jede Substitution reduziert die kumulative Belastung.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">3</span>
          <div class="guide-step-body">
            <strong>Erhöhe die Ballaststoffzufuhr – das hat Priorität</strong>
            <p>Hülsenfrüchte, Gemüse, Vollkorngetreide, ganzes Obst: Ballaststoffe ernähren die nützlichen Bakterien des Mikrobioms und wirken direkt vielen negativen Effekten von UPF entgegen. Es muss nichts „eliminiert" werden – es genügt, etwas hinzuzufügen.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">4</span>
          <div class="guide-step-body">
            <strong>Nutze die App, um versteckte UPF zu erkennen</strong>
            <p>Viele Produkte, die gesund wirken (Proteinriegel, „0 %"-Joghurt, Fruchtsäfte), sind in Wirklichkeit NOVA 4. Wenn du den Barcode mit E-Codes Reader scannst, werden dir sofort die NOVA-Klassifikation und die enthaltenen Zusatzstoffe angezeigt.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">5</span>
          <div class="guide-step-body">
            <strong>Strebe keine Perfektion an</strong>
            <p>Das Ziel ist keine „reine" Ernährung. Es geht darum, dass das meiste, was du jede Woche isst, echtes Essen ist – und dass ultraverarbeitete Lebensmittel die Ausnahme bleiben, nicht die Grundlage. Selbst eine Verbesserung der Ernährungsqualität um 20–30 % erzeugt mittelfristig messbare gesundheitliche Auswirkungen.</p>
          </div>
        </li>
