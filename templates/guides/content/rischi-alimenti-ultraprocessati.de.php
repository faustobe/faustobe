    <section id="obesita-metabolismo" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">1</div>
        <h2 class="guide-section-title">Fettleibigkeit und Stoffwechselstörungen</h2>
      </div>

      <p>
        Ultraverarbeitete Lebensmittel machen nicht einfach deshalb dick, weil sie kalorienreich sind. Der Mechanismus ist heimtückischer: Sie sind so konzipiert, dass sie <strong>hyperattraktiv</strong> schmecken, und greifen aktiv in die natürlichen Sättigungssignale des Körpers ein.
      </p>

      <div class="guide-insight">
        <strong>Die Studie von Kevin Hall (NIH, 2019):</strong> In einem kontrollierten klinischen Versuch nahmen Teilnehmer, die einer auf ultraverarbeiteten Lebensmitteln basierenden Ernährung ausgesetzt waren, im Durchschnitt <strong>500 Kalorien mehr pro Tag</strong> zu sich als Personen, die minimal verarbeitete Lebensmittel aßen – bei gleicher Verfügbarkeit von Nahrung und freier Wahl. In nur zwei Wochen hatte die UPF-Gruppe fast ein Kilogramm zugenommen.
      </div>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🔥</span>
          <div class="guide-checklist__body">
            <strong>Hohe Energiedichte</strong>
            <p>UPF enthalten im Durchschnitt <strong>2,15 kcal pro Gramm</strong> – fast doppelt so viel wie frische Lebensmittel. Die gleiche Menge an Nahrung liefert deutlich mehr Kalorien, ohne ein entsprechendes Sättigungsgefühl zu erzeugen.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🧠</span>
          <div class="guide-checklist__body">
            <strong>Übermäßige Stimulation der Lustzentren</strong>
            <p>Die industrielle Kombination aus Zucker, Salz, Fetten und Aromastoffen aktiviert das Dopaminsystem auf ähnliche Weise wie Suchtmittel. Das Ergebnis: Man isst, auch wenn kein echter Hunger besteht.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">📉</span>
          <div class="guide-checklist__body">
            <strong>Reduzierung des Peptid YY</strong>
            <p>UPF senken den Spiegel des <strong>Peptids YY</strong>, des Darmhormons, das dem Gehirn Sattheit signalisiert. Wer viele ultraverarbeitete Lebensmittel isst, empfängt nach den Mahlzeiten weniger „Stop"-Signale.</p>
          </div>
        </li>
      </ul>

      <div class="signal-cards">
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">📊</span>
          <div class="signal-card__content">
            <strong>+41 % Risiko für abdominale Fettleibigkeit</strong>
            <p>Wer sich überwiegend von UPF ernährt, hat ein um 41 % höheres Risiko, Fettleibigkeit oder abdominale Fettleibigkeit zu entwickeln, als Personen mit geringem UPF-Konsum.</p>
          </div>
        </div>
      </div>
    </section>

