    <section id="microbiota-intestino" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">3</div>
        <h2 class="guide-section-title">Darmgesundheit und Mikrobiom</h2>
      </div>

      <p>
        Der industrielle Ultraverarbeitungsprozess entzieht Lebensmitteln die <strong>Ballaststoffe</strong> und <strong>bioaktiven pflanzlichen Verbindungen</strong> (Polyphenole, Antioxidantien), die zur Ernährung der „guten" Darmbakterien notwendig sind. Stattdessen überwiegen zugesetzter Zucker, Salz, künstliche Süßungsmittel und Zusatzstoffe, die ein Ungleichgewicht im Mikrobiom begünstigen – die sogenannte <strong>Dysbiose</strong>.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🦠</span>
          <div class="guide-checklist__body">
            <strong>Dysbiose: weniger bakterielle Vielfalt, mehr Risiken</strong>
            <p>Ein verarmtes Mikrobiom – mit weniger nützlichen Bakterienarten – ist mit Insulinresistenz, chronischer Entzündung und erhöhter Anfälligkeit für Infektionen und Stoffwechselerkrankungen verbunden. UPF reduzieren diese Vielfalt aktiv.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🧱</span>
          <div class="guide-checklist__body">
            <strong>Schwächung der schützenden Schleimhautschicht</strong>
            <p>Die Fülle an Emulgatoren und künstlichen Süßungsmitteln schwächt die Schleimschicht, die den Darm auskleidet, und verringert die physikalische Barriere zwischen den Darmbakterien und der Dickdarmwand.</p>
          </div>
        </li>
      </ul>

      <p>Beobachtungsstudien zeigen, dass Personen mit hohem UPF-Konsum häufiger leiden an:</p>

      <div class="signal-cards">
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🔴</span>
          <div class="signal-card__content">
            <strong>Chronisch-entzündliche Darmerkrankungen</strong>
            <p>Morbus Crohn und Colitis ulcerosa – zwei Erkrankungen, die in Ländern mit dem höchsten Konsum an ultraverarbeiteten Lebensmitteln stark zunehmen.</p>
          </div>
        </div>
        <div class="signal-card signal-card--warning">
          <span class="signal-card__emoji">⚠️</span>
          <div class="signal-card__content">
            <strong>Funktionelle und präkanzeröse Störungen</strong>
            <p>Reizdarmsyndrom, Magengeschwüre und präkanzeröse Dickdarmpolypen treten bei regelmäßigen UPF-Konsumenten häufiger auf.</p>
          </div>
        </div>
      </div>
    </section>

