    <section id="infiammazione-immunita" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">2</div>
        <h2 class="guide-section-title">Entzündungen und Dysfunktionen des Immunsystems</h2>
      </div>

      <p>
        Der häufige Konsum von ultraverarbeiteten Lebensmitteln ist mit einer Zunahme entzündlicher Immunreaktionen im Organismus verbunden – ein Effekt, der weit über das bloße Nährwertprofil des Produkts hinausgeht.
      </p>

      <p>
        Einige in UPF häufig vorkommende Substanzen – Emulgatoren, Verdickungsmittel und Mikropartikel wie <strong>Titandioxid (E171)</strong> – können das Darmmikrobiom verändern und die Durchlässigkeit der Darmschleimhaut erhöhen. Dieses Phänomen, bekannt als <em>leaky gut</em> oder „durchlässiger Darm", ermöglicht es krankheitserregenden Bakterien und Schadstoffen, ins Blut zu gelangen, und löst chronische systemische Entzündungen aus.
      </p>

      <div class="signal-cards">
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🚩</span>
          <div class="signal-card__content">
            <strong>Zusatzstoffe, die die Darmdurchlässigkeit erhöhen</strong>
            <p>Emulgatoren wie <strong>Carboxymethylcellulose (E466)</strong> und <strong>Polysorbat 80 (E433)</strong> verändern laut Studien an Tiermodellen und Zellkulturen den schützenden Darmschleim selbst bei nach Vorschrift zugelassenen Dosierungen.</p>
          </div>
        </div>
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🚩</span>
          <div class="signal-card__content">
            <strong>Erhöhte Entzündungsbiomarker</strong>
            <p>Personen mit hohem UPF-Konsum weisen höhere Spiegel von <strong>C-reaktivem Protein</strong> und anderen zirkulierenden entzündlichen Proteinbiomarkern auf, die mit einem erhöhten kardiovaskulären Mortalitätsrisiko verbunden sind.</p>
          </div>
        </div>
      </div>

      <p>Chronische immunologische Dysfunktionen, die durch UPF ausgelöst werden, sind mit einem erhöhten Risiko für die Entwicklung von Autoimmunerkrankungen verbunden:</p>

      <div class="rating-grid">
        <div class="rating-block rating-block--red">
          <div class="rating-block__dot">⚠️</div>
          <div class="rating-block__body">
            <strong>Mit hohem UPF-Konsum assoziierte Autoimmunerkrankungen</strong>
            <ul>
              <li>Zöliakie</li>
              <li>Hashimoto-Thyreoiditis</li>
              <li>Multiple Sklerose</li>
              <li>Systemischer Lupus erythematodes</li>
              <li>Typ-1-Diabetes</li>
            </ul>
          </div>
        </div>
      </div>

      <div class="guide-insight">
        Der Zusammenhang zwischen UPF und Autoimmunerkrankungen ist noch nicht vollständig verstanden, und die Korrelation impliziert keine direkte Kausalität. Die Übereinstimmung mehrerer unabhängiger Studien an verschiedenen Bevölkerungsgruppen macht die Assoziation jedoch klinisch bedeutsam.
      </div>
    </section>

