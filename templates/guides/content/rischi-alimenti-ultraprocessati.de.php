      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">4</div>
        <h2 class="guide-section-title">Ein systemisches Risiko: mehrere Mechanismen gleichzeitig</h2>
      </div>

      <p>
        Was UPF besonders besorgniserregend macht, ist nicht ein einziger negativer Effekt, sondern das Zusammenwirken mehrerer Mechanismen, die sich gegenseitig verstärken.
      </p>

      <div class="guide-table-wrap">
        <table>
          <thead>
            <tr>
              <th>Mechanismus</th>
              <th>Wirkung</th>
              <th>Langzeitfolge</th>
            </tr>
          </thead>
          <tbody>
            <tr>
              <td>Hyperattraktivität</td>
              <td>Übermäßige Kalorienaufnahme</td>
              <td>Fettleibigkeit, Typ-2-Diabetes</td>
            </tr>
            <tr>
              <td>Ballaststoffmangel</td>
              <td>Intestinale Dysbiose</td>
              <td>Chronische Entzündung, IBD</td>
            </tr>
            <tr>
              <td>Zusatzstoffe (Emulgatoren)</td>
              <td>Darmdurchlässigkeit</td>
              <td>Autoimmun- und Herz-Kreislauf-Erkrankungen</td>
            </tr>
            <tr>
              <td>Zucker und Süßungsmittel</td>
              <td>Insulinresistenz, Dysbiose</td>
              <td>Typ-2-Diabetes, metabolisches Syndrom</td>
            </tr>
            <tr>
              <td>Verdrängung frischer Lebensmittel</td>
              <td>Mikronährstoffmangel</td>
              <td>Oxidativer Stress, Immunsuppression</td>
            </tr>
          </tbody>
        </table>
      </div>

      <p>
        Zusammenfassend wirken UPF als <strong>systemischer Risikofaktor</strong>, der gleichzeitig Insulinresistenz, oxidativen Stress und chronisch-schwache Entzündungen fördert – drei Zustände, die nahezu allen weit verbreiteten nicht-übertragbaren chronischen Krankheiten in westlichen Ländern zugrunde liegen.
      </p>

      <div class="guide-insight">
        <strong>Große epidemiologische Studien bestätigen dies:</strong> NutriNet-Santé (Frankreich, über 100.000 Teilnehmer) und UK Biobank (UK, über 500.000 Teilnehmer) verbinden hohen UPF-Konsum mit erhöhten Risiken für Krebs, Herz-Kreislauf-Erkrankungen, Depressionen und Gesamtmortalität – unabhängig vom Nährwertprofil der einzelnen Produkte.
      </div>
    </section>

