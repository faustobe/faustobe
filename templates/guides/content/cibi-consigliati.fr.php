    <!-- SECTION 4 -->
    <section id="grassi-buoni" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">4</div>
        <h2 class="guide-section-title">Bonnes graisses : les plus simples à ajouter</h2>
      </div>

      <p>
        Les graisses ne font pas grossir en elles-mêmes. Celles de qualité — huile extra vierge, noix, avocat, poisson gras — sont parmi les composants les plus protecteurs pour le système cardiovasculaire.
      </p>

      <div class="signal-cards">
        <div class="signal-card signal-card--ok">
          <span class="signal-card__emoji">🫒</span>
          <div class="signal-card__content">
            <strong>Sources de graisses insaturées recommandées</strong>
            <p>Huile d'olive extra vierge (crue ou pour des cuissons courtes), huile de lin crue, noix, amandes, noisettes, graines de lin, graines de chia, avocat, poissons gras (saumon, maquereau, sardines, thon), œufs entiers.</p>
          </div>
        </div>
        <div class="signal-card signal-card--warning">
          <span class="signal-card__emoji">⚠️</span>
          <div class="signal-card__content">
            <strong>Avec modération</strong>
            <p>Beurre (saturé mais naturel), huile de coco (riche en saturés), lait entier. Ils ne sont pas ultra-transformés, mais une consommation excessive de graisses saturées reste défavorable pour la santé cardiovasculaire.</p>
          </div>
        </div>
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🚩</span>
          <div class="signal-card__content">
            <strong>Graisses à éviter</strong>
            <p>Margarines avec graisses partiellement hydrogénées, shortening végétal, huile de palme raffinée dans les produits de boulangerie industriels, crème végétale avec émulsifiants, huiles de graines rectifiées à hautes températures.</p>
          </div>
        </div>
      </div>
    </section>

