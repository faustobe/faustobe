    <!-- SECTION 5 -->
    <section id="spuntini" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">5</div>
        <h2 class="guide-section-title">Encas intelligents : ce qu'il faut toujours avoir à portée de main</h2>
      </div>

      <p>
        Les encas industriels (gâteaux, snacks emballés, barres protéinées ultra-transformées) sont parmi les principaux vecteurs d'ultra-transformés dans l'alimentation quotidienne. Voici ce qu'on peut mettre à la place.
      </p>

      <ol class="guide-step-list">
        <li>
          <span class="guide-step-num">🥜</span>
          <div class="guide-step-body">
            <strong>Fruits secs au naturel</strong>
            <p>Noix, amandes, noisettes, noix de cajou sans sel ajouté. Une poignée (30 g) apporte protéines, bonnes graisses et minéraux. Évitez les versions « grillées au miel » avec sirops et additifs.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">🍎</span>
          <div class="guide-step-body">
            <strong>Fruits frais entiers</strong>
            <p>Le fruit entier contient des fibres qui ralentissent l'absorption des sucres. Une pomme, une poire, un kiwi — pas le jus de fruits, qui a perdu presque toutes ses fibres et est souvent classé comme ultra-transformé.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">🥛</span>
          <div class="guide-step-body">
            <strong>Yaourt grec nature</strong>
            <p>Haute teneur en protéines, faible index glycémique, aucun additif. Ajoutez vous-même le miel ou les fruits frais pour la douceur — vous contrôlez ainsi la quantité de sucre.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">🧀</span>
          <div class="guide-step-body">
            <strong>Fromage à pâte dure + crackers complets</strong>
            <p>Un peu de parmesan ou de pecorino sur des crackers complets avec une liste d'ingrédients courte (farine complète, huile, sel, levure). Satiété durable, ingrédients simples.</p>
          </div>
        </li>
      </ol>
    </section>

