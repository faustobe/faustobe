    <!-- SECTION 3 -->
    <section id="proteine" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">3</div>
        <h2 class="guide-section-title">Protéines : les meilleures sources</h2>
      </div>

      <p>
        Les protéines de bonne qualité se trouvent aussi bien dans le monde animal que végétal. Le point n'est pas la source, mais le degré de transformation : un blanc de poulet grillé et un nugget de poulet sont deux choses complètement différentes.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🐟</span>
          <div class="guide-checklist__body">
            <strong>Poissons et fruits de mer</strong>
            <p>Poisson frais ou surgelé (sans panure), sardines et maquereaux à l'huile d'olive, thon au naturel, anchois au sel (rincés). Le poisson gras est parmi les sources protéiques les plus économiques et les plus nutritives disponibles.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🥚</span>
          <div class="guide-checklist__body">
            <strong>Œufs et produits laitiers</strong>
            <p>Œufs entiers (pas seulement les blancs), yaourt grec nature, fromage blanc, faisselle, fromages affinés avec modération. Évitez les substituts industriels des œufs, les yaourts « protéinés » avec épaississants et édulcorants, les fromages fondus avec phosphates.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🌿</span>
          <div class="guide-checklist__body">
            <strong>Légumineuses : la protéine végétale la plus sous-estimée</strong>
            <p>Lentilles, pois chiches, haricots, petits pois, edamame. En boîte au naturel, elles sont déjà cuites et s'ajoutent en 30 secondes à n'importe quel repas. Elles coûtent peu, sont riches en fibres et ont un excellent profil en acides aminés quand elles sont associées aux céréales.</p>
          </div>
        </li>
      </ul>
    </section>

