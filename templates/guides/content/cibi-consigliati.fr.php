    <section id="principio-base" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">1</div>
        <h2 class="guide-section-title">Le principe de base : substituer, pas éliminer</h2>
      </div>

      <p>
        Il ne s'agit pas de suivre des régimes stricts ni de se sentir coupable chaque fois qu'on achète quelque chose d'emballé. Il s'agit de connaître les alternatives — et de les choisir quand elles sont aussi disponibles et pratiques que le produit qu'elles remplacent.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🔄</span>
          <div class="guide-checklist__body">
            <strong>Même temps, moins de transformation</strong>
            <p>Yaourt nature à la place de yaourt aromatisé avec épaississants. Riz complet à la place du riz précuit en sachet. Légumineuses en boîte au naturel à la place d'un plat préparé avec légumineuses. La praticité est presque identique, la qualité est différente.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🎯</span>
          <div class="guide-checklist__body">
            <strong>La règle 80/20 fonctionne</strong>
            <p>Si 80 % de ce que vous mangez est Groupe 1–2 (voir classification NOVA), les 20 % de produits plus transformés ne font pas de différence. La santé alimentaire se mesure sur la moyenne hebdomadaire, pas sur un repas isolé.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">💡</span>
          <div class="guide-checklist__body">
            <strong>Les aliments « recommandés » ne sont pas tous bio ou chers</strong>
            <p>Pois chiches en boîte, œufs, farine complète, yaourt nature, flocons d'avoine, lentilles, légumes de saison, sardines à l'huile — ce sont parmi les aliments les plus sains et ils coûtent moins que presque tout produit emballé équivalent.</p>
          </div>
        </li>
      </ul>
    </section>

    <hr class="guide-divider" />

    <!-- SECTION 2 -->
    <section id="cereali" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">2</div>
        <h2 class="guide-section-title">Céréales et glucides : les meilleurs choix</h2>
      </div>

      <p>
        Les glucides ne sont pas le problème — le problème est le degré de raffinage et de transformation. Une céréale complète en grain est radicalement différente d'une céréale raffinée soufflée avec du sirop de sucre.
      </p>

      <div class="signal-cards">
        <div class="signal-card signal-card--ok">
          <span class="signal-card__emoji">✅</span>
          <div class="signal-card__content">
            <strong>Céréales recommandées</strong>
            <p>
              <strong>Flocons d'avoine</strong> (sans ingrédients ajoutés) ·
              <strong>Riz complet</strong> ou semi-complet ·
              <strong>Épeautre en grain</strong> ·
              <strong>Orge perlé</strong> ·
              <strong>Sarrasin</strong> ·
              <strong>Pâtes complètes</strong> à base de semoule complète uniquement ·
              <strong>Pain de farine complète uniquement</strong>, eau, sel, levain (maximum 4–5 ingrédients)
            </p>
          </div>
        </div>
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🚩</span>
          <div class="signal-card__content">
            <strong>À limiter</strong>
            <p>Céréales du matin sucrées (même celles « complètes » avec sirop), pain de mie avec amidons modifiés et conservateurs, crackers avec huile de palme et arômes, riz et pâtes précuits en sachet avec additifs, gâteaux et biscuits industriels.</p>
          </div>
        </div>
      </div>

      <div class="guide-insight">
        <strong>Astuce rapide pour les céréales du matin :</strong> lisez le tableau nutritionnel. Si les sucres dépassent 10 g/100 g, vous mangez un produit de dessert — pas un petit-déjeuner sain.
      </div>
    </section>

