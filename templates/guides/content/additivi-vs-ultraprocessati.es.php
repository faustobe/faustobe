    <section id="cosa-sono-ultraprocessati" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">2</div>
        <h2 class="guide-section-title">Qué son los alimentos ultraprocesados</h2>
      </div>

      <p>
        El término "ultraprocesado" proviene de la <strong>clasificación NOVA</strong>, desarrollada por la Universidad de São Paulo. No se refiere a los nutrientes, sino al <em>proceso industrial</em> que ha dado lugar al alimento.
      </p>

      <p>
        Un alimento es ultraprocesado (NOVA 4) cuando contiene ingredientes que no se encuentran en una cocina doméstica: proteínas aisladas, almidones modificados, hidrolizados de proteínas, jarabes de glucosa-fructosa, aromas "idénticos a los naturales", emulsionantes, colorantes, estabilizantes — a menudo en combinaciones complejas.
      </p>

      <div class="signal-cards">
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🚩</span>
          <div class="signal-card__content">
            <strong>Ejemplos comunes de ultraprocesados</strong>
            <p>Bollería industrial, snacks envasados, cereales de desayuno azucarados, bebidas gaseosas y zumos "a base de", salchichas y embutidos reconstituidos, platos preparados congelados con salsas, yogures aromatizados con espesantes, pan de molde industrial con muchos aditivos.</p>
          </div>
        </div>
        <div class="signal-card signal-card--ok">
          <span class="signal-card__emoji">✅</span>
          <div class="signal-card__content">
            <strong>Qué NO son ultraprocesados</strong>
            <p>Quesos curados (tienen pocos aditivos), embutidos enteros de calidad (aunque procesados), pan artesanal, pasta seca tradicional, conservas de tomate sin aditivos, vinagre, aceite de oliva virgen extra.</p>
          </div>
        </div>
      </div>
    </section>

