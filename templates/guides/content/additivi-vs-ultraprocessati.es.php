    <section id="doppio-problema" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">4</div>
        <h2 class="guide-section-title">El doble problema: por qué importan los dos aspectos</h2>
      </div>

      <p>
        Fijarse solo en los aditivos o solo en el grado de procesamiento lleva a valoraciones parciales. Los riesgos para la salud vienen de ambas direcciones.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🧬</span>
          <div class="guide-checklist__body">
            <strong>Riesgos ligados a aditivos específicos</strong>
            <p>Ciertos aditivos tienen efectos documentados: los colorantes azoicos y la hiperactividad en niños (EFSA 2007), los nitratos en la carne procesada y las nitrosaminas cancerígenas, los edulcorantes intensivos y la posible alteración del microbioma intestinal.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🏭</span>
          <div class="guide-checklist__body">
            <strong>Riesgos ligados al ultraprocesamiento en sí</strong>
            <p>Estudios a gran escala (NutriNet-Santé, UK Biobank) asocian el consumo elevado de NOVA 4 con un mayor riesgo de obesidad, diabetes tipo 2, enfermedades cardiovasculares y algunos tumores — independientemente de la calidad nutricional.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🔁</span>
          <div class="guide-checklist__body">
            <strong>El efecto combinación</strong>
            <p>Los estudios sobre aditivos casi siempre evalúan una sustancia a la vez. Pero en la vida real comemos combinaciones de 10–20 aditivos distintos en la misma comida. Los efectos sinérgicos todavía están poco estudiados.</p>
          </div>
        </li>
      </ul>
    </section>

