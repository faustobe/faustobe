      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">3</div>
        <h2 class="guide-section-title">Cuándo coinciden — y cuándo no</h2>
      </div>

      <p>
        Este es el punto que más sorprende: la superposición entre "con aditivos" y "ultraprocesado" no es total.
      </p>

      <div class="rating-grid">
        <div class="rating-block rating-block--green">
          <div class="rating-block__dot">✅</div>
          <div class="rating-block__body">
            <strong>Con aditivos pero NO ultraprocesado</strong>
            <ul>
              <li>Queso con conservante E252 (nitrato de potasio): procesado pero no NOVA 4</li>
              <li>Vino con sulfitos (E220): aditivo natural, producto tradicional</li>
              <li>Aceite con vitamina E (E306) como antioxidante: mínima transformación</li>
            </ul>
          </div>
        </div>
        <div class="rating-block rating-block--red">
          <div class="rating-block__dot">🚩</div>
          <div class="rating-block__body">
            <strong>Ultraprocesado con POCOS aditivos E</strong>
            <ul>
              <li>Pan de molde con almidón modificado y aromas: no tiene muchos E pero es NOVA 4</li>
              <li>Barritas proteicas con proteínas aisladas y jarabe de arroz: pocos códigos, pero muy transformadas</li>
              <li>Surimi: casi ningún E en la lista, pero elaborado a partir de proteínas de pescado reconstituidas</li>
            </ul>
          </div>
        </div>
        <div class="rating-block rating-block--yellow">
          <div class="rating-block__dot">⚠️</div>
          <div class="rating-block__body">
            <strong>Ultraprocesado Y con muchos aditivos (el peor caso)</strong>
            <ul>
              <li>Bollería industrial: almidones modificados + colorantes + emulsionantes + aromas + edulcorantes</li>
              <li>Salsas industriales: conservantes + estabilizantes + potenciadores del sabor + colorantes</li>
              <li>Bebidas energéticas: edulcorantes + colorantes + acidulantes + aromas complejos</li>
            </ul>
          </div>
        </div>
      </div>
    </section>

