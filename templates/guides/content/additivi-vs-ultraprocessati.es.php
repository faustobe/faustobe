      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">5</div>
        <h2 class="guide-section-title">Cómo reconocerlos en la práctica: la doble lectura</h2>
      </div>

      <p>
        Para una evaluación completa, aplica ambos criterios cada vez que leas una etiqueta.
      </p>

      <ol class="guide-step-list">
        <li>
          <span class="guide-step-num">1</span>
          <div class="guide-step-body">
            <strong>Mira si hay ingredientes "de laboratorio"</strong>
            <p>Almidones modificados, proteínas aisladas, hidrolizados, jarabes de glucosa-fructosa, aromas "idénticos a los naturales", aceites parcialmente hidrogenados: son la señal principal de ultraprocesamiento incluso sin muchos códigos E.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">2</span>
          <div class="guide-step-body">
            <strong>Cuenta los códigos E e identifica las categorías críticas</strong>
            <p>Un E de antioxidante natural (E306) es muy diferente a un colorante azoico (E102) o a un edulcorante intensivo (E951). Usa la app para conocer el riesgo de cada aditivo.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">3</span>
          <div class="guide-step-body">
            <strong>Ten en cuenta el contexto de la comida</strong>
            <p>Una comida equilibrada con verduras, legumbres y cereales integrales "compensa" mejor algún producto más procesado. El problema es cuando los ultraprocesados se convierten en la base de la dieta diaria.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">4</span>
          <div class="guide-step-body">
            <strong>Usa la app como apoyo, no como juez absoluto</strong>
            <p>E-Codes Reader te muestra la clasificación NOVA y los detalles sobre los aditivos. Úsala para entender mejor los productos, no para generar ansiedad. El objetivo es reducir los ultraprocesados con el tiempo, no eliminarlos todos de un día para otro.</p>
          </div>
        </li>
      </ol>

      <div class="guide-insight">
        <strong>Resumen en una frase:</strong> un producto con un E natural no es un problema. Un producto con 15 ingredientes industriales sin ningún E sigue siendo un ultraprocesado. Fíjate en las dos cosas.
      </div>
    </section>

