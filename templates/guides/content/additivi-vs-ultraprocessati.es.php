    <section id="cosa-sono-additivi" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">1</div>
        <h2 class="guide-section-title">Qué son los aditivos alimentarios</h2>
      </div>

      <p>
        Los aditivos alimentarios son sustancias añadidas intencionalmente a los alimentos para cumplir una función tecnológica: conservar, colorear, espesar, emulsionar, estabilizar o mejorar el sabor. En Europa, cada aditivo aprobado recibe un código <strong>E</strong> (E100, E200, E300...) y debe superar una evaluación de seguridad de la EFSA.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🔵</span>
          <div class="guide-checklist__body">
            <strong>No todos los aditivos son sintéticos</strong>
            <p>El ácido cítrico (E330), la curcumina (E100), la lecitina de soja (E322) y el ácido ascórbico (E300, vitamina C) son aditivos de origen natural, presentes también en muchos alimentos sin transformar.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🔵</span>
          <div class="guide-checklist__body">
            <strong>No todos los aditivos son peligrosos</strong>
            <p>La gran mayoría de los E aprobados se considera segura en las dosis de uso permitidas. El problema no es la presencia de un E, sino la <em>cantidad</em> y la <em>combinación</em> de muchos aditivos diferentes en el mismo producto.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">⚠️</span>
          <div class="guide-checklist__body">
            <strong>Algunos aditivos merecen atención</strong>
            <p>Colorantes azoicos (E102, E110, E122, E124, E129), conservantes como nitratos (E249–E252), edulcorantes intensivos (E950–E962): estas categorías presentan evidencias de riesgo más sólidas, especialmente en niños.</p>
          </div>
        </li>
      </ul>
    </section>

