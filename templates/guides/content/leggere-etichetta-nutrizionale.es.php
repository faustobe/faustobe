    <section id="ingredienti" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">1</div>
        <h2 class="guide-section-title">La lista de ingredientes: por dónde empezar</h2>
      </div>

      <p>
        Por ley, los ingredientes se listan en <strong>orden decreciente de peso</strong>: lo que aparece primero está presente en mayor cantidad. Es el punto de partida más importante de la etiqueta.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🥇</span>
          <div class="guide-checklist__body">
            <strong>El primer ingrediente es el más importante</strong>
            <p>Si el primer ingrediente es "azúcar", "jarabe de glucosa" o "aceite de palma", el producto está construido en torno a ese elemento. En las galletas de chocolate, el chocolate suele aparecer en cuarto o quinto lugar.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">📏</span>
          <div class="guide-checklist__body">
            <strong>Longitud de la lista = grado de procesamiento</strong>
            <p>Pan casero: harina, agua, sal, levadura — 4 ingredientes. Pan industrial de supermercado: puede tener 15 o más. La longitud no es un problema en sí misma, pero siempre es una señal a tener en cuenta.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🔍</span>
          <div class="guide-checklist__body">
            <strong>Busca los nombres difíciles de pronunciar</strong>
            <p>Términos como "almidón modificado", "mono- y diglicéridos de ácidos grasos", "carboximetilcelulosa" o "hidrogenado" indican ingredientes de origen industrial, ausentes en las recetas caseras.</p>
          </div>
        </li>
      </ul>

      <div class="guide-insight">
        <strong>Regla práctica:</strong> si no encontrarías todos los ingredientes en un supermercado normal, probablemente el producto está muy procesado.
      </div>
    </section>

    <hr class="guide-divider" />

    <!-- SECCIÓN 2 -->
    <section id="tabella-nutrizionale" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">2</div>
        <h2 class="guide-section-title">La tabla nutricional: qué mirar (y qué ignorar)</h2>
      </div>

      <p>
        La tabla nutricional es obligatoria en casi todos los productos envasados. Contiene mucha información, pero con tres valores basta para hacerse una idea general.
      </p>

      <div class="signal-cards">
        <div class="signal-card signal-card--ok">
          <span class="signal-card__emoji">✅</span>
          <div class="signal-card__content">
            <strong>Azúcares: menos de 5 g por 100 g</strong>
            <p>Valores por debajo de 5 g/100 g indican un producto bajo en azúcares. Entre 5 y 22,5 g es medio; por encima de 22,5 g es alto. Para las bebidas, el límite "bajo" es 2,5 g/100 ml.</p>
          </div>
        </div>
        <div class="signal-card signal-card--warning">
          <span class="signal-card__emoji">⚠️</span>
          <div class="signal-card__content">
            <strong>Sal: menos de 0,3 g por 100 g</strong>
            <p>Por debajo de 0,3 g/100 g es bajo, por encima de 1,5 g/100 g es alto. Muchos productos salados como crackers, quesos y embutidos superan fácilmente 1 g/100 g. Atención: el sodio en la etiqueta hay que multiplicarlo por 2,5 para obtener la sal.</p>
          </div>
        </div>
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🚩</span>
          <div class="signal-card__content">
            <strong>Grasas saturadas: menos de 1,5 g por 100 g</strong>
            <p>Son el tipo de grasa que hay que vigilar con más atención. Valores por encima de 5 g/100 g se consideran altos. Los productos de bollería industrial, los snacks y los platos preparados suelen superarlos con creces.</p>
          </div>
        </div>
      </div>
    </section>

