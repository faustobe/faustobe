    <section id="porzioni" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">3</div>
        <h2 class="guide-section-title">Raciones vs 100 g: el truco que confunde</h2>
      </div>

      <p>
        Los valores nutricionales se indican habitualmente tanto por 100 g como "por ración". Las empresas tienden a destacar los valores por ración, que parecen más bajos — pero las raciones suelen estar subestimadas.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🎭</span>
          <div class="guide-checklist__body">
            <strong>Cereales de desayuno: ración declarada 30 g, pero comes 60</strong>
            <p>Si el envase declara 10 g de azúcares por ración (30 g) y tú comes un bol de 60 g, estás tomando 20 g de azúcares — casi la mitad del límite diario recomendado.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">📊</span>
          <div class="guide-checklist__body">
            <strong>Usa siempre los valores por 100 g para comparar</strong>
            <p>¿Quieres saber cuál es la mejor pasta entre dos marcas? Compara los valores por 100 g, no por ración (que varía de una marca a otra). Es la única comparación justa.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🧮</span>
          <div class="guide-checklist__body">
            <strong>Calcula la ración real</strong>
            <p>Mira cuántas raciones hay en el envase. Si una bolsa de patatas fritas contiene "3,5 raciones" y te la comes entera, multiplica todos los valores por 3,5.</p>
          </div>
        </li>
      </ul>
    </section>

