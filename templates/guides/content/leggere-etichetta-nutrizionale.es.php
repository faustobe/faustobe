    <section id="checklist" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">6</div>
        <h2 class="guide-section-title">Lista de verificación rápida para usar en el supermercado</h2>
      </div>

      <p>
        No tienes que hacer todo cada vez. Empieza por los controles más rápidos y añade los demás a medida que se convierta en un hábito.
      </p>

      <ol class="guide-step-list">
        <li>
          <span class="guide-step-num">1</span>
          <div class="guide-step-body">
            <strong>Mira el primer ingrediente</strong>
            <p>¿Es un alimento real (harina, leche, tomate, aceite)? Bien. ¿Es un azúcar, jarabe o grasa procesada? Valora si tienes una alternativa.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">2</span>
          <div class="guide-step-body">
            <strong>Cuenta los ingredientes</strong>
            <p>Menos de 5: excelente. De 5 a 10: aceptable. Más de 10–12: revisa bien qué contienen.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">3</span>
          <div class="guide-step-body">
            <strong>Comprueba los códigos E</strong>
            <p>¿Pocos o ninguno? Bien. ¿Muchos (sobre todo colorantes, edulcorantes, conservantes)? El producto está muy procesado.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">4</span>
          <div class="guide-step-body">
            <strong>Mira sal y azúcares por 100 g</strong>
            <p>Sal por encima de 1,5 g/100 g = alto. Azúcares por encima de 22,5 g/100 g = alto. Grasas saturadas por encima de 5 g/100 g = alto.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">5</span>
          <div class="guide-step-body">
            <strong>Ignora las declaraciones en grande del frontal</strong>
            <p>"Integral", "natural", "0% grasas" casi nunca significan lo que parecen. Confía siempre en la lista de ingredientes, no en los textos promocionales.</p>
          </div>
        </li>
      </ol>

      <div class="guide-insight">
        <strong>El truco más sencillo de todos:</strong> si reconoces todos los ingredientes y podrías cocinarlos en casa, vas por buen camino. Si no, has encontrado un ultraprocessado.
      </div>
    </section>

