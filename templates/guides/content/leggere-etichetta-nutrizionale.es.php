    <section id="sale-nascosto" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">4</div>
        <h2 class="guide-section-title">La sal oculta: dónde se esconde de verdad</h2>
      </div>

      <p>
        La sal no solo se encuentra en los productos salados. En muchos alimentos aparentemente dulces — galletas, cereales, pan — el contenido de sal es sorprendentemente alto.
      </p>

      <div class="signal-cards">
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🍞</span>
          <div class="signal-card__content">
            <strong>Pan y productos de panadería</strong>
            <p>Dos rebanadas de pan industrial pueden contener 0,5–0,8 g de sal. Multiplicado por 4 comidas al día, ya es la mitad del límite diario recomendado (5 g).</p>
          </div>
        </div>
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🥣</span>
          <div class="signal-card__content">
            <strong>Cereales de desayuno</strong>
            <p>Incluso los "saludables" e "integrales" pueden contener 0,8–1,2 g de sal por 100 g — más que muchos snacks salados. Leer la etiqueta en este caso casi siempre sorprende.</p>
          </div>
        </div>
        <div class="signal-card signal-card--warning">
          <span class="signal-card__emoji">🫙</span>
          <div class="signal-card__content">
            <strong>Salsas, sofritos y condimentos</strong>
            <p>La salsa de tomate lista, la mayonesa, los aliños para ensalada: pueden contener entre 1 y 3 g de sal por 100 g. Uno de los casos en que lo casero siempre gana a lo industrial.</p>
          </div>
        </div>
      </div>

      <div class="guide-insight">
        <strong>Nota importante:</strong> en la etiqueta puedes encontrar "sodio" en lugar de "sal". Para convertir: <strong>sal = sodio × 2,5</strong>.
      </div>
    </section>

