    <section id="claim" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">5</div>
        <h2 class="guide-section-title">Las declaraciones nutricionales: qué significan de verdad</h2>
      </div>

      <p>
        Las declaraciones nutricionales son frases como "sin grasas", "light", "rico en fibra" o "0% azúcares" que aparecen en grande en la cara frontal del envase. Están reguladas por ley, pero pueden llevar a confusión.
      </p>

      <div class="rating-grid">
        <div class="rating-block rating-block--yellow">
          <div class="rating-block__dot">🏷️</div>
          <div class="rating-block__body">
            <strong>"Sin azúcares añadidos"</strong>
            <ul>
              <li>No significa sin azúcares: puede contener azúcares naturales (fructosa del zumo, lactosa)</li>
              <li>Puede contener edulcorantes artificiales o polialcoholes</li>
              <li>No equivale a "sano" ni a "apto para diabéticos"</li>
            </ul>
          </div>
        </div>
        <div class="rating-block rating-block--yellow">
          <div class="rating-block__dot">🏷️</div>
          <div class="rating-block__body">
            <strong>"Light" o "reducido"</strong>
            <ul>
              <li>Significa reducido un 30% respecto al producto de referencia</li>
              <li>Si el producto base tenía muchas calorías, el light sigue teniendo bastantes</li>
              <li>A menudo la grasa eliminada se sustituye con azúcares o espesantes</li>
            </ul>
          </div>
        </div>
        <div class="rating-block rating-block--yellow">
          <div class="rating-block__dot">🏷️</div>
          <div class="rating-block__body">
            <strong>"Rico en fibra" o "fuente de proteínas"</strong>
            <ul>
              <li>Requiere umbrales mínimos legales (fibra: mín. 6 g/100 g; proteínas: mín. 20% de las calorías)</li>
              <li>No dice nada sobre los demás ingredientes: una galleta "rica en fibra" puede contener mucho azúcar</li>
              <li>Siempre lee la lista de ingredientes, no solo la declaración</li>
            </ul>
          </div>
        </div>
      </div>
    </section>

