    <section id="infiammazione-immunita" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">2</div>
        <h2 class="guide-section-title">Inflamación y disfunciones del sistema inmunitario</h2>
      </div>

      <p>
        El consumo frecuente de ultraprocesados está asociado a un aumento de las respuestas inmunitarias inflamatorias en el organismo — un efecto que va mucho más allá del simple perfil nutricional del producto.
      </p>

      <p>
        Algunas sustancias comunes en los UPF — emulsionantes, espesantes y micropartículas como el <strong>dióxido de titanio (E171)</strong> — pueden alterar la microbiota intestinal y aumentar la permeabilidad de la mucosa intestinal. Este fenómeno, conocido como <em>leaky gut</em> o "intestino permeable", permite que bacterias patógenas y sustancias nocivas entren en la sangre, desencadenando una inflamación sistémica crónica.
      </p>

      <div class="signal-cards">
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🚩</span>
          <div class="signal-card__content">
            <strong>Aditivos que aumentan la permeabilidad intestinal</strong>
            <p>Emulsionantes como la <strong>carboximetilcelulosa (E466)</strong> y el <strong>polisorbato 80 (E433)</strong> alteran el moco protector intestinal incluso a dosis permitidas por la normativa, según estudios en modelos animales y cultivos celulares.</p>
          </div>
        </div>
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🚩</span>
          <div class="signal-card__content">
            <strong>Biomarcadores inflamatorios elevados</strong>
            <p>Quienes consumen muchos UPF presentan niveles más altos de <strong>proteína C reactiva</strong> y otros biomarcadores proteicos inflamatorios circulantes, asociados a un mayor riesgo de mortalidad cardiovascular.</p>
          </div>
        </div>
      </div>

      <p>Las disfunciones inmunitarias crónicas inducidas por los UPF están asociadas a un mayor riesgo de desarrollar enfermedades autoinmunes:</p>

      <div class="rating-grid">
        <div class="rating-block rating-block--red">
          <div class="rating-block__dot">⚠️</div>
          <div class="rating-block__body">
            <strong>Enfermedades autoinmunes asociadas al consumo elevado de UPF</strong>
            <ul>
              <li>Celiaquía</li>
              <li>Tiroiditis de Hashimoto</li>
              <li>Esclerosis múltiple</li>
              <li>Lupus eritematoso sistémico</li>
              <li>Diabetes tipo 1</li>
            </ul>
          </div>
        </div>
      </div>

      <div class="guide-insight">
        El vínculo entre UPF y enfermedades autoinmunes aún no se comprende completamente, y la correlación no implica causalidad directa. Sin embargo, la convergencia de múltiples estudios independientes sobre poblaciones diversas hace que la asociación sea digna de atención clínica.
      </div>
    </section>

