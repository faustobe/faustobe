    <section id="microbiota-intestino" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">3</div>
        <h2 class="guide-section-title">Salud intestinal y microbiota</h2>
      </div>

      <p>
        El proceso de ultratransformación industrial priva a los alimentos de <strong>fibra dietética</strong> y <strong>compuestos vegetales bioactivos</strong> (polifenoles, antioxidantes) necesarios para nutrir las bacterias "buenas" del intestino. En su lugar, abundan azúcares añadidos, sal, edulcorantes artificiales y aditivos que favorecen un desequilibrio de la microbiota — la llamada <strong>disbiosis</strong>.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🦠</span>
          <div class="guide-checklist__body">
            <strong>Disbiosis: menos diversidad bacteriana, más riesgos</strong>
            <p>Una microbiota empobrecida — con menos especies bacterianas beneficiosas — está asociada a resistencia a la insulina, inflamación crónica y mayor vulnerabilidad a infecciones y enfermedades metabólicas. Los UPF reducen activamente esta diversidad.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🧱</span>
          <div class="guide-checklist__body">
            <strong>Debilitamiento de la capa mucosa protectora</strong>
            <p>La abundancia de emulsionantes y edulcorantes artificiales debilita la capa de moco que recubre el intestino, reduciendo la barrera física entre las bacterias intestinales y la pared del colon.</p>
          </div>
        </li>
      </ul>

      <p>Estudios observacionales muestran que quienes consumen grandes cantidades de UPF padecen con más frecuencia:</p>

      <div class="signal-cards">
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🔴</span>
          <div class="signal-card__content">
            <strong>Enfermedades inflamatorias crónicas intestinales</strong>
            <p>Enfermedad de Crohn y colitis ulcerosa — dos afecciones en fuerte aumento en los países con mayor consumo de ultraprocesados.</p>
          </div>
        </div>
        <div class="signal-card signal-card--warning">
          <span class="signal-card__emoji">⚠️</span>
          <div class="signal-card__content">
            <strong>Trastornos funcionales y precancerosos</strong>
            <p>El síndrome del intestino irritable, las úlceras gástricas y los pólipos precancerosos de colon son más frecuentes entre los consumidores habituales de UPF.</p>
          </div>
        </div>
      </div>
    </section>

