    <section id="cosa-fare" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">5</div>
        <h2 class="guide-section-title">Qué hacer en la práctica</h2>
      </div>

      <p>
        Las evidencias científicas son sólidas, pero el mensaje práctico no es "elimina todo". Es <strong>reduce la proporción de UPF en la dieta diaria</strong> y sustitúyelos por alternativas sencillas, cuando estén disponibles y sean accesibles.
      </p>

      <ol class="guide-step-list">
        <li>
          <span class="guide-step-num">1</span>
          <div class="guide-step-body">
            <strong>Identifica tus UPF habituales</strong>
            <p>Cereales de desayuno azucarados, snacks envasados, pan de molde con aditivos, platos preparados, bebidas aromatizadas: estos son los productos que, consumidos a diario, marcan la diferencia. No el helado una vez al mes.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">2</span>
          <div class="guide-step-body">
            <strong>Sustitúyelos de uno en uno</strong>
            <p>No hace falta una revolución. Avena en copos en lugar de cereales azucarados. Yogur griego natural en lugar del aromatizado con espesantes. Pan artesanal en lugar del pan de molde con conservantes. Cada sustitución reduce la carga acumulativa.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">3</span>
          <div class="guide-step-body">
            <strong>Aumenta la fibra — es la prioridad</strong>
            <p>Legumbres, verduras, cereales integrales en grano, fruta entera: la fibra nutre las bacterias beneficiosas de la microbiota y contrarresta directamente muchos de los efectos negativos de los UPF. No requiere "eliminar" nada, solo añadir.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">4</span>
          <div class="guide-step-body">
            <strong>Usa la app para identificar los UPF ocultos</strong>
            <p>Muchos productos que parecen saludables (barritas "proteicas", yogures "0%", zumos "de frutas") son en realidad NOVA 4. Escanear el código de barras con E-Codes Reader te muestra de inmediato la clasificación NOVA y los aditivos presentes.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">5</span>
          <div class="guide-step-body">
            <strong>No busques la perfección</strong>
            <p>El objetivo no es una dieta "pura". Es que la mayor parte de lo que comas cada semana sea comida real — y que los ultraprocesados sean la excepción, no la base. Incluso una mejora del 20-30% en la calidad de la dieta produce efectos medibles en la salud a medio plazo.</p>
          </div>
        </li>
      </ol>
