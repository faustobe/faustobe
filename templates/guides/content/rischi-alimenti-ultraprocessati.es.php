    <section id="obesita-metabolismo" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">1</div>
        <h2 class="guide-section-title">Obesidad y alteración del metabolismo</h2>
      </div>

      <p>
        Los alimentos ultraprocesados no engordan simplemente porque son calóricos. El mecanismo es más insidioso: están diseñados para ser <strong>hiper-apetecibles</strong> e interfieren activamente con las señales naturales de saciedad del cuerpo.
      </p>

      <div class="guide-insight">
        <strong>El estudio de Kevin Hall (NIH, 2019):</strong> en un ensayo clínico controlado, los participantes expuestos a una dieta basada en ultraprocesados consumían en promedio <strong>500 calorías más al día</strong> en comparación con quienes comían alimentos mínimamente procesados — con igual accesibilidad a la comida y libertad de elección. En solo dos semanas, el grupo UPF había ganado casi un kilo.
      </div>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🔥</span>
          <div class="guide-checklist__body">
            <strong>Alta densidad energética</strong>
            <p>Los UPF contienen en promedio <strong>2,15 kcal por gramo</strong> — casi el doble que los alimentos frescos. El mismo volumen de comida aporta muchas más calorías, sin la saciedad correspondiente.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🧠</span>
          <div class="guide-checklist__body">
            <strong>Estimulación excesiva de los centros de placer</strong>
            <p>La combinación industrial de azúcar, sal, grasas y aromas activa el sistema dopaminérgico de forma similar a las sustancias adictivas. El resultado: se come incluso en ausencia de hambre real.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">📉</span>
          <div class="guide-checklist__body">
            <strong>Reducción del péptido YY</strong>
            <p>Los UPF reducen los niveles del <strong>péptido YY</strong>, la hormona intestinal que señala al cerebro la saciedad. Quienes comen muchos ultraprocesados reciben menos señales de "basta" después de las comidas.</p>
          </div>
        </li>
      </ul>

      <div class="signal-cards">
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">📊</span>
          <div class="signal-card__content">
            <strong>+41% de riesgo de obesidad abdominal</strong>
            <p>Quienes siguen una dieta con alto contenido de UPF presentan un riesgo un 41% mayor de desarrollar obesidad o obesidad abdominal en comparación con quienes consumen pocos.</p>
          </div>
        </div>
      </div>
    </section>

