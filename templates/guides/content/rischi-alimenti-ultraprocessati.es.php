    <section id="rischio-sistemico" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">4</div>
        <h2 class="guide-section-title">Un riesgo sistémico: múltiples mecanismos a la vez</h2>
      </div>

      <p>
        Lo que hace a los UPF particularmente preocupantes no es un único efecto negativo, sino la convergencia de múltiples mecanismos que se refuerzan mutuamente.
      </p>

      <div class="guide-table-wrap">
        <table>
          <thead>
            <tr>
              <th>Mecanismo</th>
              <th>Efecto</th>
              <th>Consecuencia a largo plazo</th>
            </tr>
          </thead>
          <tbody>
            <tr>
              <td>Hiper-apetecibilidad</td>
              <td>Consumo excesivo de calorías</td>
              <td>Obesidad, diabetes tipo 2</td>
            </tr>
            <tr>
              <td>Carencia de fibra</td>
              <td>Disbiosis intestinal</td>
              <td>Inflamación crónica, EII</td>
            </tr>
            <tr>
              <td>Aditivos (emulsionantes)</td>
              <td>Permeabilidad intestinal</td>
              <td>Enfermedades autoinmunes, cardiovasculares</td>
            </tr>
            <tr>
              <td>Azúcares y edulcorantes</td>
              <td>Resistencia a la insulina, disbiosis</td>
              <td>Diabetes tipo 2, síndrome metabólico</td>
            </tr>
            <tr>
              <td>Sustitución del alimento fresco</td>
              <td>Carencia de micronutrientes</td>
              <td>Estrés oxidativo, inmunosupresión</td>
            </tr>
          </tbody>
        </table>
      </div>

      <p>
        En resumen, los UPF actúan como un <strong>factor de riesgo sistémico</strong> que promueve simultáneamente la resistencia a la insulina, el estrés oxidativo y la inflamación crónica de bajo grado — tres condiciones que están en la base de casi todas las enfermedades crónicas no transmisibles más difundidas en Occidente.
      </p>

      <div class="guide-insight">
        <strong>Los grandes estudios epidemiológicos lo confirman:</strong> NutriNet-Santé (Francia, más de 100.000 participantes) y UK Biobank (Reino Unido, más de 500.000 participantes) asocian el consumo elevado de UPF a mayores riesgos de cáncer, enfermedades cardiovasculares, depresión y mortalidad por todas las causas — independientemente de la calidad nutricional de los productos individuales.
      </div>
    </section>

