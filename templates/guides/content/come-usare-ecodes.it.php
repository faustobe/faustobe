            <p>Controlla merendine, snack salati, biscotti, dolci confezionati. Se la lista è lunghissima e piena di E, l'app segnalerà rosso. In quei casi, scegli sempre l'alternativa più semplice (frutta, pane integrale, noci).</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">3</span>
          <div class="guide-step-body">
            <strong>Usa l'app per confrontare prodotti simili</strong>
            <p>Cerca due prodotti dello stesso tipo (es. yogurt greco, pane, snack) e confronta le schede nell'app. Spesso quello con meno ingredienti è solo leggermente meno appariscente sullo scaffale, ma molto più naturale.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">4</span>
          <div class="guide-step-body">
