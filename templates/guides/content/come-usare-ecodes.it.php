        <h2 class="guide-section-title">Prima di scansionare, fai tre controlli rapidi</h2>
      </div>

      <p>
        Prima ancora di aprire l'app, osserva la confezione. Le etichette frontali sono spesso pensate per sembrare più sani di quello che sono.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">👁️</span>
          <div class="guide-checklist__body">
            <strong>Guarda il fronte della confezione</strong>
            <p>Fai attenzione ai claim come "senza zucchero ma con dolcificanti", "senza grassi ma con emulsionanti", "gusto extra" o "aromatizzato". Spesso indicano che il prodotto è stato molto modificato.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">📱</span>
          <div class="guide-checklist__body">
            <strong>Apri l'app e scansiona il prodotto</strong>
            <p>Usa la fotocamera per scansionare il codice a barre o carica una foto dell'etichetta. L'app ti mostrerà subito la lista degli ingredienti e degli additivi.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🔍</span>
          <div class="guide-checklist__body">
            <strong>Controlla tre punti chiave</strong>
            <p>Quanti <strong>additivi</strong> compaiono (soprattutto i codici E)? Quanto è <strong>lunga e complessa</strong> la lista ingredienti? Quanti <strong>zuccheri, sale e grassi</strong> sono dichiarati?</p>
          </div>
        </li>
      </ul>

      <div class="guide-insight">
        Se rispondi "molti" o "lunga/complicata" a più di una domanda, ti trovi probabilmente davanti a un cibo molto lavorato.
      </div>
