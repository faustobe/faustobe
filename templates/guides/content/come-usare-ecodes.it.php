            <p>Non serve eliminare tutto ciò che è ultra-processato: basta ridurlo. Deciditi una soglia — ad esempio, massimo 1–2 prodotti "rossi" alla settimana — mentre il resto della spesa sia fatto di cibi semplici.</p>
