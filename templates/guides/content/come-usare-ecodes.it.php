        <div class="signal-card signal-card--ok">
          <span class="signal-card__emoji">✅</span>
          <div class="signal-card__content">
            <strong>Pochi ingredienti = meglio</strong>
            <p>Se la lista è breve e contiene nomi familiari (es. "farina, acqua, sale, olio"), il prodotto è poco lavorato e in genere più affidabile.</p>
          </div>
        </div>
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🚩</span>
          <div class="signal-card__content">
            <strong>Molti ingredienti "impronunciabili"</strong>
            <p>Termini come "amidi modificati", "proteine isolate", "mono‑ e digliceridi degli acidi grassi", "esaltatori di sapidità" o "aromi complessi" indicano chiaramente un alimento molto processato.</p>
          </div>
        </div>
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🚩</span>
          <div class="signal-card__content">
            <strong>Tanti codici E e additivi</strong>
            <p>Non tutti gli E sono pericolosi, ma la loro presenza in massa — coloranti, conservanti, stabilizzanti, emulsionanti — indica un grado elevato di trasformazione industriale.</p>
          </div>
        </div>
      </div>
