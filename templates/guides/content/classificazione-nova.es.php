    <section id="nova-in-pratica" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">6</div>
        <h2 class="guide-section-title">NOVA en la compra diaria</h2>
      </div>

      <p>
        Aplicar NOVA en el supermercado no requiere aprenderse de memoria las categorías. Basta con hacerse algunas preguntas mentales mientras lees la etiqueta.
      </p>

      <ol class="guide-step-list">
        <li>
          <span class="guide-step-num">1</span>
          <div class="guide-step-body">
            <strong>"¿Podría cocinarlo en casa con ingredientes normales?"</strong>
            <p>Si la respuesta es sí, probablemente es Grupo 1, 2 o 3. Si no (porque usa ingredientes que no existen en tiendas normales), casi seguro es Grupo 4.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">2</span>
          <div class="guide-step-body">
            <strong>Busca las "palabras clave" del Grupo 4</strong>
            <p>"Almidón modificado", "proteínas de soja aisladas", "maltodextrinas", "jarabe de", "aromas complejos", "aceite de palma hidrogenado", "caseinato de sodio". Si aparecen, estás en el Grupo 4.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">3</span>
          <div class="guide-step-body">
            <strong>Regla práctica: más de la mitad del plato del Grupo 1</strong>
            <p>No se trata de eliminar cada ultraprocesado. El objetivo es que verduras, legumbres, cereales en grano, huevos y carnes/pescados sin procesar sean la base de tus comidas principales.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">4</span>
          <div class="guide-step-body">
            <strong>Usa la app cuando tengas dudas</strong>
            <p>E-Codes Reader muestra la clasificación NOVA de cada producto escaneado. En los casos en que la etiqueta es ambigua, la app te da una respuesta en un segundo.</p>
          </div>
        </li>
      </ol>

