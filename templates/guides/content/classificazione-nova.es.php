    <section id="cosa-e-nova" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">1</div>
        <h2 class="guide-section-title">Qué es la clasificación NOVA</h2>
      </div>

      <p>
        NOVA es un sistema de clasificación de alimentos desarrollado por el grupo del profesor Carlos Monteiro en la Universidad de São Paulo (Brasil) y hoy adoptado por la OMS, la FAO y numerosos estudios epidemiológicos globales.
      </p>

      <p>
        A diferencia de los sistemas basados en nutrientes (como el Nutri-Score, que evalúa calorías, grasas, azúcares y fibra), <strong>NOVA clasifica según el grado de transformación industrial</strong>: no "cuántas calorías tiene", sino "cuánta industria ha intervenido en su producción".
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">💡</span>
          <div class="guide-checklist__body">
            <strong>Por qué importa la transformación</strong>
            <p>Los procesos industriales modernos pueden alterar la estructura de la fibra, la biodisponibilidad de los nutrientes, el microbioma intestinal y la respuesta hormonal a la saciedad — independientemente de la composición nutricional final del producto.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">📋</span>
          <div class="guide-checklist__body">
            <strong>4 grupos, no una escala continua</strong>
            <p>NOVA divide todos los alimentos en 4 grupos distintos. No es una escala del 1 al 10, sino una clasificación categórica. Cada grupo tiene características bien definidas.</p>
          </div>
        </li>
      </ul>
    </section>

    <hr class="guide-divider" />

    <!-- SECCIÓN 2 -->
    <section id="gruppo-1" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">2</div>
        <h2 class="guide-section-title">Grupo 1 — Alimentos no procesados o mínimamente procesados</h2>
      </div>

      <p>
        Son alimentos de origen vegetal o animal que no han sufrido ninguna transformación industrial significativa, o solo procesos físicos mínimos (secado, molido, refrigeración, pasteurización) que no alteran la naturaleza del alimento.
      </p>

      <div class="signal-cards">
        <div class="signal-card signal-card--ok">
          <span class="signal-card__emoji">🥦</span>
          <div class="signal-card__content">
            <strong>Ejemplos del Grupo 1</strong>
            <p>Fruta y verdura fresca o congelada, carne y pescado frescos o congelados sin aditivos, huevos, legumbres secas, cereales en grano (arroz, avena, espelta), harina integral, leche fresca pasteurizada, yogur natural sin aditivos, té, café, agua.</p>
          </div>
        </div>
        <div class="signal-card signal-card--ok">
          <span class="signal-card__emoji">✅</span>
          <div class="signal-card__content">
            <strong>Objetivo práctico: la base de la dieta</strong>
            <p>Los alimentos del Grupo 1 deberían constituir la mayor parte de cada comida. No exclusivamente — se cocina con los ingredientes del Grupo 2 — pero la mayor parte de lo que acaba en el plato.</p>
          </div>
        </div>
      </div>
    </section>

    <hr class="guide-divider" />

    <!-- SECCIÓN 3 -->
    <section id="gruppo-2" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">3</div>
        <h2 class="guide-section-title">Grupo 2 — Ingredientes culinarios procesados</h2>
      </div>

      <p>
        Son sustancias extraídas de alimentos del Grupo 1 o de la naturaleza, usadas en la cocina para cocinar, condimentar y preparar la comida. No se consumen solos sino en combinación con el Grupo 1.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🫙</span>
          <div class="guide-checklist__body">
            <strong>Ejemplos del Grupo 2</strong>
            <p>Aceite de oliva y otros aceites vegetales, mantequilla, vinagre, sal, azúcar, miel, harina blanca, almidón de maíz puro, jarabe de arce. También la leche en polvo y los quesos curados tradicionales pertenecen al Grupo 2.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">✅</span>
          <div class="guide-checklist__body">
            <strong>Úsalos con moderación</strong>
            <p>No son problemáticos en sí mismos, pero el azúcar, la sal y las grasas saturadas en exceso siguen siendo perjudiciales. El hecho de que sean Grupo 2 no los convierte en "ilimitados".</p>
          </div>
        </li>
      </ul>
    </section>

    <hr class="guide-divider" />

    <!-- SECCIÓN 4 -->
    <section id="gruppo-3" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">4</div>
        <h2 class="guide-section-title">Grupo 3 — Alimentos procesados</h2>
      </div>

      <p>
        Son productos obtenidos añadiendo ingredientes del Grupo 2 a alimentos del Grupo 1, mediante procesos como la conservación en sal, el ahumado, la fermentación o la curación. Se reconocen porque suelen contener solo 2–3 ingredientes.
      </p>

      <div class="signal-cards">
        <div class="signal-card signal-card--warning">
          <span class="signal-card__emoji">⚠️</span>
          <div class="signal-card__content">
            <strong>Ejemplos del Grupo 3</strong>
            <p>Verduras y legumbres en conserva (con agua y sal), fruta confitada y en almíbar, frutos secos tostados y salados, anchoas y sardinas en aceite, jamón serrano de calidad, quesos tradicionales (parmesano, manchego), pan artesanal, vino, cerveza artesanal, aceite virgen extra con acidulante.</p>
          </div>
        </div>
        <div class="signal-card signal-card--ok">
          <span class="signal-card__emoji">✅</span>
          <div class="signal-card__content">
            <strong>Son aceptables en la dieta diaria</strong>
            <p>El Grupo 3 no debe evitarse. Muchos alimentos tradicionales y culturalmente importantes pertenecen a este grupo. Pueden ser ricos en sal (conservas, embutidos) o azúcares (fruta en almíbar): valora en función de la cantidad.</p>
          </div>
        </div>
      </div>
    </section>

    <hr class="guide-divider" />

    <!-- SECCIÓN 5 -->
    <section id="gruppo-4" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">5</div>
        <h2 class="guide-section-title">Grupo 4 — Ultraprocesados: el grupo que hay que limitar</h2>
      </div>

      <p>
        Los ultraprocesados son formulaciones industriales que no contienen alimentos enteros o los usan en una proporción mínima. Están elaborados con ingredientes extraídos, purificados o modificados químicamente, más aditivos tecnológicos en grandes cantidades.
      </p>

      <p>
        La señal clave: contienen ingredientes que <strong>no se encuentran en una cocina doméstica</strong>.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🚩</span>
          <div class="guide-checklist__body">
            <strong>Ingredientes típicos del Grupo 4</strong>
            <p>Proteínas hidrolizadas, almidones modificados, jarabe de glucosa-fructosa, aceites hidrogenados, aromas "idénticos a los naturales", colorantes artificiales, edulcorantes intensivos (aspartamo, sacarina, sucralosa), emulsionantes (E471, E472), estabilizantes, agentes leudantes químicos múltiples.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">📦</span>
          <div class="guide-checklist__body">
            <strong>Ejemplos comunes</strong>
            <p>Bollería industrial, cereales de desayuno azucarados, bebidas gaseosas y energéticas, pan de molde con aditivos, salchichas y embutidos reconstituidos, nuggets y palitos de pescado, yogures aromatizados con espesantes, platos preparados congelados, pizza industrial, salsas en sobre, caldos en pastilla con potenciadores del sabor.</p>
          </div>
        </li>
      </ul>

      <div class="guide-insight">
        <strong>Datos clave:</strong> en Italia alrededor del 14–18% de las calorías diarias proviene de ultraprocesados. En el Reino Unido y EE. UU. el porcentaje supera el 50–60%. La investigación muestra que ya un consumo superior al 20% de las calorías diarias de NOVA 4 se asocia con riesgos para la salud.
      </div>
    </section>

