      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">4</div>
        <h2 class="guide-section-title">Das doppelte Problem: warum beides z&auml;hlt</h2>
      </div>

      <p>
        Wer nur auf Zusatzstoffe oder nur auf den Verarbeitungsgrad schaut, kommt zu unvollst&auml;ndigen Bewertungen. Die gesundheitlichen Risiken kommen von beiden Seiten.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🧬</span>
          <div class="guide-checklist__body">
            <strong>Risiken durch spezifische Zusatzstoffe</strong>
            <p>Bestimmte Zusatzstoffe haben dokumentierte Wirkungen: Azofarbstoffe und Hyperaktivit&auml;t bei Kindern (EFSA 2007), Nitrate in verarbeitetem Fleisch und krebserregende Nitrosamine, intensive S&uuml;&szlig;ungsmittel und m&ouml;gliche Ver&auml;nderungen des Darmmikrobioms.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🏭</span>
          <div class="guide-checklist__body">
            <strong>Risiken durch Ultra-Verarbeitung als solche</strong>
            <p>Gro&szlig;angelegte Studien (NutriNet-Sant&eacute;, UK Biobank) bringen einen hohen Konsum von NOVA 4 mit einem erh&ouml;hten Risiko f&uuml;r Adipositas, Typ-2-Diabetes, Herz-Kreislauf-Erkrankungen und bestimmte Krebsarten in Verbindung &mdash; unabh&auml;ngig von der n&auml;hrstofflichen Qualit&auml;t.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🔁</span>
          <div class="guide-checklist__body">
            <strong>Der Kombinationseffekt</strong>
            <p>Studien zu Zusatzstoffen testen fast immer eine Substanz nach der anderen. Im echten Leben nehmen wir aber Kombinationen von 10&ndash;20 verschiedenen Zus&auml;tzen im selben Essen zu uns. Die synergistischen Effekte sind noch wenig erforscht.</p>
          </div>
        </li>
      </ul>
    </section>

