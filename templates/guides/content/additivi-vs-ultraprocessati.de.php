        <h2 class="guide-section-title">Wie man sie in der Praxis erkennt: das doppelte Lesen</h2>
      </div>

      <p>
        F&uuml;r eine vollst&auml;ndige Bewertung wende beim Lesen eines Etiketts beide Kriterien an.
      </p>

      <ol class="guide-step-list">
        <li>
          <span class="guide-step-num">1</span>
          <div class="guide-step-body">
            <strong>Pr&uuml;fen, ob &bdquo;Laborzutaten&ldquo; enthalten sind</strong>
            <p>Modifizierte St&auml;rken, isolierte Proteine, Hydrolysate, Glukose-Fruktose-Sirupe, &bdquo;naturidentische&ldquo; Aromen, partiell hydrierte &Ouml;le: Das sind die Hauptsignale f&uuml;r Ultra-Verarbeitung &mdash; auch ohne viele E-Codes.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">2</span>
          <div class="guide-step-body">
            <strong>E-Codes z&auml;hlen und kritische Kategorien identifizieren</strong>
            <p>Ein nat&uuml;rliches Antioxidans (E306) ist sehr verschieden von einem Azofarbstoff (E102) oder einem intensiven S&uuml;&szlig;ungsmittel (E951). Nutze die App, um das Risiko jedes Zusatzstoffs herauszufinden.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">3</span>
          <div class="guide-step-body">
            <strong>Den Mahlzeitenkontext ber&uuml;cksichtigen</strong>
            <p>Eine ausgewogene Mahlzeit mit Gem&uuml;se, H&uuml;lsenfr&uuml;chten und Vollkorngetreide &bdquo;vertr&auml;gt&ldquo; das eine oder andere st&auml;rker verarbeitete Produkt besser. Das Problem entsteht, wenn ultra-verarbeitete Lebensmittel die Grundlage der t&auml;glichen Ern&auml;hrung werden.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">4</span>
          <div class="guide-step-body">
            <strong>Die App als Unterst&uuml;tzung nutzen, nicht als absoluten Richter</strong>
            <p>E-Codes Reader zeigt dir die NOVA-Klassifizierung und Details zu Zus&auml;tzen. Nutze sie, um Produkte besser zu verstehen, nicht um Angst zu erzeugen. Das Ziel ist, Ultra-Verarbeitetes mit der Zeit zu reduzieren &mdash; nicht alles auf einmal zu eliminieren.</p>
          </div>
        </li>
      </ol>

      <div class="guide-insight">
        <strong>Zusammengefasst in einem Satz:</strong> Ein Produkt mit einem nat&uuml;rlichen E ist kein Problem. Ein Produkt mit 15 industriellen Zutaten ohne ein einziges E ist trotzdem ein Ultra-Verarbeitetes. Schaue auf beides.
