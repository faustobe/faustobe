      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">1</div>
        <h2 class="guide-section-title">Was sind Lebensmittelzusatzstoffe</h2>
      </div>

      <p>
        Lebensmittelzusatzstoffe sind Stoffe, die Lebensmitteln gezielt zugesetzt werden, um eine technologische Funktion zu erf&uuml;llen: haltbar machen, f&auml;rben, andicken, emulgieren, stabilisieren oder den Geschmack verbessern. In Europa erh&auml;lt jeder zugelassene Zusatzstoff einen <strong>E</strong>-Code (E100, E200, E300 &hellip;) und muss eine Sicherheitsbewertung der EFSA bestehen.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🔵</span>
          <div class="guide-checklist__body">
            <strong>Nicht alle Zusatzstoffe sind synthetisch</strong>
            <p>Zitronens&auml;ure (E330), Curcumin (E100), Sojalecithin (E322) und Ascorbins&auml;ure (E300, Vitamin C) sind Zusatzstoffe nat&uuml;rlichen Ursprungs, die auch in vielen unverarbeiteten Lebensmitteln vorkommen.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🔵</span>
          <div class="guide-checklist__body">
            <strong>Nicht alle Zusatzstoffe sind gef&auml;hrlich</strong>
            <p>Die gro&szlig;e Mehrheit der zugelassenen E-Nummern gilt in den erlaubten Dosierungen als sicher. Das Problem liegt nicht in der Anwesenheit eines E-Codes an sich, sondern in der <em>Menge</em> und der <em>Kombination</em> vieler verschiedener Zusatzstoffe im selben Produkt.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">⚠️</span>
          <div class="guide-checklist__body">
            <strong>Einige Zusatzstoffe verdienen besondere Beachtung</strong>
            <p>Azofarbstoffe (E102, E110, E122, E124, E129), Konservierungsstoffe wie Nitrate (E249&ndash;E252), intensive S&uuml;&szlig;ungsmittel (E950&ndash;E962): Diese Kategorien haben robustere Risikohinweise, vor allem f&uuml;r Kinder.</p>
          </div>
        </li>
      </ul>
    </section>

