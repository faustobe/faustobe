      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">2</div>
        <h2 class="guide-section-title">Was sind Ultra-Verarbeitete Lebensmittel</h2>
      </div>

      <p>
        Der Begriff &bdquo;ultra-verarbeitet&ldquo; stammt aus der <strong>NOVA-Klassifizierung</strong>, die an der Universit&auml;t S&atilde;o Paulo entwickelt wurde. Es geht dabei nicht um N&auml;hrstoffe, sondern um den <em>industriellen Prozess</em>, der das Lebensmittel hergestellt hat.
      </p>

      <p>
        Ein Lebensmittel gilt als ultra-verarbeitet (NOVA 4), wenn es Zutaten enth&auml;lt, die in einer Haushaltsк&uuml;che nicht vorkommen: isolierte Proteine, modifizierte St&auml;rken, Proteinhydrolysate, Glukose-Fruktose-Sirupe, &bdquo;naturidentische&ldquo; Aromen, Emulgatoren, Farbstoffe, Stabilisatoren &mdash; oft in komplexen Kombinationen.
      </p>

      <div class="signal-cards">
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🚩</span>
          <div class="signal-card__content">
            <strong>Beispiele g&auml;ngiger ultra-verarbeiteter Produkte</strong>
            <p>Fertigsnacks und verpackte S&uuml;&szlig;igkeiten, s&uuml;&szlig;e Fr&uuml;hst&uuml;cksflocken, Softdrinks und Fruchtnektare, rekonstituierte W&uuml;rstchen und Aufschnitt, tiefgefrorene Fertiggerichte mit So&szlig;en, aromatisierter Joghurt mit Verdickungsmitteln, industrielles Toastbrot mit vielen Zus&auml;tzen.</p>
          </div>
        </div>
        <div class="signal-card signal-card--ok">
          <span class="signal-card__emoji">✅</span>
          <div class="signal-card__content">
            <strong>Was KEIN Ultra-Verarbeitetes ist</strong>
            <p>Gereifter K&auml;se (enth&auml;lt wenige Zus&auml;tze), qualit&auml;tvoller ganzer Aufschnitt (auch wenn er verarbeitet ist), handwerkliches Brot, traditionelle Trockenpasta, Tomatenkonserven ohne Zus&auml;tze, Essig, natives Oliven&ouml;l extra.</p>
          </div>
        </div>
      </div>
    </section>

