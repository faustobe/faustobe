    <section id="cosa-e-nova" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">1</div>
        <h2 class="guide-section-title">What the NOVA classification is</h2>
      </div>

      <p>
        NOVA is a food classification system developed by the research group of Professor Carlos Monteiro at the University of São Paulo (Brazil) and now adopted by the WHO, FAO and numerous global epidemiological studies.
      </p>

      <p>
        Unlike nutrient-based systems (such as Nutri-Score, which evaluates calories, fat, sugar and fibre), <strong>NOVA classifies foods according to their degree of industrial processing</strong>: not "how many calories does it have", but "how much industrial transformation went into making it".
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">💡</span>
          <div class="guide-checklist__body">
            <strong>Why processing matters</strong>
            <p>Modern industrial processes can alter fibre structure, nutrient bioavailability, the gut microbiome and the hormonal response to satiety — regardless of the final nutritional composition of the product.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">📋</span>
          <div class="guide-checklist__body">
            <strong>4 groups, not a continuous scale</strong>
            <p>NOVA divides all foods into 4 distinct groups. It's not a scale from 1 to 10, but a categorical classification. Each group has well-defined characteristics.</p>
          </div>
        </li>
      </ul>
    </section>

    <hr class="guide-divider" />

    <!-- SECTION 2 -->
    <section id="gruppo-1" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">2</div>
        <h2 class="guide-section-title">Group 1 — Unprocessed or minimally processed foods</h2>
      </div>

      <p>
        These are foods of plant or animal origin that have not undergone any significant industrial transformation, or only minimal physical processes (drying, milling, chilling, pasteurisation) that do not alter the nature of the food.
      </p>

      <div class="signal-cards">
        <div class="signal-card signal-card--ok">
          <span class="signal-card__emoji">🥦</span>
          <div class="signal-card__content">
            <strong>Examples of Group 1</strong>
            <p>Fresh or frozen fruit and vegetables, fresh or frozen meat and fish without additives, eggs, dried pulses, whole-grain cereals (rice, oats, spelt), wholemeal flour, fresh pasteurised milk, plain yoghurt without additives, tea, coffee, water.</p>
          </div>
        </div>
        <div class="signal-card signal-card--ok">
          <span class="signal-card__emoji">✅</span>
          <div class="signal-card__content">
            <strong>Practical goal: the base of your diet</strong>
            <p>Group 1 foods should make up the majority of every meal. Not exclusively — you cook with Group 2 ingredients — but most of what ends up on the plate.</p>
          </div>
        </div>
      </div>
    </section>

    <hr class="guide-divider" />

    <!-- SECTION 3 -->
    <section id="gruppo-2" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">3</div>
        <h2 class="guide-section-title">Group 2 — Processed culinary ingredients</h2>
      </div>

      <p>
        These are substances extracted from Group 1 foods or from nature, used in the kitchen to cook, season and prepare food. They are not eaten on their own, but in combination with Group 1 foods.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🫙</span>
          <div class="guide-checklist__body">
            <strong>Examples of Group 2</strong>
            <p>Olive oil and other vegetable oils, butter, vinegar, salt, sugar, honey, plain flour, pure cornstarch, maple syrup. Powdered milk and traditional aged cheeses also belong to Group 2.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">✅</span>
          <div class="guide-checklist__body">
            <strong>Use them in moderation</strong>
            <p>They are not problematic in themselves, but excess sugar, salt and saturated fat remain unfavourable. Being Group 2 doesn't mean they're "unlimited".</p>
          </div>
        </li>
      </ul>
    </section>

    <hr class="guide-divider" />

    <!-- SECTION 4 -->
    <section id="gruppo-3" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">4</div>
        <h2 class="guide-section-title">Group 3 — Processed foods</h2>
      </div>

      <p>
        These are products made by adding Group 2 ingredients to Group 1 foods, using processes such as salt-preservation, smoking, fermentation or ageing. Recognisable because they typically contain just 2–3 ingredients.
      </p>

      <div class="signal-cards">
        <div class="signal-card signal-card--warning">
          <span class="signal-card__emoji">⚠️</span>
          <div class="signal-card__content">
            <strong>Examples of Group 3</strong>
            <p>Canned vegetables and pulses (with water and salt), candied and syrup-preserved fruit, roasted salted nuts, anchovies and sardines in oil, quality cured ham, traditional cheeses (parmesan, pecorino), artisan bread, wine, craft beer, extra-virgin olive oil with acidifier.</p>
          </div>
        </div>
        <div class="signal-card signal-card--ok">
          <span class="signal-card__emoji">✅</span>
          <div class="signal-card__content">
            <strong>Acceptable in the daily diet</strong>
            <p>Group 3 should not be avoided. Many traditional and culturally important foods belong to this group. They may however be high in salt (preserves, cured meats) or sugar (syrup fruit): judge by quantity.</p>
          </div>
        </div>
      </div>
    </section>

    <hr class="guide-divider" />

    <!-- SECTION 5 -->
    <section id="gruppo-4" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">5</div>
        <h2 class="guide-section-title">Group 4 — Ultra-processed: the group to limit</h2>
      </div>

      <p>
        Ultra-processed foods are industrial formulations that contain no whole food ingredients, or use them only minimally. They are built from extracted, purified or chemically modified ingredients, plus large quantities of technological additives.
      </p>

      <p>
        The key signal: they contain ingredients that <strong>you wouldn't find in a home kitchen</strong>.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🚩</span>
          <div class="guide-checklist__body">
            <strong>Typical Group 4 ingredients</strong>
            <p>Hydrolysed proteins, modified starches, glucose-fructose syrup, hydrogenated oils, "nature-identical" flavourings, artificial colorings, intense sweeteners (aspartame, saccharin, sucralose), emulsifiers (E471, E472), stabilisers, multiple chemical leavening agents.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">📦</span>
          <div class="guide-checklist__body">
            <strong>Common examples</strong>
            <p>Industrial snack cakes, sugary breakfast cereals, carbonated drinks and energy drinks, additive-laden sandwich bread, reconstructed hot dogs and deli meats, chicken nuggets and fish fingers, flavoured yoghurts with thickeners, frozen ready meals, industrial pizza, packet sauces, stock cubes with flavour enhancers.</p>
          </div>
        </li>
      </ul>

      <div class="guide-insight">
        <strong>Key data:</strong> in Italy around 14–18% of daily calories come from ultra-processed foods. In the UK and USA the figure exceeds 50–60%. Research shows that even consuming more than 20% of daily calories from NOVA 4 foods is associated with health risks.
      </div>
    </section>

