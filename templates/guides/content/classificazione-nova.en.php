      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">6</div>
        <h2 class="guide-section-title">NOVA in everyday shopping</h2>
      </div>

      <p>
        Applying NOVA at the supermarket doesn't require memorising all the categories. A few mental questions while reading a label are enough.
      </p>

      <ol class="guide-step-list">
        <li>
          <span class="guide-step-num">1</span>
          <div class="guide-step-body">
            <strong>"Could I make this at home with normal ingredients?"</strong>
            <p>If the answer is yes, it's probably Group 1, 2 or 3. If no (because it uses ingredients that don't exist in regular shops), it's almost certainly Group 4.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">2</span>
          <div class="guide-step-body">
            <strong>Look for Group 4 "trigger words"</strong>
            <p>"Modified starch", "isolated soy protein", "maltodextrins", "syrup of", "complex flavourings", "hydrogenated palm oil", "sodium caseinate". If these appear, you're in Group 4.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">3</span>
          <div class="guide-step-body">
            <strong>Practical rule: more than half your plate from Group 1</strong>
            <p>It's not about eliminating every ultra-processed food. The goal is for vegetables, legumes, whole grains, eggs and unprocessed meat/fish to form the foundation of your main meals.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">4</span>
          <div class="guide-step-body">
            <strong>Use the app when in doubt</strong>
            <p>E-Codes Reader shows the NOVA classification for every scanned product. When the label is ambiguous, the app gives you an answer in seconds.</p>
          </div>
        </li>
      </ol>

