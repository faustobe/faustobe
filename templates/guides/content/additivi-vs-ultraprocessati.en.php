    <!-- SECTION 4 -->
    <section id="doppio-problema" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">4</div>
        <h2 class="guide-section-title">The double problem: why both matter</h2>
      </div>

      <p>
        Looking only at additives or only at the degree of processing leads to an incomplete picture. Health risks come from both directions.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🧬</span>
          <div class="guide-checklist__body">
            <strong>Risks linked to specific additives</strong>
            <p>Certain additives have documented effects: azo dyes and hyperactivity in children (EFSA 2007), nitrates in processed meat and carcinogenic nitrosamines, intense sweeteners and possible disruption of the gut microbiome.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🏭</span>
          <div class="guide-checklist__body">
            <strong>Risks linked to ultra-processing itself</strong>
            <p>Large-scale studies (NutriNet-Santé, UK Biobank) associate high consumption of NOVA 4 foods with increased risk of obesity, type 2 diabetes, cardiovascular disease and some cancers — independently of nutritional quality.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🔁</span>
          <div class="guide-checklist__body">
            <strong>The combination effect</strong>
            <p>Additive studies almost always test one substance at a time. But in real life we eat combinations of 10–20 different additives in the same meal. The synergistic effects are still poorly understood.</p>
          </div>
        </li>
      </ul>
    </section>

