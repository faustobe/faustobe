    <!-- SECTION 1 -->
    <section id="cosa-sono-additivi" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">1</div>
        <h2 class="guide-section-title">What food additives are</h2>
      </div>

      <p>
        Food additives are substances intentionally added to food to perform a technological function: preserving, colouring, thickening, emulsifying, stabilising or enhancing flavour. In Europe, every approved additive receives an <strong>E code</strong> (E100, E200, E300...) and must pass a safety assessment by EFSA.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🔵</span>
          <div class="guide-checklist__body">
            <strong>Not all additives are synthetic</strong>
            <p>Citric acid (E330), curcumin (E100), soy lecithin (E322) and ascorbic acid (E300, vitamin C) are naturally derived additives, also found in many unprocessed foods.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🔵</span>
          <div class="guide-checklist__body">
            <strong>Not all additives are dangerous</strong>
            <p>The vast majority of approved E codes are considered safe at permitted usage levels. The issue isn't the presence of an E code, but the <em>quantity</em> and <em>combination</em> of many different additives in the same product.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">⚠️</span>
          <div class="guide-checklist__body">
            <strong>Some additives deserve attention</strong>
            <p>Azo dyes (E102, E110, E122, E124, E129), preservatives such as nitrates (E249–E252), and intense sweeteners (E950–E962): these categories have more consistent risk evidence, especially for children.</p>
          </div>
        </li>
      </ul>
    </section>

    <hr class="guide-divider" />

    <!-- SECTION 2 -->
    <section id="cosa-sono-ultraprocessati" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">2</div>
        <h2 class="guide-section-title">What ultra-processed foods are</h2>
      </div>

      <p>
        The term "ultra-processed" comes from the <strong>NOVA classification</strong>, developed at the University of São Paulo. It's not about nutrients, but about the <em>industrial process</em> that produced the food.
      </p>

      <p>
        A food is ultra-processed (NOVA 4) when it contains ingredients you wouldn't find in a home kitchen: isolated proteins, modified starches, protein hydrolysates, glucose-fructose syrups, "nature-identical" flavourings, emulsifiers, colorings, stabilisers — often in complex combinations.
      </p>

      <div class="signal-cards">
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🚩</span>
          <div class="signal-card__content">
            <strong>Examples of common ultra-processed foods</strong>
            <p>Snack cakes, packaged snacks, sugary breakfast cereals, carbonated drinks and "fruit-based" juices, reconstructed hot dogs and deli meats, frozen ready meals with sauces, flavoured yoghurts with thickeners, industrial sandwich bread with many additives.</p>
          </div>
        </div>
        <div class="signal-card signal-card--ok">
          <span class="signal-card__emoji">✅</span>
          <div class="signal-card__content">
            <strong>What is NOT ultra-processed</strong>
            <p>Aged cheeses (they have few additives), quality whole cured meats (even if processed), artisan bread, traditional dried pasta, tomato preserves without additives, vinegar, extra-virgin olive oil.</p>
          </div>
        </div>
      </div>
    </section>

    <hr class="guide-divider" />

    <!-- SECTION 3 -->
    <section id="quando-coincidono" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">3</div>
        <h2 class="guide-section-title">When they overlap — and when they don't</h2>
      </div>

      <p>
        This is the part that surprises most people: the overlap between "contains additives" and "ultra-processed" is not total.
      </p>

      <div class="rating-grid">
        <div class="rating-block rating-block--green">
          <div class="rating-block__dot">✅</div>
          <div class="rating-block__body">
            <strong>Additives but NOT ultra-processed</strong>
            <ul>
              <li>Cheese with preservative E252 (potassium nitrate): processed but not NOVA 4</li>
              <li>Wine with sulphites (E220): natural additive, traditional product</li>
              <li>Oil with vitamin E (E306) as an antioxidant: minimal processing</li>
            </ul>
          </div>
        </div>
        <div class="rating-block rating-block--red">
          <div class="rating-block__dot">🚩</div>
          <div class="rating-block__body">
            <strong>Ultra-processed with FEW E additives</strong>
            <ul>
              <li>Toast bread with modified starch and flavourings: not many E codes, but still NOVA 4</li>
              <li>Protein bars with isolated proteins and rice syrup: few codes, but heavily transformed</li>
              <li>Surimi: almost no E codes on the list, but made from reconstructed fish proteins</li>
            </ul>
          </div>
        </div>
        <div class="rating-block rating-block--yellow">
          <div class="rating-block__dot">⚠️</div>
          <div class="rating-block__body">
            <strong>Ultra-processed AND many additives (the worst case)</strong>
            <ul>
              <li>Snack cakes: modified starches + colorings + emulsifiers + flavourings + sweeteners</li>
              <li>Industrial sauces: preservatives + stabilisers + flavour enhancers + colorings</li>
              <li>Energy drinks: sweeteners + colorings + acidifiers + complex flavourings</li>
            </ul>
          </div>
        </div>
      </div>
    </section>

