    <section id="come-riconoscerli" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">5</div>
        <h2 class="guide-section-title">How to spot them in practice: the double read</h2>
      </div>

      <p>
        For a complete assessment, apply both criteria whenever you read a label.
      </p>

      <ol class="guide-step-list">
        <li>
          <span class="guide-step-num">1</span>
          <div class="guide-step-body">
            <strong>Look for "lab-derived" ingredients</strong>
            <p>Modified starches, isolated proteins, hydrolysates, glucose-fructose syrups, "nature-identical" flavourings, partially hydrogenated oils: these are the main signal of ultra-processing even without many E codes.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">2</span>
          <div class="guide-step-body">
            <strong>Count the E codes and identify critical categories</strong>
            <p>A natural antioxidant E code (E306) is very different from an azo dye (E102) or an intense sweetener (E951). Use the app to check the risk level of each additive.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">3</span>
          <div class="guide-step-body">
            <strong>Consider the meal context</strong>
            <p>A balanced meal with vegetables, legumes and whole grains can better accommodate the occasional more processed product. The problem is when ultra-processed foods become the foundation of your daily diet.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">4</span>
          <div class="guide-step-body">
            <strong>Use the app as a guide, not the final judge</strong>
            <p>E-Codes Reader shows you the NOVA classification and details on additives. Use it to better understand products, not to create anxiety. The goal is to reduce ultra-processed foods over time, not to eliminate them all in a single day.</p>
          </div>
        </li>
      </ol>

      <div class="guide-insight">
        <strong>Summary in one sentence:</strong> a product with one natural E code is not a problem. A product with 15 industrial ingredients and no E codes at all is still ultra-processed. Look at both things.
      </div>
    </section>

