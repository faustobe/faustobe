      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">2</div>
        <h2 class="guide-section-title">Drei Regeln, um sofort zu erkennen, ob ein Lebensmittel „zu verarbeitet" ist</h2>
      </div>

      <p>
        Sie können diese drei Regeln jedes Mal anwenden, wenn Sie ein Produkt in der App überprüfen — besondere Vorkenntnisse sind nicht erforderlich.
      </p>

      <div class="signal-cards">
        <div class="signal-card signal-card--ok">
          <span class="signal-card__emoji">✅</span>
          <div class="signal-card__content">
            <strong>Wenige Zutaten = besser</strong>
            <p>Wenn die Liste kurz ist und vertraute Namen enthält (z. B. „Mehl, Wasser, Salz, Öl"), ist das Produkt wenig verarbeitet und in der Regel zuverlässiger.</p>
          </div>
        </div>
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🚩</span>
          <div class="signal-card__content">
            <strong>Viele „unaussprechliche" Zutaten</strong>
            <p>Begriffe wie „modifizierte Stärken", „isolierte Proteine", „Mono- und Diglyceride von Fettsäuren", „Geschmacksverstärker" oder „komplexe Aromen" sind ein eindeutiges Zeichen für ein stark verarbeitetes Lebensmittel.</p>
          </div>
        </div>
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🚩</span>
          <div class="signal-card__content">
            <strong>Viele E-Nummern und Zusatzstoffe</strong>
            <p>Nicht alle E-Nummern sind schädlich, aber ihr massives Vorkommen — Farbstoffe, Konservierungsstoffe, Stabilisatoren, Emulgatoren — weist auf einen hohen Grad industrieller Verarbeitung hin.</p>
          </div>
        </div>
      </div>
    </section>

