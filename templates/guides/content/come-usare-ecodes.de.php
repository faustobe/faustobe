    <section id="guida-pratica" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">4</div>
        <h2 class="guide-section-title">So nutzen Sie die App praktisch beim Einkaufen</h2>
      </div>

      <p>
        Die App in Ihren Einkaufsalltag zu integrieren ist einfacher als gedacht. Folgen Sie diesen vier Schritten beim ersten Mal — sie werden schnell zur natürlichen Gewohnheit.
      </p>

      <ol class="guide-step-list">
        <li>
          <span class="guide-step-num">1</span>
          <div class="guide-step-body">
            <strong>Beginnen Sie mit dem Kühl- und Frühstücksbereich</strong>
            <p>Scannen Sie Joghurt, Brot, Milch, Getränke. Suchen Sie nach Produkten mit wenigen Zutaten, wenigen E-Nummern und einfachen Zuckern (Obst, Milch, Rohrzucker) statt Sirupen und Süßungsmitteln.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">2</span>
          <div class="guide-step-body">
            <strong>Dann weiter zu Süßwaren und Snacks</strong>
            <p>Überprüfen Sie Gebäck, salzige Snacks, Kekse, verpackte Süßigkeiten. Wenn die Liste sehr lang und voller E-Nummern ist, zeigt die App Rot an. Wählen Sie dann immer die einfachere Alternative (Obst, Vollkornbrot, Nüsse).</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">3</span>
          <div class="guide-step-body">
            <strong>Nutzen Sie die App zum Vergleich ähnlicher Produkte</strong>
            <p>Suchen Sie zwei Produkte derselben Art (z. B. griechischer Joghurt, Brot, Snacks) und vergleichen Sie deren Profile in der App. Oft ist das mit weniger Zutaten nur geringfügig unscheinbarer im Regal, aber deutlich natürlicher.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">4</span>
          <div class="guide-step-body">
            <strong>„Rote" Produkte als Ausnahme betrachten, nicht als Regel</strong>
            <p>Es ist nicht nötig, alles Stark-Verarbeitete zu eliminieren — es genügt, es zu reduzieren. Setzen Sie sich ein Limit — zum Beispiel maximal 1–2 „rote" Produkte pro Woche — während der Rest Ihres Einkaufs aus einfachen Lebensmitteln besteht.</p>
          </div>
        </li>
      </ol>
    </section>

