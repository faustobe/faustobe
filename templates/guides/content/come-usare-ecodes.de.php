        <div class="guide-section-number" aria-hidden="true">1</div>
        <h2 class="guide-section-title">Vor dem Scannen: drei schnelle Überprüfungen</h2>
      </div>

      <p>
        Schauen Sie sich die Verpackung an, noch bevor Sie die App öffnen. Vorderseiten-Etiketten sind oft so gestaltet, dass Produkte gesünder wirken, als sie wirklich sind.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">👁️</span>
          <div class="guide-checklist__body">
            <strong>Schauen Sie auf die Vorderseite der Verpackung</strong>
            <p>Achten Sie auf Aussagen wie „zuckerfrei, aber mit Süßungsmitteln", „fettfrei, aber mit Emulgatoren", „Extra-Geschmack" oder „aromatisiert". Diese Hinweise deuten oft auf ein stark verändertes Produkt hin.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">📱</span>
          <div class="guide-checklist__body">
            <strong>Öffnen Sie die App und scannen Sie das Produkt</strong>
            <p>Verwenden Sie die Kamera, um den Barcode zu scannen, oder laden Sie ein Foto des Etiketts hoch. Die App zeigt Ihnen sofort die Zutatenliste und die enthaltenen Zusatzstoffe an.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🔍</span>
          <div class="guide-checklist__body">
            <strong>Überprüfen Sie drei Schlüsselpunkte</strong>
            <p>Wie viele <strong>Zusatzstoffe</strong> sind aufgeführt (vor allem E-Nummern)? Wie <strong>lang und komplex</strong> ist die Zutatenliste? Wie viel <strong>Zucker, Salz und Fett</strong> wird angegeben?</p>
          </div>
        </li>
      </ul>

      <div class="guide-insight">
        Wenn Sie auf mehr als eine Frage mit „viele" oder „lang/kompliziert" antworten, haben Sie es wahrscheinlich mit einem stark verarbeiteten Lebensmittel zu tun.
      </div>
    </section>

