    <section id="zuccheri-nascosti" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">1</div>
        <h2 class="guide-section-title">Sugars: the 10 hidden names on labels</h2>
      </div>

      <p>
        Sugar doesn't always go by the name "sugar". Companies use dozens of variations to spread it across multiple entries in the ingredients list — making it look less significant. Once you know these names, you stop being fooled.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🔍</span>
          <div class="guide-checklist__body">
            <strong>The 10 most common aliases</strong>
            <p>
              <strong>Glucose-fructose syrup</strong> (the worst: metabolised differently from table sugar) ·
              <strong>Maltodextrins</strong> (starch broken down almost completely) ·
              <strong>Dextrose</strong> ·
              <strong>Fructose</strong> ·
              <strong>Concentrated cane juice</strong> ·
              <strong>Corn syrup</strong> ·
              <strong>Rice syrup</strong> ·
              <strong>Dehydrated honey</strong> ·
              <strong>Coconut sugar</strong> ·
              <strong>Agave nectar</strong>
            </p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">⚠️</span>
          <div class="guide-checklist__body">
            <strong>The "no added sugars" trap</strong>
            <p>A "100% fruit with no added sugars" juice can contain 10–12 g of sugars per 100 ml — more than a Coca-Cola (10.6 g/100 ml). The fructose from concentrated juice is chemically identical to added sugar.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">📊</span>
          <div class="guide-checklist__body">
            <strong>The threshold to keep in mind</strong>
            <p>The WHO recommends keeping free sugars (added + from juices) below <strong>10% of daily calories</strong> — about 50 g per day for an adult on 2000 kcal. Preferably below 5% (25 g).</p>
          </div>
        </li>
      </ul>
    </section>

