    <!-- SECTION 3 -->
    <section id="additivi-controversi" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">3</div>
        <h2 class="guide-section-title">Controversial additives: the E codes to know</h2>
      </div>

      <p>
        Most E codes on a label are safe. But some categories have a more significant risk profile, especially for children and regular consumers. It's not about avoiding them entirely, but about being informed.
      </p>

      <div class="rating-grid">
        <div class="rating-block rating-block--red">
          <div class="rating-block__dot">🔴</div>
          <div class="rating-block__body">
            <strong>Azo dyes — maximum caution with children</strong>
            <ul>
              <li>E102 (Tartrazine), E104 (Quinoline Yellow), E110 (Sunset Yellow FCF)</li>
              <li>E122 (Carmoisine), E124 (Ponceau 4R), E129 (Allura Red AC)</li>
              <li>EFSA study 2007: link to hyperactivity in children. In Europe a mandatory label warning is required: "may have an adverse effect on activity and attention in children"</li>
            </ul>
          </div>
        </div>
        <div class="rating-block rating-block--yellow">
          <div class="rating-block__dot">🟡</div>
          <div class="rating-block__body">
            <strong>Nitrates and nitrites — processed meat</strong>
            <ul>
              <li>E249–E252 (sodium and potassium nitrites and nitrates)</li>
              <li>Used as preservatives in cured meats, hot dogs, bacon, frankfurters</li>
              <li>In the presence of animal proteins they form nitrosamines, classified as probable carcinogens (IARC Group 2A). Limiting processed meat consumption is the key recommendation</li>
            </ul>
          </div>
        </div>
        <div class="rating-block rating-block--yellow">
          <div class="rating-block__dot">🟡</div>
          <div class="rating-block__body">
            <strong>Intense sweeteners — regular consumption</strong>
            <ul>
              <li>E950 (Acesulfame K), E951 (Aspartame), E952 (Cyclamate), E954 (Saccharin), E955 (Sucralose)</li>
              <li>Safe at approved doses for adults. Recent studies suggest possible effects on the gut microbiome with high chronic consumption</li>
              <li>The WHO (2023) expressed caution about chronic use for weight control: not recommended as a long-term sugar substitute</li>
            </ul>
          </div>
        </div>
      </div>
    </section>

