    <section id="regola-del-5" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">5</div>
        <h2 class="guide-section-title">The rule of 5: the fastest filter</h2>
      </div>

      <p>
        You don't need to become a biochemistry expert to make better choices. The "rule of 5" is a practical filter you can apply in a few seconds standing in front of the shelf.
      </p>

      <ol class="guide-step-list">
        <li>
          <span class="guide-step-num">1</span>
          <div class="guide-step-body">
            <strong>More than 5 ingredients? Start reading carefully</strong>
            <p>It's not an absolute rule (a good artisan granola may have 7–8), but it's a first filter: most simple, minimally processed products stay below 5 ingredients.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">2</span>
          <div class="guide-step-body">
            <strong>Sugars above 5 g/100 g? Check if it's the first or second ingredient</strong>
            <p>5 g/100 g is the EU threshold for "low sugar content". Above that it's just information — not a ban — but it signals you should check where those sugars are coming from.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">3</span>
          <div class="guide-step-body">
            <strong>Salt above 0.5 g/100 g in a sweet product? A sign of ultra-processing</strong>
            <p>Cookies, cereals and sweet snacks shouldn't contain much salt. When they do, it often serves to mask the artificial taste of sugar substitutes or to compensate for the lack of real ingredients.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">4</span>
          <div class="guide-step-body">
            <strong>More than 5 E additives? The product is probably ultra-processed</strong>
            <p>It's not the type of E that counts here, it's the quantity. A product with 7–8 different additives (colorings + emulsifiers + sweeteners + preservatives + flavourings) doesn't need all that chemistry if the quality of the base ingredients is high.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">5</span>
          <div class="guide-step-body">
            <strong>Don't recognise 5 or more ingredients? Put it back on the shelf</strong>
            <p>If you don't know what "maltodextrins", "calcium caseinate", "modified tapioca starch", "E471" and "nature-identical flavourings" are — and they all appear in the same product — you've found an ultra-processed food to limit.</p>
          </div>
        </li>
      </ol>

      <div class="guide-insight">
        <strong>Remember:</strong> the rule of 5 is a guide, not a verdict. An excellent wholegrain pasta with certified ingredients may have more than 5; a cheap snack cake may just scrape under. Use it as a starting point, not your only criterion.
      </div>
    </section>

