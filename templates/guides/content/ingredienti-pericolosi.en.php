    <section id="grassi-da-evitare" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">2</div>
        <h2 class="guide-section-title">Fats to avoid: hydrogenated and trans fats</h2>
      </div>

      <p>
        Not all fats are equal. Trans fatty acids (TFAs), produced by the industrial hydrogenation of vegetable oils, are associated with increased cardiovascular risk and have been banned or heavily restricted in many countries.
      </p>

      <div class="signal-cards">
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🚩</span>
          <div class="signal-card__content">
            <strong>Hydrogenated and partially hydrogenated fats</strong>
            <p>Look on the label for: "partially hydrogenated vegetable oil", "hydrogenated vegetable fat", "shortening". In Europe they have been banned above 2 g/100 g of fat since 2021, but some exemptions remain. Avoid them regardless.</p>
          </div>
        </div>
        <div class="signal-card signal-card--warning">
          <span class="signal-card__emoji">⚠️</span>
          <div class="signal-card__content">
            <strong>Palm oil: not trans, but...</strong>
            <p>Palm oil is saturated but not hydrogenated. The concern is different: during refining at high temperatures, potentially carcinogenic compounds form (3-MCPD, glycidol). It's not banned, but worth limiting.</p>
          </div>
        </div>
        <div class="signal-card signal-card--ok">
          <span class="signal-card__emoji">✅</span>
          <div class="signal-card__content">
            <strong>Fats that are fine</strong>
            <p>Extra-virgin olive oil, linseed oil, nuts and seeds, avocado, fats from oily fish. These contain monounsaturated fatty acids and omega-3s that have documented protective effects.</p>
          </div>
        </div>
      </div>
    </section>

