    <!-- SECTION 4 -->
    <section id="sodio-nascosto" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">4</div>
        <h2 class="guide-section-title">Hidden sodium: beyond the salt shaker</h2>
      </div>

      <p>
        Excess sodium raises blood pressure and increases cardiovascular risk. The problem is that around 75% of sodium in a Western diet doesn't come from what you add during cooking — it's already in the packaged product.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🧂</span>
          <div class="guide-checklist__body">
            <strong>The main sources of hidden sodium</strong>
            <p>Bread (the leading source of sodium in many diets), cheeses, cured meats, ready meals, jarred sauces, stock cubes and bouillon, soy sauce, ketchup, mayonnaise, salted snacks, industrial breakfast cereals.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🔢</span>
          <div class="guide-checklist__body">
            <strong>How to convert sodium to salt on the label</strong>
            <p>If the label shows "sodium" instead of "salt": multiply the value by 2.5. Example: 0.6 g of sodium = 1.5 g of salt. The WHO limit is 5 g of salt per day (about 2 g of sodium).</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🏷️</span>
          <div class="guide-checklist__body">
            <strong>Additives that contain sodium (not just salt)</strong>
            <p>Monosodium glutamate (E621), sodium benzoate (E211), sodium nitrite (E250), sodium bicarbonate, disodium phosphate — all contribute to total sodium intake, even if they don't appear as "salt" on the label.</p>
          </div>
        </li>
      </ul>
    </section>

