    <!-- SECTION 4 -->
    <section id="sale-nascosto" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">4</div>
        <h2 class="guide-section-title">Le sel caché : où se cache-t-il vraiment</h2>
      </div>

      <p>
        Le sel ne se trouve pas seulement dans les produits salés. Dans de nombreux aliments apparemment sucrés — biscuits, céréales, pain — la teneur en sel est étonnamment élevée.
      </p>

      <div class="signal-cards">
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🍞</span>
          <div class="signal-card__content">
            <strong>Pain et produits de boulangerie</strong>
            <p>Deux tranches de pain industriel peuvent contenir 0,5–0,8 g de sel. Multipliez par 4 repas par jour, et vous atteignez déjà la moitié de la limite journalière recommandée (5 g).</p>
          </div>
        </div>
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🥣</span>
          <div class="signal-card__content">
            <strong>Céréales du matin</strong>
            <p>Même celles « saines » et « complètes » peuvent contenir 0,8–1,2 g de sel pour 100 g — plus que beaucoup de snacks salés. Lire l'étiquette dans ce cas réserve presque toujours une surprise.</p>
          </div>
        </div>
        <div class="signal-card signal-card--warning">
          <span class="signal-card__emoji">🫙</span>
          <div class="signal-card__content">
            <strong>Sauces, condiments et assaisonnements</strong>
            <p>La sauce tomate prête à l'emploi, la mayonnaise, les vinaigrettes : elles peuvent contenir entre 1 et 3 g de sel pour 100 g. Un des cas où le fait maison bat toujours l'industriel.</p>
          </div>
        </div>
      </div>

      <div class="guide-insight">
        <strong>À noter :</strong> sur l'étiquette, vous pouvez trouver « sodium » plutôt que « sel ». Pour convertir : <strong>sel = sodium × 2,5</strong>.
      </div>
    </section>

