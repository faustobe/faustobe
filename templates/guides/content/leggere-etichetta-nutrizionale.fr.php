    <!-- SECTION 6 -->
    <section id="checklist" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">6</div>
        <h2 class="guide-section-title">Liste de contrôle rapide à utiliser au supermarché</h2>
      </div>

      <p>
        Vous n'avez pas besoin de tout faire à chaque fois. Commencez par les vérifications les plus rapides et ajoutez les autres au fur et à mesure que cela devient une habitude.
      </p>

      <ol class="guide-step-list">
        <li>
          <span class="guide-step-num">1</span>
          <div class="guide-step-body">
            <strong>Regardez le premier ingrédient</strong>
            <p>Est-ce un vrai aliment (farine, lait, tomate, huile) ? Bien. Est-ce un sucre, un sirop ou une graisse transformée ? Vérifiez si vous avez une alternative.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">2</span>
          <div class="guide-step-body">
            <strong>Comptez les ingrédients</strong>
            <p>Moins de 5 : excellent. De 5 à 10 : acceptable. Plus de 10–12 : vérifiez attentivement ce qu'ils contiennent.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">3</span>
          <div class="guide-step-body">
            <strong>Vérifiez les codes E</strong>
            <p>Peu ou pas du tout ? Bien. Beaucoup (surtout colorants, édulcorants, conservateurs) ? Le produit est très transformé.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">4</span>
          <div class="guide-step-body">
            <strong>Regardez le sel et les sucres pour 100 g</strong>
            <p>Sel supérieur à 1,5 g/100 g = élevé. Sucres supérieurs à 22,5 g/100 g = élevé. Graisses saturées supérieures à 5 g/100 g = élevé.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">5</span>
          <div class="guide-step-body">
            <strong>Ignorez les allégations en grand sur la face avant</strong>
            <p>« Complet », « naturel », « 0 % de matières grasses » ne veulent presque jamais dire ce qu'ils semblent dire. Faites toujours confiance à la liste des ingrédients, pas aux mentions promotionnelles.</p>
          </div>
        </li>
      </ol>

      <div class="guide-insight">
        <strong>L'astuce la plus simple :</strong> si vous reconnaissez tous les ingrédients et pourriez les cuisiner à la maison, vous êtes sur la bonne voie. Sinon, vous avez trouvé un ultra-transformé.
      </div>
    </section>

