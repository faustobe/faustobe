    <!-- SECTION 1 -->
    <section id="ingredienti" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">1</div>
        <h2 class="guide-section-title">La liste des ingrédients : par où commencer</h2>
      </div>

      <p>
        Par la loi, les ingrédients sont listés en <strong>ordre décroissant de poids</strong> : ce qui apparaît en premier est présent en plus grande quantité. C'est le point de départ le plus important de l'étiquette.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🥇</span>
          <div class="guide-checklist__body">
            <strong>Le premier ingrédient compte le plus</strong>
            <p>Si le premier ingrédient est « sucre », « sirop de glucose » ou « huile de palme », le produit est construit autour de cet élément. Dans les biscuits au chocolat, le chocolat n'apparaît souvent qu'en quatrième ou cinquième position.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">📏</span>
          <div class="guide-checklist__body">
            <strong>Longueur de la liste = degré de transformation</strong>
            <p>Pain fait maison : farine, eau, sel, levure — 4 ingrédients. Pain industriel du rayon : il peut en compter 15 ou plus. La longueur n'est pas un crime en soi, mais c'est toujours un signal à prendre en compte.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🔍</span>
          <div class="guide-checklist__body">
            <strong>Repérez les noms difficiles à prononcer</strong>
            <p>Des termes comme « amidon modifié », « mono- et diglycérides d'acides gras », « carboxymethyl cellulose » ou « hydrogéné » indiquent des ingrédients d'origine industrielle, souvent absents des recettes faites maison.</p>
          </div>
        </li>
      </ul>

      <div class="guide-insight">
        <strong>Règle pratique :</strong> si vous ne trouveriez pas tous les ingrédients dans une épicerie ordinaire, le produit est probablement très transformé.
      </div>
    </section>

    <hr class="guide-divider" />

    <!-- SECTION 2 -->
    <section id="tabella-nutrizionale" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">2</div>
        <h2 class="guide-section-title">Le tableau nutritionnel : ce qu'il faut regarder (et ignorer)</h2>
      </div>

      <p>
        Le tableau nutritionnel est obligatoire sur presque tous les produits emballés. Il contient beaucoup d'informations, mais trois valeurs suffisent pour comprendre l'essentiel.
      </p>

      <div class="signal-cards">
        <div class="signal-card signal-card--ok">
          <span class="signal-card__emoji">✅</span>
          <div class="signal-card__content">
            <strong>Sucres : moins de 5 g pour 100 g</strong>
            <p>Des valeurs inférieures à 5 g/100 g indiquent un produit à faible teneur en sucres. Entre 5 et 22,5 g c'est moyen ; au-dessus de 22,5 g c'est élevé. Pour les boissons, le seuil « faible » est de 2,5 g/100 ml.</p>
          </div>
        </div>
        <div class="signal-card signal-card--warning">
          <span class="signal-card__emoji">⚠️</span>
          <div class="signal-card__content">
            <strong>Sel : moins de 0,3 g pour 100 g</strong>
            <p>En dessous de 0,3 g/100 g c'est faible, au-dessus de 1,5 g/100 g c'est élevé. De nombreux produits salés comme les crackers, les fromages et la charcuterie dépassent facilement 1 g/100 g. Attention : le sodium indiqué sur l'étiquette doit être multiplié par 2,5 pour obtenir la valeur en sel.</p>
          </div>
        </div>
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🚩</span>
          <div class="signal-card__content">
            <strong>Graisses saturées : moins de 1,5 g pour 100 g</strong>
            <p>C'est le type de graisse à surveiller le plus attentivement. Des valeurs supérieures à 5 g/100 g sont considérées comme élevées. Les produits de boulangerie industrielle, les gâteaux et les plats préparés les dépassent souvent largement.</p>
          </div>
        </div>
      </div>
    </section>

    <hr class="guide-divider" />

    <!-- SECTION 3 -->
    <section id="porzioni" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">3</div>
        <h2 class="guide-section-title">Portions vs 100 g : l'astuce qui prête à confusion</h2>
      </div>

      <p>
        Les valeurs nutritionnelles sont souvent indiquées à la fois pour 100 g et « par portion ». Les entreprises ont tendance à mettre en avant les valeurs par portion, qui semblent plus faibles — mais les portions sont souvent sous-estimées.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🎭</span>
          <div class="guide-checklist__body">
            <strong>Céréales du matin : portion déclarée 30 g, mais vous en mangez 60</strong>
            <p>Si l'emballage indique 10 g de sucres par portion (30 g) et que vous en mangez un bol de 60 g, vous consommez 20 g de sucres — soit près de la moitié du seuil journalier recommandé.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">📊</span>
          <div class="guide-checklist__body">
            <strong>Utilisez toujours les valeurs pour 100 g pour comparer</strong>
            <p>Vous voulez savoir quelle pâte est la meilleure entre deux marques ? Comparez les valeurs pour 100 g, pas par portion (qui varie d'une marque à l'autre). C'est la seule comparaison équitable.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🧮</span>
          <div class="guide-checklist__body">
            <strong>Calculez la portion réelle</strong>
            <p>Regardez combien de portions contient l'emballage. Si un sachet de chips contient « 3,5 portions » et que vous le mangez entier, multipliez toutes les valeurs par 3,5.</p>
          </div>
        </li>
      </ul>
    </section>

