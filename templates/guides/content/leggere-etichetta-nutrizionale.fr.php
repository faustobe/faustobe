    <!-- SECTION 5 -->
    <section id="claim" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">5</div>
        <h2 class="guide-section-title">Les allégations nutritionnelles : ce qu'elles signifient vraiment</h2>
      </div>

      <p>
        Les allégations nutritionnelles sont les mentions comme « sans matières grasses », « light », « riche en fibres » ou « 0 % de sucres » qui apparaissent en grand sur la face avant de l'emballage. Elles sont réglementées par la loi, mais peuvent induire en erreur.
      </p>

      <div class="rating-grid">
        <div class="rating-block rating-block--yellow">
          <div class="rating-block__dot">🏷️</div>
          <div class="rating-block__body">
            <strong>« Sans sucres ajoutés »</strong>
            <ul>
              <li>Ne signifie pas sans sucres : peut en contenir des naturels (fructose du jus, lactose)</li>
              <li>Peut contenir des édulcorants artificiels ou des polyols</li>
              <li>N'est pas équivalent à « sain » ou « adapté aux diabétiques »</li>
            </ul>
          </div>
        </div>
        <div class="rating-block rating-block--yellow">
          <div class="rating-block__dot">🏷️</div>
          <div class="rating-block__body">
            <strong>« Light » ou « allégé »</strong>
            <ul>
              <li>Signifie réduit de 30 % par rapport au produit de référence</li>
              <li>Si le produit de base était très calorique, le light l'est encore beaucoup</li>
              <li>Souvent la graisse retirée est remplacée par des sucres ou des épaississants</li>
            </ul>
          </div>
        </div>
        <div class="rating-block rating-block--yellow">
          <div class="rating-block__dot">🏷️</div>
          <div class="rating-block__body">
            <strong>« Riche en fibres » ou « source de protéines »</strong>
            <ul>
              <li>Requiert des seuils minimaux légaux (fibres : min. 6 g/100 g ; protéines : min. 20 % des calories)</li>
              <li>Ne dit rien sur les autres ingrédients : un biscuit « riche en fibres » peut contenir beaucoup de sucre</li>
              <li>Lire toujours la liste des ingrédients, pas seulement l'allégation</li>
            </ul>
          </div>
        </div>
      </div>
    </section>

