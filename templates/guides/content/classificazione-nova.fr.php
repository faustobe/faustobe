    <!-- SECTION 1 -->
    <section id="cosa-e-nova" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">1</div>
        <h2 class="guide-section-title">Qu'est-ce que la classification NOVA</h2>
      </div>

      <p>
        NOVA est un système de classification des aliments développé par le groupe du professeur Carlos Monteiro à l'Université de São Paulo (Brésil) et adopté aujourd'hui par l'OMS, la FAO et de nombreuses études épidémiologiques mondiales.
      </p>

      <p>
        Contrairement aux systèmes basés sur les nutriments (comme le Nutri-Score, qui évalue calories, graisses, sucres et fibres), <strong>NOVA classe en fonction du degré de transformation industrielle</strong> : non pas « combien de calories a-t-il », mais « combien d'industrie est entrée dans sa production ».
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">💡</span>
          <div class="guide-checklist__body">
            <strong>Pourquoi la transformation compte</strong>
            <p>Les procédés industriels modernes peuvent altérer la structure des fibres, la biodisponibilité des nutriments, le microbiome intestinal et la réponse hormonale à la satiété — indépendamment de la composition nutritionnelle finale du produit.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">📋</span>
          <div class="guide-checklist__body">
            <strong>4 groupes, pas une échelle continue</strong>
            <p>NOVA divise tous les aliments en 4 groupes distincts. Ce n'est pas une échelle de 1 à 10, mais une classification catégorielle. Chaque groupe a des caractéristiques bien définies.</p>
          </div>
        </li>
      </ul>
    </section>

    <hr class="guide-divider" />

    <!-- SECTION 2 -->
    <section id="gruppo-1" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">2</div>
        <h2 class="guide-section-title">Groupe 1 — Aliments non transformés ou peu transformés</h2>
      </div>

      <p>
        Ce sont des aliments d'origine végétale ou animale qui n'ont subi aucune transformation industrielle significative, ou seulement des procédés physiques minimaux (séchage, mouture, réfrigération, pasteurisation) qui n'altèrent pas la nature de l'aliment.
      </p>

      <div class="signal-cards">
        <div class="signal-card signal-card--ok">
          <span class="signal-card__emoji">🥦</span>
          <div class="signal-card__content">
            <strong>Exemples du Groupe 1</strong>
            <p>Fruits et légumes frais ou surgelés, viandes et poissons frais ou surgelés sans additifs, œufs, légumineuses sèches, céréales en grain (riz, avoine, épeautre), farine complète, lait frais pasteurisé, yaourt nature sans additifs, thé, café, eau.</p>
          </div>
        </div>
        <div class="signal-card signal-card--ok">
          <span class="signal-card__emoji">✅</span>
          <div class="signal-card__content">
            <strong>Objectif pratique : la base de l'alimentation</strong>
            <p>Les aliments du Groupe 1 devraient constituer la majeure partie de chaque repas. Pas exclusivement — on cuisine avec les ingrédients du Groupe 2 — mais la majorité de ce qui finit dans l'assiette.</p>
          </div>
        </div>
      </div>
    </section>

    <hr class="guide-divider" />

    <!-- SECTION 3 -->
    <section id="gruppo-2" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">3</div>
        <h2 class="guide-section-title">Groupe 2 — Ingrédients culinaires transformés</h2>
      </div>

      <p>
        Ce sont des substances extraites d'aliments du Groupe 1 ou de la nature, utilisées en cuisine pour cuire, assaisonner et préparer les aliments. On ne les consomme pas seuls mais en association avec le Groupe 1.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🫙</span>
          <div class="guide-checklist__body">
            <strong>Exemples du Groupe 2</strong>
            <p>Huile d'olive et autres huiles végétales, beurre, vinaigre, sel, sucre, miel, farine blanche, amidon de maïs pur, sirop d'érable. Le lait en poudre et les fromages affinés traditionnels appartiennent également au Groupe 2.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">✅</span>
          <div class="guide-checklist__body">
            <strong>Utilisez-les avec modération</strong>
            <p>Ils ne sont pas problématiques en eux-mêmes, mais le sucre, le sel et les graisses saturées en excès restent défavorables. Le fait qu'ils soient Groupe 2 ne les rend pas « illimités ».</p>
          </div>
        </li>
      </ul>
    </section>

    <hr class="guide-divider" />

    <!-- SECTION 4 -->
    <section id="gruppo-3" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">4</div>
        <h2 class="guide-section-title">Groupe 3 — Aliments transformés</h2>
      </div>

      <p>
        Ce sont des produits obtenus en ajoutant des ingrédients du Groupe 2 à des aliments du Groupe 1, avec des procédés comme la conservation au sel, le fumage, la fermentation ou l'affinage. Reconnaissables parce qu'ils ne contiennent souvent que 2 à 3 ingrédients.
      </p>

      <div class="signal-cards">
        <div class="signal-card signal-card--warning">
          <span class="signal-card__emoji">⚠️</span>
          <div class="signal-card__content">
            <strong>Exemples du Groupe 3</strong>
            <p>Légumes et légumineuses en conserve (avec eau et sel), fruits confits et au sirop, noix grillées et salées, anchois et sardines à l'huile, jambon cru de qualité, fromages traditionnels (parmesan, pecorino), pain artisanal, vin, bière artisanale, huile extra vierge avec acidifiant.</p>
          </div>
        </div>
        <div class="signal-card signal-card--ok">
          <span class="signal-card__emoji">✅</span>
          <div class="signal-card__content">
            <strong>Acceptables dans l'alimentation quotidienne</strong>
            <p>Le Groupe 3 n'est pas à éviter. De nombreux aliments traditionnels et culturellement importants appartiennent à ce groupe. Ils peuvent toutefois être riches en sel (conserves, charcuteries) ou en sucres (fruits au sirop) : évaluez en fonction de la quantité.</p>
          </div>
        </div>
      </div>
    </section>

    <hr class="guide-divider" />

    <!-- SECTION 5 -->
    <section id="gruppo-4" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">5</div>
        <h2 class="guide-section-title">Groupe 4 — Ultra-transformés : le groupe à limiter</h2>
      </div>

      <p>
        Les ultra-transformés sont des formulations industrielles qui ne contiennent pas d'aliments entiers ou qui n'en utilisent qu'une infime partie. Ils sont construits avec des ingrédients extraits, purifiés ou modifiés chimiquement, plus des additifs technologiques en grandes quantités.
      </p>

      <p>
        Le signal clé : ils contiennent des ingrédients qu'<strong>on ne trouve pas dans une cuisine domestique</strong>.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🚩</span>
          <div class="guide-checklist__body">
            <strong>Ingrédients typiques du Groupe 4</strong>
            <p>Protéines hydrolysées, amidons modifiés, sirop de glucose-fructose, huiles hydrogénées, arômes « nature-identiques », colorants artificiels, édulcorants intenses (aspartame, saccharine, sucralose), émulsifiants (E471, E472), stabilisants, agents levants chimiques multiples.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">📦</span>
          <div class="guide-checklist__body">
            <strong>Exemples courants</strong>
            <p>Gâteaux industriels, céréales sucrées du matin, boissons gazeuses et energy drinks, pain de mie avec additifs, saucisses et charcuteries reconstituées, nuggets et bâtonnets de poisson, yaourts aromatisés avec épaississants, plats surgelés prêts à l'emploi, pizza industrielle, sauces en sachet, bouillons cubes avec exhausteurs de goût.</p>
          </div>
        </li>
      </ul>

      <div class="guide-insight">
        <strong>Données clés :</strong> en France, environ 30–35 % des calories journalières proviennent d'ultra-transformés. Au Royaume-Uni et aux États-Unis, ce pourcentage dépasse 50–60 %. La recherche montre qu'une consommation supérieure à 20 % des calories journalières provenant de NOVA 4 est déjà associée à des risques pour la santé.
      </div>
    </section>

