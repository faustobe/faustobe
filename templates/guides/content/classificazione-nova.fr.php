    <!-- SECTION 6 -->
    <section id="nova-in-pratica" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">6</div>
        <h2 class="guide-section-title">NOVA dans les courses au quotidien</h2>
      </div>

      <p>
        Appliquer NOVA au supermarché ne demande pas d'apprendre par cœur les catégories. Quelques questions mentales suffisent pendant la lecture de l'étiquette.
      </p>

      <ol class="guide-step-list">
        <li>
          <span class="guide-step-num">1</span>
          <div class="guide-step-body">
            <strong>« Pourrais-je le cuisiner à la maison avec des ingrédients normaux ? »</strong>
            <p>Si la réponse est oui, c'est probablement Groupe 1, 2 ou 3. Si non (parce qu'il utilise des ingrédients qu'on ne trouve pas dans les épiceries ordinaires), c'est presque certainement Groupe 4.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">2</span>
          <div class="guide-step-body">
            <strong>Cherchez les « mots révélateurs » du Groupe 4</strong>
            <p>« Amidon modifié », « protéines de soja isolées », « maltodextrines », « sirop de », « arômes complexes », « huile de palme hydrogénée », « caséinate de sodium ». S'ils apparaissent, vous êtes dans le Groupe 4.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">3</span>
          <div class="guide-step-body">
            <strong>Règle pratique : plus de la moitié de l'assiette depuis le Groupe 1</strong>
            <p>Il ne s'agit pas d'éliminer chaque ultra-transformé. L'objectif est que légumes, légumineuses, céréales en grain, œufs et viandes/poissons non transformés constituent la base de vos repas principaux.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">4</span>
          <div class="guide-step-body">
            <strong>Utilisez l'application en cas de doute</strong>
            <p>E-Codes Reader affiche la classification NOVA pour chaque produit scanné. Dans les cas où l'étiquette est ambiguë, l'application vous donne une réponse en une seconde.</p>
          </div>
        </li>
      </ol>

