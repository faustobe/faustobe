    <section id="piu-vulnerabili" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">1</div>
        <h2 class="guide-section-title">Perché bambini e anziani sono più vulnerabili</h2>
      </div>

      <p>
        La stessa dose di additivo che un adulto sano metabolizza senza problemi può avere effetti diversi su un bambino piccolo o su una persona anziana. Non è allarmismo: è fisiologia.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">👶</span>
          <div class="guide-checklist__body">
            <strong>Bambini: peso corporeo basso, sistemi non maturi</strong>
            <p>La dose giornaliera accettabile (DGA) degli additivi è calcolata in mg per kg di peso corporeo. Un bambino da 15 kg che mangia una merenda da 30 g con coloranti riceve una dose relativa 4–5 volte superiore a quella di un adulto da 70 kg. In più, il fegato e i reni dei bambini piccoli metabolizzano alcune sostanze più lentamente.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">👴</span>
          <div class="guide-checklist__body">
            <strong>Anziani: ridotta funzionalità renale ed epatica</strong>
            <p>Con l'età diminuisce la capacità di eliminare alcune sostanze. Farmaci multipli (polifarmacia) possono interagire con certi additivi. Il sodio in eccesso impatta di più su chi soffre di ipertensione o insufficienza renale.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🔬</span>
          <div class="guide-checklist__body">
            <strong>Effetti cumulativi: la dieta abituale conta più del singolo pasto</strong>
            <p>Un gelato con coloranti una volta al mese non è un problema. Se i bambini mangiano quotidianamente prodotti con E102, E110, E122 a colazione, a merenda, a pranzo e nella bevanda pomeridiana — l'effetto cumulativo diventa rilevante.</p>
          </div>
        </li>
      </ul>
    </section>

