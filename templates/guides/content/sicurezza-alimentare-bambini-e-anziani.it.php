          </div>
        </li>
        <li>
          <span class="guide-step-num">5</span>
          <div class="guide-step-body">
            <strong>Preferisci sempre il Gruppo 1–2 (NOVA) per i pasti principali</strong>
            <p>Verdure, legumi, uova, pesce, yogurt naturale, frutta intera. Sono la base più sicura per entrambe le categorie vulnerabili — senza bisogno di controllare etichette complesse.</p>
