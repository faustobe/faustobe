    <section id="anziani" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">3</div>
        <h2 class="guide-section-title">Cosa limitare negli anziani</h2>
      </div>

      <p>
        Per le persone anziane le priorità sono diverse rispetto ai bambini. Le principali criticità riguardano il sodio, i fosfati e certi conservanti.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🧂</span>
          <div class="guide-checklist__body">
            <strong>Sodio: il rischio principale</strong>
            <p>Ipertensione e insufficienza renale sono tra le condizioni più comuni negli anziani, e il sodio le aggrava. I piatti pronti, i salumi, i formaggi stagionati e i brodi industriali sono i principali veicoli. Obiettivo: meno di 5 g di sale al giorno, e meno per chi ha problemi renali o ipertensione documentata.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🦴</span>
          <div class="guide-checklist__body">
            <strong>Fosfati: interferiscono con calcio e ossa</strong>
            <p>I fosfati (E338–E341, E450–E452) sono comuni nei prodotti a base di carne lavorata, formaggi fusi, carne in scatola e piatti pronti. Un consumo elevato di fosfati di origine additiva interferisce con l'assorbimento del calcio — un problema per chi è a rischio osteoporosi.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">💊</span>
          <div class="guide-checklist__body">
            <strong>Interazioni con farmaci comuni</strong>
            <p>Alcune sostanze negli alimenti possono interagire con farmaci comuni negli anziani: la vitamina K nei vegetali a foglia verde con i warfarinici, il pompelmo con le statine, il succo di mirtillo con certi anticoagulanti. Non si tratta di evitare questi alimenti, ma di mantenere la dieta costante e segnalare le abitudini alimentari al medico.</p>
          </div>
        </li>
      </ul>
    </section>

