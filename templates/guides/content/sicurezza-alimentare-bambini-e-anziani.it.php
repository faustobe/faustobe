          <div class="guide-step-body">
            <strong>Per i bambini: scegli prodotti con pochi ingredienti riconoscibili</strong>
            <p>Più corta è la lista ingredienti, meglio è. Evita prodotti con più di 2–3 coloranti, dolcificanti artificiali, o la dicitura "può influire sull'attività e l'attenzione dei bambini".</p>
