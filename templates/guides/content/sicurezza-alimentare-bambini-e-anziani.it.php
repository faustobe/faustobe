    <section id="prodotti-bambini" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">4</div>
        <h2 class="guide-section-title">Prodotti "per bambini" da guardare con attenzione</h2>
      </div>

      <p>
        Il marketing "per bambini" è spesso il contrario di ciò che sembra. I prodotti con personaggi colorati, mascotte e scritte come "con vitamine" o "energia per crescere" sono spesso tra i più lavorati sullo scaffale.
      </p>

      <div class="signal-cards">
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🚩</span>
          <div class="signal-card__content">
            <strong>Cereali da colazione "per bambini"</strong>
            <p>Spesso contengono 25–35 g di zuccheri per 100 g (equivalente a 7–9 cucchiaini per 100 g), coloranti artificiali, aromi complessi e vitamine aggiunte per compensare la scarsa qualità nutrizionale degli ingredienti base. Alternativa: avena in fiocchi + frutta fresca + miele.</p>
          </div>
        </div>
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🚩</span>
          <div class="signal-card__content">
            <strong>Bevande "ai succhi di frutta"</strong>
            <p>Prodotti come "bevanda all'arancia" o "succo multifrutta" possono contenere solo il 10–30% di succo reale, il resto è acqua, zuccheri, acidificanti e coloranti. Non sono equivalenti ai succhi al 100%. Controlla sempre la percentuale di frutta in etichetta.</p>
          </div>
        </div>
        <div class="signal-card signal-card--warning">
          <span class="signal-card__emoji">⚠️</span>
          <div class="signal-card__content">
            <strong>Yogurt "per bambini" con frutta</strong>
            <p>Molti contengono più zucchero di uno yogurt adulto standard, più addensanti (pectina, amido modificato) e aromi artificiali. Lo yogurt greco naturale con frutta fresca aggiunta al momento è sempre una scelta migliore — e spesso costa meno.</p>
          </div>
        </div>
      </div>
    </section>

