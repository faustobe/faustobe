            <p>Privilegia prodotti con meno di 0,3 g di sale/100 g. Scegli tonno al naturale invece che all'olio di semi salato, verdure surgelate senza sale aggiunto, brodi fatti in casa o a basso contenuto di sodio.</p>
          </div>
        </li>
