      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">2</div>
        <h2 class="guide-section-title">Additivi critici per i bambini</h2>
      </div>

      <p>
        Non tutti i codici E sono problematici per i bambini. Le categorie più rilevanti, con evidenze scientifiche più robuste, sono queste.
      </p>

      <div class="rating-grid">
        <div class="rating-block rating-block--red">
          <div class="rating-block__dot">🔴</div>
          <div class="rating-block__body">
            <strong>Coloranti azoici — da evitare con i bambini</strong>
            <ul>
              <li>E102 (Tartrazina), E104 (Giallo chinolina), E110 (Giallo tramonto FCF)</li>
              <li>E122 (Carmoisina), E124 (Rosso Ponceau 4R), E129 (Rosso Allura AC)</li>
              <li>In Europa è obbligatorio scrivere sull'etichetta: <em>"può influire sull'attività e sull'attenzione dei bambini"</em></li>
              <li>Dove si trovano: caramelle colorate, bibite arancioni/rosse, gelatine, dolci industriali, cereali da colazione colorati</li>
            </ul>
          </div>
        </div>
        <div class="rating-block rating-block--yellow">
          <div class="rating-block__dot">🟡</div>
          <div class="rating-block__body">
            <strong>Dolcificanti intensivi — non approvati per bambini piccoli</strong>
            <ul>
              <li>E951 (Aspartame), E950 (Acesulfame K), E955 (Sucralosio), E954 (Saccarina)</li>
              <li>Non sono autorizzati negli alimenti specificamente destinati ai lattanti e ai bambini piccoli (Reg. UE 1333/2008)</li>
              <li>Dove si trovano spesso: yogurt "0% zucchero", bibite "light" o "zero", sciroppi per la tosse, caramelle senza zucchero, certi succhi di frutta</li>
            </ul>
          </div>
        </div>
        <div class="rating-block rating-block--yellow">
          <div class="rating-block__dot">🟡</div>
          <div class="rating-block__body">
            <strong>Caffeina — spesso nascosta</strong>
            <ul>
              <li>Non è un additivo E, ma è presente nelle energy drink, in certi soft drink e in snack "al caffè"</li>
              <li>L'EFSA raccomanda che i bambini non assumano più di 3 mg/kg/die di caffeina</li>
              <li>Una lattina da 250 ml di energy drink può contenere fino a 80 mg di caffeina — quasi il doppio del limite per un bambino da 15 kg</li>
            </ul>
          </div>
        </div>
      </div>
