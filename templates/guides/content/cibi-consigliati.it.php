    <!-- SEZIONE 3 -->
    <section id="proteine" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">3</div>
        <h2 class="guide-section-title">Proteine: le fonti migliori</h2>
      </div>

      <p>
        Le proteine di buona qualità si trovano sia nel mondo animale che vegetale. Il punto non è la fonte, ma il grado di lavorazione: un petto di pollo grigliato e un nugget di pollo sono due cose completamente diverse.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🐟</span>
          <div class="guide-checklist__body">
            <strong>Pesce e frutti di mare</strong>
            <p>Pesce fresco o surgelato (senza impanatura), sardine e sgombri in olio d'oliva, tonno al naturale, acciughe sotto sale (risciacquate). Il pesce azzurro è tra le fonti proteiche più economiche e nutrienti disponibili.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🥚</span>
          <div class="guide-checklist__body">
            <strong>Uova e latticini</strong>
            <p>Uova intere (non solo albumi), yogurt greco naturale, ricotta, fiocchi di latte, formaggi stagionati con moderazione. Evita sostituti industriali delle uova, yogurt "proteici" con addensanti e dolcificanti, formaggi fusi con fosfati.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🌿</span>
          <div class="guide-checklist__body">
            <strong>Legumi: la proteina vegetale più sottovalutata</strong>
            <p>Lenticchie, ceci, fagioli, piselli, soia in edamame. In scatola al naturale sono già cotti e si aggiungono in 30 secondi a qualsiasi pasto. Costano poco, sono ricchi di fibre e hanno un profilo amminoacidico ottimo se abbinati ai cereali.</p>
          </div>
        </li>
      </ul>
    </section>

