    <section id="spuntini" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">5</div>
        <h2 class="guide-section-title">Spuntini intelligenti: cosa tenere sempre a portata di mano</h2>
      </div>

      <p>
        Gli spuntini industriali (merendine, snack confezionati, barrette proteiche ultra-lavorate) sono tra i principali veicoli di ultra-processati nella dieta quotidiana. Ecco cosa tenerci al posto loro.
      </p>

      <ol class="guide-step-list">
        <li>
          <span class="guide-step-num">🥜</span>
          <div class="guide-step-body">
            <strong>Frutta secca al naturale</strong>
            <p>Noci, mandorle, nocciole, anacardi senza sale aggiunto. Una manciata (30 g) fornisce proteine, grassi buoni e minerali. Evita le versioni "tostate al miele" con sciroppi e additivi.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">🍎</span>
          <div class="guide-step-body">
            <strong>Frutta fresca intera</strong>
            <p>La frutta intera ha fibre che rallentano l'assorbimento degli zuccheri. Una mela, una pera, un kiwi — non il succo di frutta, che ha perso quasi tutte le fibre ed è classificato spesso come ultra-processato.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">🥛</span>
          <div class="guide-step-body">
            <strong>Yogurt greco naturale</strong>
            <p>Alto contenuto proteico, basso indice glicemico, nessun additivo. Aggiungi tu il miele o la frutta fresca se vuoi dolcezza — così controlli la quantità di zucchero.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">🧀</span>
          <div class="guide-step-body">
            <strong>Formaggio a pasta dura + cracker integrali</strong>
            <p>Un po' di parmigiano o pecorino su cracker integrali con lista ingredienti corta (farina integrale, olio, sale, lievito). Sazietà lunga, ingredienti semplici.</p>
          </div>
        </li>
      </ol>
    </section>

