    <section id="principio-base" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">1</div>
        <h2 class="guide-section-title">Il principio base: sostituire, non eliminare</h2>
      </div>

      <p>
        Non si tratta di fare diete rigide o di sentirsi in colpa ogni volta che si compra qualcosa di confezionato. Si tratta di conoscere le alternative — e di sceglierle quando sono disponibili e pratiche quanto il prodotto che sostituiscono.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🔄</span>
          <div class="guide-checklist__body">
            <strong>Stesso tempo, meno lavorazione</strong>
            <p>Yogurt naturale al posto di yogurt aromatizzato con addensanti. Riso integrale al posto del riso precotto in busta. Legumi in scatola al naturale al posto di piatto pronto con legumi. La praticità è quasi uguale, la qualità è diversa.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🎯</span>
          <div class="guide-checklist__body">
            <strong>Il 80/20 funziona</strong>
            <p>Se l'80% di quello che mangi è Gruppo 1–2 (vedi classificazione NOVA), il 20% di prodotti più lavorati non fa la differenza. La salute alimentare si misura sulla media settimanale, non sul singolo pasto.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">💡</span>
          <div class="guide-checklist__body">
            <strong>I cibi "consigliati" non sono tutti bio o costosi</strong>
            <p>Ceci in scatola, uova, farina integrale, yogurt bianco, avena, lenticchie, verdure di stagione, sardine in olio — sono tra gli alimenti più sani e costano meno di quasi ogni prodotto confezionato equivalente.</p>
          </div>
        </li>
      </ul>
    </section>

    <hr class="guide-divider" />

    <!-- SEZIONE 2 -->
    <section id="cereali" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">2</div>
        <h2 class="guide-section-title">Cereali e carboidrati: le scelte migliori</h2>
      </div>

      <p>
        I carboidrati non sono il problema — il problema è il grado di raffinazione e lavorazione. Un cereale integrale in chicco è radicalmente diverso da un cereale raffinato soffiato con sciroppo di zucchero.
      </p>

      <div class="signal-cards">
        <div class="signal-card signal-card--ok">
          <span class="signal-card__emoji">✅</span>
          <div class="signal-card__content">
            <strong>Cereali consigliati</strong>
            <p>
              <strong>Avena in fiocchi</strong> (senza ingredienti aggiunti) ·
              <strong>Riso integrale</strong> o semintegrale ·
              <strong>Farro in chicco</strong> ·
              <strong>Orzo perlato</strong> ·
              <strong>Grano saraceno</strong> ·
              <strong>Pasta integrale</strong> con sola semola integrale ·
              <strong>Pane di sola farina integrale</strong>, acqua, sale, lievito madre (massimo 4–5 ingredienti)
            </p>
          </div>
        </div>
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🚩</span>
          <div class="signal-card__content">
            <strong>Da limitare</strong>
            <p>Cereali da colazione con zucchero (anche quelli "integrali" con sciroppo), pane da cassetta con amidi modificati e conservanti, cracker con olio di palma e aromi, riso e pasta pre-cotti in busta con additivi, merendine e biscotti industriali.</p>
          </div>
        </div>
      </div>

      <div class="guide-insight">
        <strong>Tip rapido per i cereali da colazione:</strong> leggi la tabella nutrizionale. Se i zuccheri sono sopra 10 g/100 g, stai mangiando un prodotto da dessert — non una colazione sana.
      </div>
    </section>

