    <section id="grassi-buoni" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">4</div>
        <h2 class="guide-section-title">Grassi buoni: i più semplici da aggiungere</h2>
      </div>

      <p>
        I grassi non fanno ingrassare di per sé. Quelli di qualità — olio extravergine, noci, avocado, pesce grasso — sono tra i componenti più protettivi per il sistema cardiovascolare.
      </p>

      <div class="signal-cards">
        <div class="signal-card signal-card--ok">
          <span class="signal-card__emoji">🫒</span>
          <div class="signal-card__content">
            <strong>Fonti di grassi insaturi consigliati</strong>
            <p>Olio d'oliva extravergine (crudo o per cotture brevi), olio di lino crudo, noci, mandorle, nocciole, semi di lino, semi di chia, avocado, pesce azzurro (salmone, sgombro, sardine, tonno), uova intere.</p>
          </div>
        </div>
        <div class="signal-card signal-card--warning">
          <span class="signal-card__emoji">⚠️</span>
          <div class="signal-card__content">
            <strong>Con moderazione</strong>
            <p>Burro (saturo ma naturale), olio di cocco (ricco di saturi), latte intero. Non sono ultra-processati, ma un consumo eccessivo di grassi saturi rimane sfavorevole per la salute cardiovascolare.</p>
          </div>
        </div>
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🚩</span>
          <div class="signal-card__content">
            <strong>Grassi da evitare</strong>
            <p>Margarine con grassi parzialmente idrogenati, shortening vegetale, olio di palma raffinato in prodotti da forno industriali, panna vegetale con emulsionanti, oli di semi rettificati ad alte temperature.</p>
          </div>
        </div>
      </div>
    </section>

