      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">1</div>
        <h2 class="guide-section-title">Das Grundprinzip: ersetzen, nicht eliminieren</h2>
      </div>

      <p>
        Es geht nicht darum, strenge Di&auml;ten zu machen oder sich schuldig zu f&uuml;hlen, wenn man mal etwas Verpacktes kauft. Es geht darum, die Alternativen zu kennen &mdash; und sie zu w&auml;hlen, wenn sie genauso verf&uuml;gbar und praktisch sind wie das Produkt, das sie ersetzen.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🔄</span>
          <div class="guide-checklist__body">
            <strong>Gleicher Aufwand, weniger Verarbeitung</strong>
            <p>Naturjoghurt statt aromatisiertem Joghurt mit Verdickungsmitteln. Vollkornreis statt vorgekochtem Reis im Beutel. H&uuml;lsenfr&uuml;chte aus der Dose (im eigenen Saft) statt Fertiggericht mit H&uuml;lsenfr&uuml;chten. Die Praktik ist fast gleich, die Qualit&auml;t ist eine andere.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🎯</span>
          <div class="guide-checklist__body">
            <strong>Das 80/20-Prinzip funktioniert</strong>
            <p>Wenn 80 % des Gegessenen aus Gruppe 1&ndash;2 stammt (siehe NOVA-Klassifizierung), macht der 20%-Anteil st&auml;rker verarbeiteter Produkte keinen Unterschied. Die ern&auml;hrungsm&auml;&szlig;ige Gesundheit misst sich am wochendurchschnittlichen Verhalten, nicht an der einzelnen Mahlzeit.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">💡</span>
          <div class="guide-checklist__body">
            <strong>&bdquo;Empfohlene&ldquo; Lebensmittel sind nicht alle Bio oder teuer</strong>
            <p>Kichererbsen aus der Dose, Eier, Vollkornmehl, Naturjoghurt, Haferflocken, Linsen, saisonales Gem&uuml;se, Sardinen in &Ouml;l &mdash; das sind einige der gesundheitlichsten Lebensmittel und kosten weniger als fast jedes entsprechende verpackte Produkt.</p>
          </div>
        </li>
      </ul>
    </section>

