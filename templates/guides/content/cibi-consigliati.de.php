    <section id="spuntini" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">5</div>
        <h2 class="guide-section-title">Clevere Snacks: was man immer griffbereit haben sollte</h2>
      </div>

      <p>
        Industriesnacks (Fertigkekse, verpackte Snacks, stark verarbeitete Proteinriegel) sind einer der Hauptvektoren f&uuml;r Ultra-Verarbeitetes in der t&auml;glichen Ern&auml;hrung. Hier sind Alternativen.
      </p>

      <ol class="guide-step-list">
        <li>
          <span class="guide-step-num">🥜</span>
          <div class="guide-step-body">
            <strong>N&uuml;sse und Saaten natur</strong>
            <p>Waln&uuml;sse, Mandeln, Haseln&uuml;sse, Cashews ohne Salzzusatz. Eine Handvoll (30 g) liefert Proteine, gute Fette und Mineralstoffe. Versionen &bdquo;honigger&ouml;stet&ldquo; mit Sirups und Zus&auml;tzen meiden.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">🍎</span>
          <div class="guide-step-body">
            <strong>Frisches ganzes Obst</strong>
            <p>Ganzes Obst hat Ballaststoffe, die die Zuckeraufnahme verlangsamen. Ein Apfel, eine Birne, eine Kiwi &mdash; nicht Fruchtsaft, der fast alle Ballaststoffe verloren hat und oft als ultra-verarbeitet gilt.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">🥛</span>
          <div class="guide-step-body">
            <strong>Griechischer Naturjoghurt</strong>
            <p>Hoher Proteingehalt, niedriger glyk&auml;mischer Index, keine Zus&auml;tze. Honig oder frisches Obst nach Belieben selbst hinzuf&uuml;gen &mdash; so beh&auml;lt man die Kontrolle &uuml;ber die Zuckermenge.</p>
          </div>
        </li>
        <li>
          <span class="guide-step-num">🧀</span>
          <div class="guide-step-body">
            <strong>Hartkäse + Vollkorncracker</strong>
            <p>Etwas Parmesan oder Pecorino auf Vollknackerbrot mit kurzer Zutatenliste (Vollkornmehl, &Ouml;l, Salz, Hefe). S&auml;ttigt lange, einfache Zutaten.</p>
          </div>
        </li>
      </ol>
    </section>

