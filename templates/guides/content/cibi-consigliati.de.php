    <section id="cereali" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">2</div>
        <h2 class="guide-section-title">Getreide und Kohlenhydrate: die besten Entscheidungen</h2>
      </div>

      <p>
        Kohlenhydrate sind nicht das Problem &mdash; das Problem ist der Raffinationsgrad und die Verarbeitung. Ein Vollkorngetreide im ganzen Korn ist grundlegend verschieden von einem raffinierten gepufften Getreide mit Zuckersirup.
      </p>

      <div class="signal-cards">
        <div class="signal-card signal-card--ok">
          <span class="signal-card__emoji">✅</span>
          <div class="signal-card__content">
            <strong>Empfohlene Getreidesorten</strong>
            <p>
              <strong>Haferflocken</strong> (ohne zus&auml;tzliche Zutaten) &middot;
              <strong>Vollkornreis</strong> oder halbgem&auml;hlter Reis &middot;
              <strong>Dinkelvollkorn</strong> &middot;
              <strong>Graupen</strong> &middot;
              <strong>Buchweizen</strong> &middot;
              <strong>Vollkornnudeln</strong> aus reinem Vollkorngrie&szlig; &middot;
              <strong>Brot aus reinem Vollkornmehl</strong>, Wasser, Salz, Sauerteig (maximal 4&ndash;5 Zutaten)
            </p>
          </div>
        </div>
        <div class="signal-card signal-card--danger">
          <span class="signal-card__emoji">🚩</span>
          <div class="signal-card__content">
            <strong>Einzuschr&auml;nken</strong>
            <p>Fr&uuml;hst&uuml;cksflocken mit Zucker (auch die &bdquo;Vollkorn&ldquo;-Varianten mit Sirup), Toastbrot mit modifizierten St&auml;rken und Konservierungsmitteln, Cracker mit Palmfett und Aromen, vorgekochter Reis und vorgekochte Nudeln im Beutel mit Zus&auml;tzen, industrielles Geb&auml;ck und Kekse.</p>
          </div>
        </div>
      </div>

      <div class="guide-insight">
        <strong>Schnelltipp f&uuml;r Fr&uuml;hst&uuml;cksflocken:</strong> Schau auf die N&auml;hrwerttabelle. Wenn der Zuckergehalt &uuml;ber 10 g/100 g liegt, ist das ein Dessertprodukt &mdash; kein gesundes Fr&uuml;hst&uuml;ck.
      </div>
    </section>

