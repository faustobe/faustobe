    <section id="proteine" class="guide-section">
      <div class="guide-section-header">
        <div class="guide-section-number" aria-hidden="true">3</div>
        <h2 class="guide-section-title">Proteine: die besten Quellen</h2>
      </div>

      <p>
        Hochwertige Proteine findet man sowohl in tierischen als auch in pflanzlichen Quellen. Es kommt nicht auf die Quelle an, sondern auf den Verarbeitungsgrad: eine gegrillte H&uuml;hnerbrust und ein Chicken Nugget sind zwei v&ouml;llig verschiedene Dinge.
      </p>

      <ul class="guide-checklist">
        <li>
          <span class="guide-checklist__icon">🐟</span>
          <div class="guide-checklist__body">
            <strong>Fisch und Meeresfrüchte</strong>
            <p>Frischer oder tiefgefrorener Fisch (ohne Panade), Sardinen und Makrelen in Oliven&ouml;l, Thunfisch im eigenen Saft, Sardellen in Salz (abgesp&uuml;lt). Fettreicher Fisch geh&ouml;rt zu den g&uuml;nstigsten und n&auml;hrstoffreichsten Proteinquellen.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🥚</span>
          <div class="guide-checklist__body">
            <strong>Eier und Milchprodukte</strong>
            <p>Ganze Eier (nicht nur Eiwei&szlig;), griechischer Naturjoghurt, Ricotta, H&uuml;ttenk&auml;se, Hardk&auml;se in Ma&szlig;en. Industrielle Eiers&auml;tze, &bdquo;Protein&ldquo;-Joghurts mit Verdickungsmitteln und S&uuml;&szlig;ungsmitteln sowie Schmelzk&auml;se mit Phosphaten meiden.</p>
          </div>
        </li>
        <li>
          <span class="guide-checklist__icon">🌿</span>
          <div class="guide-checklist__body">
            <strong>H&uuml;lsenfr&uuml;chte: das untersch&auml;tzte pflanzliche Protein</strong>
            <p>Linsen, Kichererbsen, Bohnen, Erbsen, Soja als Edamame. Aus der Dose (im eigenen Saft) sind sie bereits gegart und lassen sich in 30 Sekunden zu jeder Mahlzeit hinzuf&uuml;gen. G&uuml;nstig, ballaststoffreich und mit einem ausgezeichneten Aminos&auml;ureprofil in Kombination mit Getreide.</p>
          </div>
        </li>
      </ul>
    </section>

